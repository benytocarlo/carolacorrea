	<h2>Mini Hamburgesas Saludables</h2>
      <h4>Ingredientes</h4>
      <p>• Ingredientes &quot;8 personas&quot;<br />
        • Masa para tapaditos:<br />
        • 3 tazas de harina<br />
        • 2 cucharadas de levadura<br />
        • 2 huevos<br />
        • 1 yema<br />
        • ¼ taza de aceite vegetal<br />
        • ¾ taza de leche<br />
        • Sal <br />
	</p>
        <h4> Relleno:</h4>
        <p>
          • 2 trutros de pavo Sopraval<br />
          • 1 yogurt natural<br />
          • 1 atado de albahaca<br />
          • Tomate – lechuga - peinillo<br />
        </p>
        <h4>Preparación<br />
    </h4>
      <p>• Comenzaremos con&nbsp; los panes. Deje en un bol la harina, haga un
        hueco al centro, deje la levadura previamente leudada, los huevos, el
        aceite, la leche, la sal y mezcle<br />
        <br />
        • Amase bastante hasta que quede una masa suave. Deje leudar 15 minutos.<br />
        <br />
        • Forme círculos pequeños y hornee a temperatura media alta por 15 minutos.<br />
        <br />
        • Para el relleno, cocine los trutros de pavo   al horno, desmenuce  y corte en trozos.<br />
        <br />
        • Mezcle el yogurt con la albahaca picada, sal y pimienta <br />
        <br />
        • Rellene los panes con el pavo,  tomate, pepinillo y la mezcla  de yogurt con albahaca<br />
      </p>
