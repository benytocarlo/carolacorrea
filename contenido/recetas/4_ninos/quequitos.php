	<h2>Quequitos</h2>
            <h4>Ingredientes</h4>
               <p>• Tiempo: 40 minutos<br />
               • Ingredientes &quot;12 personas&quot;<br />
               • 3 huevos<br />
               • 1 yogurt de vainilla <br />
               • 2,5 medidas de pote de yogurt de harina<br />
               • ¾ taza de azúcar light<br />
               • 2 cucharaditas de polvos de hornear<br />
               • ½ taza de aceite vegetal<br />
            • ½ taza de chocolate en polvo amargo</p>
         <h4>Preparación</h4>
               <p>• Mezcla la harina, con los polvos de hornear y la azúcar.<br />
                 <br />
                 • Aparte, mezcla batiendo los huevos, el yogurt y el aceite.<br />
                 <br />
                 • Incorpora a la mezcla el chocolate.<br />
                 <br />
                 • Junta los ingredientes secos y los húmedos formando una masa.<br />
                 <br />
                 • Llena cápsulas individuales hasta ¾ de su capacidad y lleva al horno por 20 minutos o hasta que suban y estén cocidos.
                 </p>
