	<h2>Galletones de Ajedrez</h2>
      <h4>Ingredientes</h4>
      <p>• 1 1/2 &nbsp;tazas de harina.<br />
        • 1/2 &nbsp;taza de margarina light.<br />
        • 1/2 taza de azúcar flor.<br />
        • 2 cucharadas de leche descremada.<br />
        • 2 cucharadas de cacao en polvo.</p>
      <h4>Preparación</h4>
      <p>• En un bowl juntar la harina, azúcar flor y la margarina cortada en cuadritos.<br />
        <br />
        • Hacer migas con la punta de los dedos, dividir la masa en 2, reserva una masa.<br />
        <br />
        • A la otra mitad de la masa agregar el cacao, amasa hasta unir.<br />
        <br />
        • Uslerear cada masa por separado y dejarla de un dedo de espesor.<br />
        <br />
        • Cortar en tiras de 1 dedo.<br />
        <br />
        • Poner una masa de chocolate, una blanca y otra de chocolate,  encima poner los colores intercalados, con lo que nos sobre de masa uslerear  y envolver lo que hicimos.<br />
        <br />
        •        Cortar con la ayuda de un cuchillo y hornear por 10 min a fuego medio.</p>
