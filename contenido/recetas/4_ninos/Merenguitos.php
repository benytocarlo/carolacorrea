	<h2>Merenguitos</h2>
      <h4>Ingredientes</h4>
      <p>•  1 taza de claras.<br />
        •  1 taza de azúcar granulada.<br />
        •  1 taza de azúcar flor<br />
        •  Lúcuma  el relleno
      </p>
      <h4>Preparación</h4>
        <p>• Bata las claras a punto de nieve.<br />
          • Agregue azúcar granulada en 3 partes sin dejar de batir, hasta que los cristales se disuelvan.<br />
          • Agregue el azúcar flor en 3 partes, dejar de batir<br />
          • Ponga el merengue en una manga pastelera con boquilla dentada y comience a formar pequeños volcancitos sobre un papel mantequilla montado en la lata del horno. <br />
          • Cocine a un  horno a temperatura baja (100°), por 1 hora, hasta que los merenguitos estén secos.<br />
          • Retírelos, déjelos enfriar y junte dos, pegados con lúcuma<br />
        </p>
