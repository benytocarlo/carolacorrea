	<h2>Galletones de Avena</h2>
      <h4>Ingredientes</h4>
      <p>• 12 cucharadas soperas rasas de harina común <br />
        • 8 cucharadas soperas de avena <br />
        • 2 cucharadas soperas de azúcar rubia<br />
        • 2 cucharadas de miel<br />
        • 1 taza de frutos secos &quot;nueces – almendras – pasas&quot;<br />
        • 1 huevo <br />
        • ¼  taza de aceite vegetal</p>
      <h4>Preparación<br />
      </h4>
      <p>• Mezcle los ingredientes secos en un bol<br />
        <br />
        • Agregue el huevo y el aceite<br />
        <br />
        • Tome una cucharada de masa, aplástela dándole forma de galletón y déjela en la lata de un horno <br />
        <br />
        • Hornee en horno mediano por 15 minutos, o hasta que se doren <br />
      </p>
