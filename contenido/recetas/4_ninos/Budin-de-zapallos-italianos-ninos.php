	<h2>Budín de Zapallos Italianos</h2>
      <h4>Ingredientes</h4>
      <p>• 3 zapallos italianos <br />
        • 3 huevos omega 3<br />
        • ½   taza de leche semi descremada<br />
        • 2 cucharadas de aceite de oliva<br />
        • 1 marraqueta remojada en leche  descremada </p>
      <h4>Preparación<br /></h4>
      <p>• Cocer los  zapallitos en agua<br />
        • Dejar escurrir<br />
        • Partir en trozos y dejarlos en una  procesadora junto con los huevos, la leche y el pan <br />
        • Dejar la mezcla en una budinera y  hornear por 25 minutos a fuego alto</p>
