	<h2>Hot Dog Saludable</h2>
      <h4>Ingredientes</h4>
      <p>• Masa para tapaditos:<br />
        • 3 tazas de harina<br />
        • 2 cucharadas de levadura<br />
        • 2 huevos<br />
        • 1 yema<br />
        • ¼ taza de aceite vegetal<br />
        • ¾ taza de leche<br />
        • Sal </p>
      <h4>Para los hot dog:</h4>
      <p>• 1 pechuga de pollo<br />
        • 1 yogurt natural<br />
        • ½ taza de mayonesa baja en grasa<br />
        • Ketchup<br />
        • Tomate – palta - mostaza<br />
      </p>
      <h4>Preparación</h4>
      <p>• Para los panes de hot dog, deje en un bol la harina, haga un hueco al centro, deje la levadura previamente leudada, los huevos, el aceite, la leche, la sal, el ciboulette y mezcle<br />
        <br />
        • Amase bastante hasta que quede una masa suave. Deje leudar 15 minutos.<br />
        <br />
        • Forme cilindros pequeños y hornee a temperatura media alta por 15 minutos.<br />
        <br />
        • Para el relleno, corte la pechuga  de pollo en tiras (simulando una salchicha) y cocine a la plancha.<br />
        <br />
        • Rellene los panes con el pollo, palta, tomate, Ketchup, mostaza y la mezcla de yogurt y mayonesa. </p>
