	<h2>Melón de Frutas</h2>
      <h4>Ingredientes</h4>
      <p>• 1 melón<br />
        • Frutillas<br />
        • Frambuesas<br />
        • Duraznos<br />
        • Sandía<br />
        • ½ taza de jugo de naranjas<br />
        • 2 cucharadas de jugo de limón<br />
        • 8 hojas de menta<br />
        • 1 cucharada de azúcar Light.
      </p>
      <h4>Preparación<br /> </h4>
      <p>• Ahuecar el melón y con un cuchillo, darle la forma de canasto<br />
        <br />
        • Hacer un turti fruti con las frutas. Si quieres darle un toque especial, utiliza un sacabocados para que queden las frutas como &quot;pelotitas&quot;<br />
        <br />
        • En la mini Blender prepara una salsa con el jugo de limón, de naranjas, la menta y el azúcar <br />
        <br />
        • Baña las frutas con la salsa y sirve.</p>
