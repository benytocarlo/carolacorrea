	<h2>Postre de Yogurt con Galletas</h2>
            <h4>Ingredientes</h4>
            <p>• 2 paquetes de galletas de cereales<br />
              • 3 yogurt natural <br />
• 1 tarro de leche condensada o% colesterol<br />
• 1 taza de frambuesas<br />
• 5 cucharadas de leche descremada</p>
<h4>Preparación<br />
 </h4>
            <p>•   Procese las galletas hasta que queden  &ldquo;polvo&rdquo;.<br />
              <br />
              • Mezcle las galletas  con la leche  hasta que tome una<br />
              Consistencia pastosa.<br />
              <br />
              • Aparte tomar un bol y mezclar el yogurt  con la leche condensada y las frambuesas.<br />
              <br />
              • En una budinera previamente  enmantequillada disponer las galletas en la base con ayuda de los dedos.<br />
              <br />
              • Cubrir la masa con la mezcla de yogurt.<br />
              <br />
              • Llevar a horno  precalentado a temperatura mediana por 20  minutos.<br />
              <br />
            • Retirar del horno, dejar enfriar y  cortar en cuadrados iguales.</p>
