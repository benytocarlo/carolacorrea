	<h2>Barritas de Cereal</h2>
                    <h4>Ingredientes</h4>
                    <p>• Mezclen 300 gramos de avena, dos cucharadas de <br />
                    salvado  de trigo. <br />
                    • 3 cucharadas de arroz inflado, ¾ taza de aceite vegetal, <br />
                    • 3 cucharadas  de azúcar Light, ½ taza de miel,<br />
                    • 2 claras de huevo, ralladura de naranja y lo  que quieran. Puede ser • maní, pasas, almendras, nueces, linaza, damasquitos,  sésamo.</p>
                    </p>
                    <h4>Preparación<br />
					</h4>
                    <p>Tener a mano un gran bol y ahí dejar la miel,  agregar las claras y mezclar. Agregar el aceite y  todos los demás ingredientes, si son muy  grandes les recomiendo picarlos. Seguir mezclando hasta formar una pasta. Dejar  toda la preparaciñon en una budinera o en una lata de horno, previamente  aceitada para que no se pegue. Cocinar por unos 20 minutos, sacarlo del horno y  mientras está caliente cortar de la forma y el tamaño que quieran. Dejar  enfriar y reservar en frascos de vidrio. Las pueden usar como colación de  grandes y niños.
                    </p>
