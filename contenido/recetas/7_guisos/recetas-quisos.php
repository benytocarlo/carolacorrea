	<h2>Budín de Zapallo y Brócoli</h2>
      <h4>Ingredientes</h4>
      <p>• 1 trozo de zapallo<br />
        • 1 brócoli<br />
        • 1 huevo<br />
        • 1 cebolla<br />
        • 1 diente de ajo<br />
        • 1 cucharada de pan rallado<br />
        • 1 pizca de nuez moscada<br />
        • 1 pizca de jengibre en polvo<br />
        • Sal y pimienta</p>
      </p>
      <h4>Preparación:</h4>
      <p>• Cortar en zapallo en cubos y cocer al vapor, luego formar un puré<br />
        <br />
        • Cocer el brócoli y picarlo finito. <br />
        <br />
        • En un bol dejar el puré de zapallo, los trozos de brócloli, el huevo, la cebolla picada previamente salteada, el ajo picado, el jengibre, el pan rallado, <br />
        <br />
        • Condimentar con nuez moscada, sal y pimienta<br />
        <br />
        • Dejar la mezcla en moldes individuales aceitados (para que no se peguen) y hornear por 30 minutos <br />
        <br />
        • Servir.<br />
      </p>
