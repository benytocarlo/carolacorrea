	<h2>Charquicán</h2>
      <h4>Ingredientes</h4>
      <p>• 1 cebolla<br />
        • 1 cucharada de páprika<br />
        • ½ kilo de carne<br />
        • 2 tazas de choclo en granos<br />
        • 2 varas de cochayuyo<br />
        • 1 trozo de cochayuyo <br />
        • 8 papas<br />
        • 1 trozo de zapallo<br />
        • ½ taza de arvejitas<br />
        • 2 zanahorias en cubos<br />
        • 8 huevos de codorniz<br />
        • Sal, comino, orégano
      </p>
      <h4>Preparación:</h4>
      <p>
        • Saltee la cebolla picada fina junto con la páprika  en gotas de aceite<br />
        <br />
        • Agregue la carne y cocina<br />
        <br />
        • Para el cochayuyo, déjelo en el horno por 30 minutos  a fuego lento hasta que se seque. Luego píquelo en pequeños trocitos<br />
        <br />
        • Agregue el choclo, la sal, pimienta, comino,  orégano, papas en mitades, zapallo, arvejitas, el cochayuyo y zanahoria en  cubos.<br />
        <br />
        • Cubra con caldo de vacuno o con agua y cocinar hasta  que las verduras estén blandas<br />
        <br />
        • Con un mazo de madera, muela ligeramente las  verduras y termine de cocinar con la olla destapada para que toda el agua se  evapore<br />
        <br />
        • Sirva con un huevito de codorniz cocido en agua  encima</p>
    </div>

