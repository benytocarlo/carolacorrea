	<h2>Omelette de Tomate, Jamón y Orégano</h2>
      <h4>Ingredientes (1 Omelette)</h4>
      <p>• 3 huevos<br />
        • 1 tomate<br />
        • 2 láminas de jamón de pavo ahumado<br />
        • 1 cucharadita de orégano, idealmente fresco<br />
        • Sal y pimienta
      </p>
      <h4>Preparación:<br />
      </h4>
      <p>
      • En un bol dejar los huevos  enteros y batirlos un poco.<br />
      <br />
      • Incorporar el tomate en cubitos, sin pepas<br />
      <br />
      • Agregar el jamón picado en cubitos, el orégano  y condimentar.<br />
      <br />
      • Mezclar bien y calentar un  sartén mediano.<br />
      <br />
      • Aceitar la base del sartén  y dejar caer toda la mezcla.<br />
      <br />
      • Después de unos minutos  despegar la mezcla del sartén con ayuda de una espátula de silicona y dar la  vuelta con ayuda de un plato.<br />
      <br />
      • Cocinar por el otro lado  unos dos minutos y servir.<br />
      <br />
      • Debe quedar mojadita jamás seca.
        <br />
      </p>
    </div>

