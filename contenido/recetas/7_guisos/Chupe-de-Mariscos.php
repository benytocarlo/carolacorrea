	<h2>Chupe de Mariscos</h2>
      <h4>Ingredientes</h4>
      <p>• 1 cebolla en cubitos<br />
        • Aceite de oliva o vegetal<br />
        • 1 pimentón rojo<br />
        • 1 diente de ajo <br />
        • 1 cucharadita de ají de color<br />
        • ½ taza vino blanco<br />
        • 6 torrejas de pan de molde<br />
        • 2 tazas de leche descremada<br />
        • 1 tarro de almejas<br />
        • 1 tarro de choritos<br />
        • 1 trozo de jibia<br />
        • 3 tazas de salsa blanca  saludable<br />
        • 3 cucharadas de queso  rallado<br />
        • Sal y pimienta
      </p>
      <h4>Preparación:</h4>
      <p>
        • Sofría la cebolla, el ajo  y el pimentón, con un poco de aceite, sal,   pimienta y ají de color. <br />
        <br />
        • Agregue el vino y cocine  por tres minutos para que se evapore el alcohol<br />
        <br />
        • Agregue el pan de molde,  previamente remojado en leche y molido en una juguera<br />
        <br />
        • Incorpore los choritos,  las almejas y la jibia, previamente cocida.<br />
        <br />
        • Incorpore también el caldo  de los mariscos y la salsa blanca<br />
        <br />
        • Cocine revolviendo por 10  minutos<br />
        <br />
        • Sirva  en una paila o en un pote individual</p>
