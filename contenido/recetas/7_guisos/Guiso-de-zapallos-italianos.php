	<h2>Guiso de Zapallos Italianos</h2>
      <h4>Ingredientes</h4>
      <p>• 1 cebolla en cubitos<br />
        • Aceite de oliva o vegetal<br />
        • 1 pimentón rojo<br />
        • 1 diente de ajo <br />
        • 1 cucharadita de ají de color<br />
        • ½ taza vino blanco<br />
        • 6 torrejas de pan de molde<br />
        • 2 tazas de leche descremada<br />
        • 1 tarro de almejas<br />
        • 1 tarro de choritos<br />
        • 1 trozo de jibia<br />
        • 3 tazas de salsa blanca  saludable<br />
        • 3 cucharadas de queso  rallado<br />
        • Sal y pimienta
      </p>
      <h4>Preparación:</h4>
      <p>
        • Cueza   4 zapallitos en agua con una pizca de  sal y déjelos escurrir<br />
        <br />
        • Ahueque  el resto de los zapallitos<br />
        <br />
        • Aparte,  sofría la cebolla, con la páprika<br />
        <br />
        • Corte  el zapallo en trozos y déjelos en una procesadora junto con los huevos, la  leche y el pan  y la cebolla <br />
        <br />
        • Rellene  los zapallitos con el guiso y cubra con pan rallado<br />
        <br />
        • Hornee  por 30 minutos a fuego alto<br />
        <br />
        • Sirva  acompañado de arroz, puré o papas salteadas</p>
