	<h2>Pastel de Jaiba</h2>
<h4>Ingredientes</h4>
              <p> • 1 cebolla<br />
• ½ pimentón rojo<br />
• 1 diente de ajo<br />
• ½ cucharadita de ají de color<br />
• ½ taza de vino blanco<br />
• 3 marraquetas añejas<br />
• 2 tazas de leche descremada<br />
• ½ kg de carne de jaiba<br />
• 2 tazas de salsa blanca<br />
• 2 huevos duros<br />
• 
Sal y pimienta
</p>
              <h4>Preparación:</h4>
            <p>
            • Sofría la cebolla  en cubitos, junto con el pimentón en cubitos pequeños, el ajo  machacado, la sal y la pimienta a fuego bajo<br />
            <br />
            •             Incorpore el pan  previamente remojado en leche y molido, cocine por un rato  y si se pega, le agrega leche a la olla<br />
Incorpore la carne  de jaiba, la salsa blanca y  cocine por  cinco minutos<br />
<br />
• Sirva en una paila,  al centro deje media lámina y cubra con más pastel<br />
<br />
• Hornee por 15  minutos o hasta que este muy caliente<br />
<br />
• Si  desea, puede agregar hierbas como ciboulette,  albahaca, etc…<br />
• Decore con pinzas  de jaiba cocidas y con huevito duro rallado</p>
