	<h2>Pastel de Pescado con Puré</h2>
          <h4>Ingredientes</h4>
              <p> • 1 cebolla<br />
                • 1 zapallo italiano<br />
                • 1 zanahoria<br />
                • 1 cucharada de aceite de oliva o vegetal<u></u><br />
                • 3 huevos duros<br />
                • 1 atado de albahaca fresca<br />
                • ½  kilo  de pescado fresco<br />
(merluza – reineta – salmón, jurel)<br />
              • Puré de papas con zanahoria</p>
            <h4>Preparación:</h4>
            <p>
            • En una olla sofría  la cebolla picada en cubitos, Incorpore el zapallo italiano lavado y rallado  (con piel) y la zanahoria rallada<br />
            <br />
• Incorpore el  pescado previamente cocido y desmenuzado, condimente, agregue la albahaca  picada fina y reserve<br />
<br />
• Aparte prepare un  puré moliendo las papas y mezclándolas con leche descremada, sal y zanahoria  molida<br />
<br />
• En una budinera,  deje una capa de la mezcla de verduras y pescado, disponga los huevos duros  partidos por la mitad y cubra con el puré de zapallo<br />
<br />
• Hornee por 15  minutos y sirva</p>
