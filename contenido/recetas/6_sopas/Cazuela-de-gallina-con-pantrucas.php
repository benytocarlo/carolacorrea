	<h2>Crema de Gallina Con Pantrucas</h2>
      <h4>Ingredientes</h4>
      <p>• Cebolla<br />
        • 1 puerro<br />
        • 1 zanahoria<br />
        • 6 presas de gallina<br />
        • 1 caparazón de gallina<br />
        • ½ taza de porotos verdes<br />
        • ¼ kg zapallo<br />
        • ½ taza de chuchoca<br />
        • 4 tazas de agua<br />
        • Pantrucas:<br />
        • 3 tazas de harina<br />
        • 1 huevo<br />
        • Salmuera  tibia<br />
        • Sal </p>
      <h4>Preparación:</h4>
      <p>• Preparar las pantrucas, dejar la harina en un bol, al centro dejar el
        huevo&nbsp; e ir incorporando de a poco la salmuera. Amasar hasta
        conseguir una masa blanda.<br />
        <br />
        •
        Uslerear  la masa formando tiras delgadas. Ir cortando con la mano la masa en cuadrados  de 4 centímetros.<br />
        <br />
        • Aparte en una olla sofreír la cebolla en cubitos, las presas de
        pollo, el puerro y la zanahoria en cubitos. Agregar el caparazón de
        gallina<br />
        <br />
        • Condimentar  con sal y pimienta.<br />
        <br />
        • Una  vez que el sofrito esté listo, agregar a la olla &nbsp;el zapallo, los porotos verdes &nbsp;y el agua.<br />
        <br />
        • Cocinar  a olla tapada hasta que esté todo cocido y el caldo tome consistencia.<br />
        <br />
        •&nbsp;
        Ir  agregando las pantrucas a la cazuela de a poco<br />
        <br />
        • Servir  en plato hondo, muy caliente</p>
