	<h2>Crema de Zapallo      </h2>
      <h4>Ingredientes</h4>
      <p>• ½  kilo de zapallo<br />
        • 1 tazas de agua<br />
        • 2 tazas de leche semi descremada<br />
        • 1 cucharadita de romero<br />
        • 1 yema de huevo<br />
        • Sal y pimienta<br />
        • ¼ taza de almendras tostadas</p>
      <h4>Preparación</h4>
      <p>• 					Pique el zapallo en trozos y cuézalo en agua con sal.<br />
        <br />
        • 	Una vez cocido, procese y agregue la leche con la yema disuelta y el agua<br />
        <br />
        • 	Cocine y si es necesario, agregue más líquido, hasta lograr la consistencia &quot;cremosa&quot;<br />
        <br />
        • 	Incorpore las almendras previamente tostadas.<br />
        <br />
        • 	Ahueque un zapallo y deje dentro la sopa</p>
    </div>

