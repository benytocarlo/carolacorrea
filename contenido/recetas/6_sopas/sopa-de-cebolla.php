	<h2>Sopa de Cebolla</h2>  
             <h4>Ingredientes</h4>
                <p>• 5 cebollas <br />
                • 2 cucharadas de aceite de oliva
                Tomillo
                ½ taza de vino blanco<br />
• 2 cucharadas de azúcar<br />
• 1 litro y medio&nbsp; de agua</p>
          <h4>Preparación</h4>
              <p>• Cortar las cebollas en pluma, dorarlas en el aceite hasta que se ablanden<br />
              .<br />
               • Agregar el azúcar y caramelizar.<br />
               <br />
              • Integrar vino blanco, tomillo, condimentar con sal y pimienta<br />
              <br />
              • Vierta el agua y hervir por dos horas hasta que reduzca.<br />
              <br />
              • Servir con crutones horneados con queso parmesano.
            </p>
</div>

