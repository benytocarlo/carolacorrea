	<h2>Cazuela de Cordero</h2>
      <h4>Ingredientes</h4>
      <p>• 6 presas de cordero &quot;ideal del espinazo&quot;<br />
        • 1 cebolla<br />
        • 2 zanahorias<br />
        • 1 pimiento morrón<br />
        • 6 trozos de zapallo<br />
        • 6 papas <br />
        • 2 choclos<br />
        • 1 cucharada de comino<br />
        • 1 cucharada de orégano<br />
        • 1 cucharada de ají en pasta<br />
        • 1 rama de perejil<br />
        • 2 cucharadas de chuchoca<br />
        • sal y pimienta
      </p>
      <h4>Preparación:</h4>
      <p>
        • Sellar el cordero previamente trozados en un poco de aceite y salpimentar.<br />
        <br />
        • Saltear la cebolla cortada en cubos, las zanahorias y el pimentón cortado en tiras finas<br />
        <br />
        • Cocer aparte las papas, zapallo y choclos partidos en agua con sal.<br />
        <br />
        • Una vez que la cebolla esta bien sofrita, incorporar los trozos de
        cordero, agregar el comino, el orégano, la rama de perejil, el ají y
        por ultimo el caldo en el cual cocimos las verduras ( 1,5 litro
        aproximadamente).<br />
        <br />
        • Cocinar hasta que el cordero este cocido y por último agregar la chuchoca. Retirar la rama de perejil.<br />
        <br />
        • Servir en plato hondo o en una greda un trozo de cordero, una papa, un trozo de zapallo, choclo y caldo. 
      </p>
    </div>

