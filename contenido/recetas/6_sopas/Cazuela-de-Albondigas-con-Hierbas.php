	<h2>Cazuela de Albóndigas con Hierbas</h2>
      <h4>Ingredientes (4 personas)</h4>
      <p>• 1 cebolla <br />
        • 1 zanahoria<br />
        • ½ taza de porotos verdes<br />
        • 4 papas<br />
        • ¼ kg de zapallo<br />
        • 2 cucharadas de chuchoca<br />
        • 6 tazas de agua</p>
      <h4>Albóndigas</h4>
      <p>• ½ kg carne molida  de pavo Sopraval<br />
        • ¼ cebolla<br />
        • 1 taza pan rallado<br />
        • 1 huevo<br />
        • 1 taza de harina<br />
        • Perejil – salvia – ciboulette - romero<br />
        • Sal y pimienta
      </p>
      <h4>Preparación:</h4>
      <p>• 	Preparar las albóndigas mezclando la carne molida, con el pan rallado, el huevo el perejil, sal, pimienta y la cebolla previamente sofrita.<br />
        <br />
        • 	Formar bolitas y pasarlas por harina. Reservar.<br />
        <br />
        • 	Sofreír la cebolla en cubitos y la zanahoria en medias lunas.<br />
        <br />
        • 	Condimentar con sal y pimienta.<br />
        <br />
        • 	Una vez que el sofrito esté listo, agregar a la olla las papas en cubos, el zapallo, los porotos verdes, las albóndigas y el agua.<br />
        <br />
        • 	Cocinar a olla tapada hasta que esté todo cocido y el caldo tome consistencia.<br />
        <br />
        • 	Una vez listo, incorporar la chuchoca, cocinar por dos minutos y apagar el fuego.</p>
