	<h2>Sopa de Brócoli</h2>
             <h4>Ingredientes</h4>
            <p>• 1 brócoli <br />
              • 2 tazas de agua <br />
              • 2 tazas de leche descremada <br />
              • 1 yema de huevo batidas <br />
              • Crutones 
              Parmesano Fresco </p>
            <h4>Preparación</h4>
            <p>• Cocinar el brócoli en dos tazas agua junto con un poco de sal.<br />
              <br />
              • Procesar en la licuadora y llevar a una olla<br />
              Añadir leche&nbsp; y calentar a fuego medio hasta que hierva<br />
  <br />
              • Retirar del fuego.<br />
  <br />
              • Agregar las yemas batidas <br />
  <br />
              • Servir en plato hondo junto con los crutones y <br />
              parmesano fresco rallado.<br />
  <br />
              • Servir de inmediato. </p>
