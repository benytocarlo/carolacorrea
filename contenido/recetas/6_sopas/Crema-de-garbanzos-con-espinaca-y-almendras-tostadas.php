	<h2>Crema de Garbanzos con Espinacas</h2>
     <h4>Preparación</h4>
      <p>• ½ diente de ajo<br />
        • 2 tazas de espinacas<br />
        • Nuez moscada<br />
        • 1 cebolla<br />
        • ½ kilo de garbanzos<br />
        • 3 tomates<br />
        • 1 litro de caldo de verduras<br />
        • Sal y pimienta</p>
      <h4>Preparación:</h4>
      <p>• 			Lave y corte los tomates en gajos, déjelos en una fuente de horno y cocine por 40 minutos a fuego muy suave, con un poco de sal<br />
        <br />
        • 	Sofría la cebolla picada en cubitos, agregue el ajo picado, los garbanzos previamente remojados y cocine por cinco minutos<br />
        <br />
        • Agregue las espinacas picadas finamente en juliana, la nuez moscada y el caldo de verduras<br />
        <br />
        • Agregue los tomates asados, corrija la sazón y cocine por 30 minutos a fuego lento<br />
        <br />
        • Sirva en plato hondo con trocitos de tocino vegetal (lo venden en el supermercado) encima</p>
    </div>

