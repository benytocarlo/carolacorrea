	<h2>Crema de Tomates</h2>
      <h4>Ingredientes</h4>
      <p>• 2 cebollines<br />
        • 1 cucharadita de azúcar<br />
        • 1 hoja de laurel<br />
        • ½ cucharadita de orégano<br />
        • ½ cucharadita de ají de color<br />
        • ½ cucharadita de tomillo<br />
        • 5 tomates <br />
        • 1 anillo de ají cacho de cabra (opcional)<br />
        • 1 taza de agua<br />
        • 1 taza de leche semi descremada<br />
        • Sal y pimienta</p>
      <h4>Preparación</h4>
      <p>• 				Lave y parta los tomates en 4<br />
        <br />
        • 	Déjelos en una olla con el agua, laurel, azúcar, orégano, tomillo, ají de color y si desea que quede picante, un anillo de ají cacho de cabra<br />
        <br />
        • Una vea que hierva y los tomates queden cocidos, pase la mezcla por la juguera y luego por un colador<br />
        <br />
        • Vierta el líquido en una olla, incorpore la leche y condimente.<br />
        <br />
        • Cocina por unos minutos hasta que tome la consistencia deseada<br />
        <br />
        • Sirva con ciboulette picado en cima </p>
    </div>

