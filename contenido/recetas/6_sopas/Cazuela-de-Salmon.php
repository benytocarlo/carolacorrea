	<h2>Cazuela de Salmón</h2>
      <h4>Ingredientes</h4>
      <p>• 5 trozos de salmón<br />
        • 1 cebolla en cubitos<br />
        • ½ taza de vino blanco<br />
        • 1 cucharadita de chuchoca<br />
        • 1 ½ litro de caldo de verduras<br />
        • 5 papas<br />
        • 5 trozos de zapallo<br />
        • 5 trozos de choclo<br />
        • ½ taza de porotos verdes<br />
        • 1 cebolla<br />
        • 1 cucharada de perejil<br />
        • 1 yema de huevo<br />
        • 1 cucharadita de ají de color<br />
        • Sal y pimienta </p>
      <h4>Preparación:</h4>
      <p>• 	Sofría la cebolla con un poco de sal y ají de color a fuego lento<br />
        <br />
        • Incorpore las papas, el zapallo, los porotos verdes, el choclo y el vino blanco<br />
        <br />
        • 	Cocine por cinco minutos y luego incorpore el caldo de verduras<br />
        <br />
        • 	Tape la olla y cocine por 15 minutos <br />
        <br />
        • 	Luego incorpore el salmón, cortado en trozos y revuelva con cuidado, para que no se rompa,  el perejil  y la chuchoca y cocine por 10 minutos más<br />
        <br />
        • 	Cuando esté cocido, integre una yema de huevo batida de a poco en forma de hilo<br />
        <br />
        • 	Sirva la cazuela en un plato hondo o en una paila de greda</p>
