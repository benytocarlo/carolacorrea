	<h2>Sopa de Pastas</h2>
             <h4>Ingredientes</h4>
            <p>• Para el caldo:	1 esqueleto de pavo, pollo o vacuno<br />
              • 1 cebolla<br />
              • 1 pimentón<br />
              • 2 ramas de apio<br />
              • 1 litro de agua<br />
              • Sal, pimienta y hoja de laurel<br />
              • ½ paquete de pastas (caracoles, etc)<br />
              • Huevos<br />
              • Perejil</p>
            </p>
            <h4>Preparación:</h4>
            <p>
              • Para el caldo, sofreír en una olla la cebolla cortada a la pluma, el pimentón cortado en tiras, la rama de apio en trozos y el esqueleto.<br />
  <br />
              • Cocinar por 5 minutos<br />
  <br />
              • Incorporar el agua, sal, pimienta y laurel y cocinar por 40 minutos.<br />
  <br />
              • Una vez hecho el caldo, colar rescatando solo el líquido, corregir el aliño.<br />
  <br />
              • En el caldo, cocinar la pasta en el caldo<br />
  <br />
              • Antes de servir, añadir perejil y un huevo (opcional) </p>
          </div>

