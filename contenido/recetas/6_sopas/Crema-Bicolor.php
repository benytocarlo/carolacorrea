	<h2>Crema Bicolor</h2>
      <h4>Ingredientes</h4>
      <p>• 4 zanahorias<br />
        • ½  kilo de arvejitas<br />
        • 1 hoja de laurel<br />
        • 2 taza de leche descremada<br />
        • 2 taza de caldo de cocción <br />
        • 1 ramita de romero<br />
        • 2 hojas de laurel<br />
        • ½ atado de ciboulette<br />
        • ½ taza de almendras tostadas y picadas<br />
        • Sal y pimienta</p>
      <h4>Preparación:</h4>
      <p>• 		Cueza  la zanahoria  en agua con el laurel<br />
        <br />
        • 	Cueza   las arvejitas en agua con una ramita de romero<br />
        <br />
        • 	Una vez cocidas las verduras,  procéselas en una licuadora con leche y caldo de cocción hasta que logremos una consistencia cremosa. Haga lo mismos con cada una por separado<br />
        <br />
        • 	Cocine cada una de las cremas en una olla diferente y condimente con sal y pimienta<br />
        <br />
        • 	Sirva en un plato hondo dos cucharones uno con cada salsa simultáneamente para conseguir que quede de dos colores<br />
        <br />
        • 	Espolvoree con ciboulette y almendras tostadas</p>
