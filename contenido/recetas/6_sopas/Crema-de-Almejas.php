	<h2>Crema de Almejas</h2>
      <h4>Ingredientes</h4>
      <p>• 1 kilo de almejas <br />
        • ¼ cebolla<br />
        • ½ taza de apio<br />
        • 2 papas<br />
        • ½  taza pimiento rojo y verde<br />
        • ½ zanahoria<br />
        • ½  cucharada de harina<br />
        • 1 cucharada de aceite de oliva<br />
        • ¾ taza de salsa blanca saludable<br />
        • ¼  taza de vino blanco<br />
        • Sal y pimienta</p>
      <h4>Preparación</h4>
      <p>• 			En una olla sofría la cebolla, el apio, la zanahoria, los morrones y las papas &quot;todo picado en pequeños cubitos&quot;<br />
        <br />
        • 	Agregue la  harina y mezcle<br />
        <br />
        • 	Cueza las almejas y reserve el caldo.<br />
        <br />
        • 	Incorpore las almejas a la olla, desconchadas y agregue el vino blanco, deje evaporar el alcohol y agregue el caldo de almejas. <br />
        <br />
        • Deje educir.<br />
        <br />
        • 	Procese la crema sin desintegrar mucho los ingredientes.<br />
        <br />
        • 	Añada salsa blanca  y termine de cocinar por cinco minutos más<br />
      </p>
    </div>

