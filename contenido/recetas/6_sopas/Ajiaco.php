	<h2>Ajiaco</h2>
            <h4>Ingredientes</h4>
               <p>• ½ kilo de carne<br />
                 • 5 papas cocidas<br />
                 • 1 cebolla pluma<br />
                 • 1 cucharada de ají de color<br />
                 • ½ cucharadita de comino<br />
                 • ½ cucharadita de orégano<br />
                 • 1 ½ litro de caldo de huesos<br />
                 • 1 diente de ajo<br />
                 • 5 huevos<br />
               • Sal y pimienta         </p>
            <h4>Preparación:</h4>
            <p>• 	Pique la cebolla a la pluma y cocínela en gotas de aceite en <br />
              una olla<br />
              <br />
              •	Incorpore la carne picada en finas tiras y dore un poco<br />
              <br />
              •	Agregue el diente de ajo, condimente con sal, pimienta y<br />
ají de color<br />
              <br />
              •	Añada el caldo, las papas y cocine por 25 minutos<br />
              <br />
              • Separe un poco del caldo en una pequeña olla y ahí cocine<br />
los huevos<br />
              <br />
              • Sirva el ajiaco en plato hondo con un huevo y un poco de perejil picado encima</p></div>

