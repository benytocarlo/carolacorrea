	<h2>Caldillo de Congrio</h2>
      <h4>Ingredientes</h4>
      <p>• 2 Kg. de congrio entero <br />
        • 4 papas peladas y cortadas en rodajas gruesas <br />
        • 1 cebolla en corte pluma <br />
        • 1 diente de ajo picado<br />
        • 1 tomate<br />
        • 1 atado de verduras surtidas (perejil, apio, laurel) <br />
        • 1/2 litro de fondo de pescado <br />
        • 3 cucharadas de aceite <br />
        • 3 cucharadas de crema líquida <br />
        • Cilantro fresco picado <br />
        • 20 camarones<br />
        • Sal y pimienta</p>
      <h4>Preparación:</h4>
      <p>• En una olla, calentar el aceite y freír la cebolla con los ajos. Luego, agregar el tomate picado en gajos. Dejar cocinar durante 4 a 5 minutos aproximadamente.<br />
        <br />
        •Agregar las papas, las ramitas de verduras surtidas, el fondo de pescado, la sal y la pimienta. <br />
        <br />
        •Cocinar hasta que las papas estén casi listas. Por último, echar el pescado ya trozado y vigilar que no se recueza y los camarones<br />
        <br />
        •	Para servir, colocar las presas, las papas y la cebolla en platos hondos y encima el caldo. Rociar con crema líquida y espolvorear con cilantro.
      </p>
