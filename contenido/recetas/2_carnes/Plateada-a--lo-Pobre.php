	<h2>Plateada a lo Pobre</h2>
            <h4>Ingredientes</h4>
               <p>&#8226; 2 kilos de plateada de vacuno sin grasa<br />
  &#8226; 2 cebollas pluma<br />
  &#8226; 1 taza de vino tinto<br />
  &#8226; 2 tazas de agua<br />
  &#8226; 2 dientes de ajo<br />
  &#8226; Sal y pimienta<br />
  &#8226; Papas fritas, cocidas al horno<br />
  &#8226; 6 huevos <br />
  &#8226; 4 cebollas <br />
  &#8226; 1 cucharada de azúcar</p>
               </p>
            <h4>Preaparación:<br />
              <br />
            </h4>
               <p>&#8226; En  una budinera haga una cama con dos cebollas y apoye la plateada<br />
                 <br />
                 &#8226;  Condimente  con sal,  pimienta y ajo picado<br />
                 <br />
                 &#8226;  Tape  la carne  con otra capa de cebollas<br />
                 <br />
                 &#8226;  Agregue  el agua y el vino.  Tape la budinera con  papel aluminio<br />
                 <br />
                 &#8226;  Cocine  a horno muy bajo por 1 ½ hora<br />
                 <br />
                 &#8226;  Destape  la budinera, cocine por media hora más<br />
                 <br />
                 &#8226;  Retire  la carne y cuele el jugo<br />
                 <br />
                 &#8226;  Reduzca  el jugo en una olla<br />
               </p>
               <p>&#8226;  Corte  la plateada y caliente en la misma olla del jugo<br />
               </p>
               <p>&#8226; Para  la cebolla, cocínela cortada a la pluma, a fuego bajo, junto con un poco de sal  y azúcar hasta que tome color &ldquo;caramelo&rdquo;<br />
             </p>
               <p>&#8226;   Sirva  con papas fritas, cocidas al horno, cebolla dorada y huevo frito</p></div>
