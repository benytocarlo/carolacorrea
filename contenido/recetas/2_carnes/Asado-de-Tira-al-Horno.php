	<h2>Asado de Tira al Horno</h2>
      <h4>Ingredientes</h4>
      <p>• 2  kilos de asado de tira<br />
        • 2  cebollas pluma<br />
        • 1  taza de vino tinto<br />
        • 2  tazas de agua<br />
        • 2  dientes de ajo<br />
        • Sal  y pimienta<br />
        • Vegetales  salteados para acompañar</p>
<h4>Preparación</h4>
      <p>• Cortar la cebolla a la pluma y dejar la mitad de  en una budinera<br />
        • Dejar la carne sobre las cebollas, condimentar con sal y pimienta<br />
        • Incorporar el vino, el agua y los dientes de ajo picados<br />
        • Tapar la carne con el resto de cebolla<br />
        • Cubrir la budinera con papel aluminio y hornear por 1 hora a temperatura baja<br />
        • Destapar la budinera, subir el fuego al máximo y cocinar por media hora más<br />
        • Dejar enfriar, retirar toda la grasa que suba a la superficie y pasar el resto de jugo y cebolla por una procesadora.<br />
        • Dejar todo en una olla, incorporar agua o caldo de carne si es necesario, trozar la carne y calentar junto con el caldo<br />
        • Servir con verduras salteadas en aceite de oliva<br />
        <br />
        <br />
      </p>
