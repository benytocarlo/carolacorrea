	<h2>Arroz Frito con Espárragos y Cerdo </h2>
              <h4>Ingredientes</h4>
              <p>&#8226; ¼ kilo carne de cerdo, sin grasa <br />
                &#8226; 3 cucharadas de soya <br />
                &#8226; 2 cucharadas de vino blanco<br />
                &#8226; 6 espárragos verdes frescos (crudos)<br />
                &#8226; 2 tazas de arroz cocido, frío
                1&nbsp; huevo, ligeramente batido<br />
                &#8226; 4 cebollines picados
                ½ taza de dientes de dragón </p>
              <h4>Preparación</h4>
              <p>&#8226; Mezclar el cerdo picado en cubos medianos con la soya y el vino blanco <br />
                <br />
                &#8226; En una olla, calentar los espárragos crudos, picados al sesgo, apenas pelados, junto a los cebollines cortados&nbsp; al sesgo. <br />
                <br />
                &#8226; Agrégueles un poco de &nbsp;aceite de oliva, Déjelos reposar por unos minutos. <br />
                <br />
                &#8226; En la misma olla cocinar el cerdo<br />
                <br />
                &#8226; En la misma olla (vacía) calentar el arroz, agregar el huevo, espárragos, cerdo, cebollines, dientes de dragón y si es necesario, un poco más de soya.<br />
                <br />
                &#8226; Servir de inmediato</p>
              </p>
