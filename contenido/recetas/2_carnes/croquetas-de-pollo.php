	<h2>Croquetas de Pollo a las Hierbas</h2>
      <h4>Ingredientes</h4>
      <p>• 2 pechugas de pollo <br />
        • 1 cebolla<br />
        • 1 huevo<br />
        • 2 cucharadas de harina<br />
        • Hierbas frescas surtidas<br />
        •&nbsp; 1½ taza de Salsa Blanca Saludable<br />
        • Sal y pimienta<br />
        • Acompañar con ensalada de rúcula, tomaes cherry y lechugas<br />
        con un rico dressing</p>
      <h4>Preparación<br />
        <br />
      </h4>
      <p>• Cortar el pollo y moles en la mini Blender<br />
        <br />
        • Sofreír la cebolla e incorporarla junto con el pollo a un bol <br />
        <br />
        • Agregar las hierbas y condimentar<br />
        <br />
        • Formar daditos con las manos y pasar po huevo y harina<br />
        <br />
        • Cocinar al horno a temperatura alta<br />
        <br />
        • Para acompañar juntar los vegetales en un bol y hacer un dressing con
        aceto balsámico, aceite de oliva, caqui madura, merquén y sal. Servirla
        en un potecito en el mismo plato.
      </p>
