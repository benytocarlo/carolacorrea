	<h2>Alitas de Pollo con Salsa Chancaca</h2>
<h4>Ingredientes</h4>
      <p>• Ingredientes &quot;4 personas&quot;<br />
        • 12 alitas, sin piel<br />
        • 125 gramos de chancaca Deliciosa<br />
        • ¼ taza de agua<br />
        • 1 cucharadita de merquén<br />
        • ½ taza de jugo de naranjas<br />
        • 2 cucharadas de vino blanco<br />
        • 1 cucharadita de mostaza<br />
        • Sal <br />
    </p>
    <h4>Preparación</h4>
      <p>•	Deje la chancaca y el agua en una olla hasta que se derrita<br />
        <br />
        •	Incorpore el merquén a la olla y reserve<br />
        <br />
        •	En un bol mezcle el jugo de naranjas, el vino blanco y la mostaza <br />
        <br />
        •	Deje los trutros en el bol por 20 minutos para que se marinen en la mezcla<br />
        <br />
        •	Retire los trutros, condiméntelos con sal  y cocínelos en una budinera, con gotas de aceite, en un horno precalentado y a temperatura alta por 20 minutos, con el caldo de la marinada que sobró.<br />
        <br />
        •	Retire del horno cuando estén dorados, envuelva las puntas con papel aluminio y sirva bañados en salsa de chancaca<br />
      </p>
