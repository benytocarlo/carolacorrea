	<h2>Garbanzos del mar</h2>
      <h4>Ingredientes</h4>
      <p>
        • 3 tazas de garbanzos<br />
        • 1 cebolla morada<br />
        • 2 cebollines<br />
        • 1 tarro de almejas<br />
     	• 1 tarro de choritos<br />
        • 8 almejas frescas con concha<br />
        • 6 choros maltones<br />
        • 1 tomate en gajos<br />
        • Caldo de verduras<br />
        • Sal, pimienta y albahaca<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• Remojar los garbanzos por 8 horas.<br />
        <br />
        • En una olla hacer un sofrito con la cebolla en cubitos, los cebollines picados finos, sal, pimienta y páprika. Cocinar<br />
        <br />
        • Incorporar el tomate en gajos, los garbanzos escurridos, los mariscos (los con concha muy bien lavados)<br />
        <br />
        • Agregar 5 tazas de caldo de verduras o agua caliente (preferentemente caldo)<br />
        <br />
        • Incorporar una rama de albahaca entera, tapar la olla y cocinar por 40 minutos a fuego bajo<br />
		<br />
        • Servir en plato hondo.<br />
		<br />
        • Decorar con una almeja en concha, un chorito y una ramita de albahaca.<br />
      </p>
