	<h2>Terrina de Pollo</h2>
            <h4>Ingredientes</h4>
               <p><br />
  &#8226; 1 pechuga de pollo<br />
  &#8226; 2 trutros cortos de pollo<br />
  &#8226; 2 claras de huevo<br />
  &#8226; 2 tazas de salsa blanca &ldquo;saludable&rdquo;<br />
  &#8226; 1 taza de tomates deshidratados<br />
  &#8226; 1 taza de hierbas frescas <br />
&ldquo;tomillo – albahaca  – romero – perejil&rdquo;</p>
               </p>
          <h4>Preparación</h4>
               <p><br />
                 &#8226;  Moler  la pechuga de pollo.<br />
               <br />
               &#8226;  Hidratar  y picar los tomates<br />
               <br />
               &#8226; Mezclar  la pechuga molida con los tomates, las hierbas y la salsa blanca. Condimentar<br />
               <br />
               &#8226; Aparte  retirar la piel de los trutros y cortarlos en filetes.<br />
               <br />
               &#8226; En  un molde alargado, forrarlo con papel film, dejar una capa de relleno, al  centro dejar los trozos de trutro y cubrir por los costados y por encima con la  pasta<br />
               <br />
               &#8226; Cocinar  a baño maría por 50 minutos. Sacar del horno, dar vuelta en una fuente y  servir. Decorar con una rama de albahaca fresca.</p>
