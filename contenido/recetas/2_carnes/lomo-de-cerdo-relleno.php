	<h2>Lomo de cerdo relleno</h2>
      <h4>Ingredientes (6 personas)</h4>
      <p>
        • 1 lomo de cerdo<br />
        • 1 paquete de queso crema Light<br />
        • 1 taza de maní tostado<br />
        • 1 atado de ciboulette<br />
     	• 1 atado de espinacas<br />
        • 4 tazas de puré de papas<br />
        • 2 berenjenas<br />
        • Sal y pimienta<br />
        • 1 cebolla<br />
        • 1 taza de agua<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• Retire toda la grasa del lomo y ábralo con un corte mariposa<br />
        <br />
        • Haga una pasta con el queso crema, el ciboulette, el maní previamente picado, sal y pimienta<br />
        <br />
        • Condimente el lomo abierto con sal y pimienta y luego unte con la pasta<br />
        <br />
        • Enrolle el lomo y amarre con una pita<br />
        <br />
        • Déjelo en una budinera sobre una cama de cebollas pluma y una taza de agua<br />
        <br />
        • Tape la budinera con papel aluminio y cocine a fuego lento por una hora y cuatro<br />
        <br />
        • Prepare un puré de papas normal. Aparte cocine las berenjenas  al horno y cuando estén blandos, las muele en una procesadora<br />
        <br />
        • Mezcle el puré de papas con el puré de berenjenas<br />
        <br />
        • Corte el lomo en trozos y sirva con el puré con berenjenas<br />
      </p>
