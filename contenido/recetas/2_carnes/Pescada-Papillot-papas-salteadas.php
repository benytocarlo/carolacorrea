	<h2>Pescada Papillot/Papas Salteadas</h2>
            <h4>Ingredientes</h4>
               <p>&#8226; 4  filetes de pescada<br />
                 &#8226; 2  cebollines<br />
                 &#8226; 2  tomates<br />
                 &#8226; ¼ taza  de vino blanco<br />
                 &#8226; ¼ taza  de jugo de limón<br />
                 &#8226; 2  cucharadas de aceite de oliva o vegetal<br />
                 &#8226; ¼ de  hinojo<br />
                 &#8226; ½ atado  de cilantro<br />
                 &#8226; Sal y  pimienta <br />
               </p>
            <h4>Preparación:<br />
            </h4>
            <p>&#8226; Limpie y divida la pescada en dos  filetes, sin espinas y con o sin piel<br />
               <br />
               &#8226; Deje los trozos de pescado dentro de un  cuadrado de papel aluminio<br />
               <br />
               &#8226; Corte los cebollines muy finamente,  corte los tomates en gajos y pique finamente el hinojo<br />
               <br />
               &#8226; Disponga los ingredientes sobre la  pescada y condimente<br />
               <br />
               &#8226; Aparte en una procesadora mezcle el  aceite, el vino, el jugo de limón, sal y pimienta y déjelo sobre el pescado<br />
               <br />
               &#8226; Por último agregue el cilantro  deshojado, envuelva cada filete con el papel y hornee por 12 minutos en un  horno precalentado a temperatura alta<br />
               <br />
            &#8226; Acompañe el pescado con papitas  pequeñas, previamente cocidas y salteadas en aceite de oliva y ciboulette </p>
             <p><strong>Dato</strong><br />
               <strong>Esta forma de cocinar el pescado, permite que  mantenga todos los jugos  dentro del  papel aluminio  y además que mantenga la  temperatura. </strong></p>
