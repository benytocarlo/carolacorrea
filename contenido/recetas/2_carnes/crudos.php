	<h2>Crudos </h2>
      <h4>Ingredientes</h4>
      <p>• 1 kg de posta rosada<br />
        • 1 atado de cebollines<br />
        • 1 taza de burgol<br />
        • 1 taza de vinagreta<br />
        • 12 tostadas<br />
        • 12 pepinillos dulces<br />
     	• &frac12; taza de mostaza Dijon<br />
        • 2 cebollas<br />
        • 1 atado de cilantro<br />
        • Jugo de limón<br />
        • Sal y pimienta<br /></p>
        
      <h4>Preparación</h4>
      <p>• Desgrase completamente la carne y muélala (de lo contrario, compre tártaro)<br />
        <br />
        • Deje remojar el trigo burgol por una hora en agua<br />
        <br />
        • En un vaso, deje los cebollines con la parte blanca hacia abajo, con vinagre, sal y pimienta por media hora<br />
        <br />
        • Pique la cebolla en cubitos, el cilantro pequeño y los pepinillos en rodajas<br />
        <br />
        • Junte la carne con el trigo burgol, forma círculos (tipo hamburguesas) de 150 gramos aproximadamente<br />
        • Lleve un gran plato con los crudos a la mesa y todos los ingredientes<br />
        • Condimente el crudo con cebollín picado, pepinillos, mostaza, cebolla, cilantro, aceite de oliva, jugo de limón, sal y pimienta<br />
      </p>
