	<h2>Huachalomo a la cerveza</h2>
      <h4>Ingredientes</h4>
      <p>
        • 1 cebolla<br />
        • 2 dientes de ajo<br />
        • 1 caldo de costilla Maggi<br />
        • &frac12; pimentón verde<br />
     	• &frac12; pimentón rojo<br />
        • 1 rama de apio<br />
        • &frac12; taza callampas secas, hidratadas<br />
        • 3 tomates<br />
        • 1 lata de cerveza<br />
        • 2 cucharadas de harina<br />
        • 1 kilo de huachalomo<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• En una juguera procese los pimentones, el tomate, el apio,  las callampas previamente hidratadas, el ajo y la cebolla<br />
        <br />
        • Aparte pase la carne por harina y selle en un sartén<br />
        <br />
        • Incorpore a la olla las verduras procesadas, la cerveza y ½ cucharada de harina previamente disuelta en agua y el caldo de costilla Maggi que aportará  a ésta receta su rico sabor.<br />
        <br />
        • Condimente y cocine por 45 minutos a fuego bajo, con tostador<br />
        <br />
        • Sirva  sobre pasta.<br />
      </p>
