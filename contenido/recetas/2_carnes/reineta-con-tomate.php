	<h2>Reineta con Tomate</h2>
            <h4>Ingredientes</h4>
               <p>• 4 filetes de reineta<br />
                 • 3 tomates<br />
                 • 5 cebollines<br />
                 • Sal <br />
                 • Orégano fresco</p>
               </p>
         <h4>Preparación</h4>
                <p><br />
                  • Retirar las espinas al pescado pero dejándoles la  piel<br />
                  <br />
                  • En una parrilla a fuego fuerte, dejar las  reinetas por 20 minutos  
                con la piel hacia el fuego.<br />
                <br />
                • Condimentar con sal <br />
                <br />
                • En una budinera aparte dejar los tomates partidos en láminas
condimentados con sal y orégano y dejar encima los cebollines cortados
finamente a lo largo. <br />
                <br />
                • Hornear por 35 minutos.<br />
                <br />
                • Sacar la reineta de la parrilla y dejar encima  los tomates <br />
               con los cebollines.</p></div>
