	<h2>Strogonoff</h2>
             <h4>Ingredientes</h4>
               <p>&#8226; ½ kilo  de carne de vacuno<br />
  &#8226; 1  bandeja de champiñones<br />
  &#8226; 12  tomates cherry<br />
  &#8226; 1  tapita de cognac<br />
  &#8226; 10  cebollas perla<br />
  &#8226; Sal y  pimienta</p>
               </p>
          <h4>Preparación</h4>
               <p><br />
&#8226; Corte la carne  en cubos y dorar en una olla<br />
                 <br />
                 &#8226; Agregue la cebolla y los champiñones en  cuartos<br />
                 <br />
                 &#8226; Flambee con cognac<br />
                 <br />
                 &#8226; Incorpore los tomates  partidos en dos y la salsa blanca<br />
                 <br />
                 &#8226; Condimente y cocine por 8 minutos <br />
                 <br />
                 &#8226; Acompañe con papas duquesas</p></div>        
