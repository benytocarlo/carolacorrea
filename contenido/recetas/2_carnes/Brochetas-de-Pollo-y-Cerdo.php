	<h2>Brochetas de Pollo y Cerdo</h2>
      <h4>Ingredientes</h4>
      <p>• 1 pechuga de pollo<br />
        • 1 filete de cerdo<br />
        • 1 pimentón rojo<br />
        • 4 tomates<br />
        • 1 cebolla morada<br />
        • ½ atado de cilantro picado<br />
        • 1 ají verde picado<br />
        • 1 cucharada de mostaza<br />
        • ½ taza de jugo de limón<br />
        • Sal, pimienta y aceite de oliva      </p>
      <h4>Preparación<br />
      </h4>
      <p>•	Corte el pollo, el cerdo y el pimentón en cubos de igual tamaño<br />
        <br />
        •	Forme las brochetas pinchando un trozo de cerdo, uno de pimentón y uno de pollo<br />
        <br />
        •	Cocine la brocheta por ambos lados,  en un sartén caliente , condimente con sal y pimienta<br />
        <br />
        •	Para la ensalada, corte los tomates en gajos, mezcle con la cebolla pluma junto con el cilantro y el ají verde. Condimente con aceite, sal y pimienta<br />
        <br />
        •	Para la salsa, mezcle el jugo de limón y la mostaza<br />
        <br />
        •	Sirva al centro la ensalada chilena, rodéela con dos brochetas y pinte el plato con la salsa de mostaza<br />
      </p>

