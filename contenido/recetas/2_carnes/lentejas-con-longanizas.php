	<h2>Lentejas con longanizas y pebre</h2>
      <h4>Ingredientes</h4>
      <p>
        • 3 tazas de lentejas<br />
        • 1 cebolla<br />
        • 2 longanizas<br />
        • 1 cucharadita de ají de color<br />
     	• 1 taza de zapallo en cubitos<br />
        • &frac12; taza de papas en cubitos<br />
        • 1 pimentón rojo<br />
        • 5 tazas de caldo de verduras<br />
        <strong>Pebre</strong><br />
        • 3 tomates<br />
        • 1 cebolla morada<br />
        • 1 atado de cilantro<br />
        • Aceite, limón, sal y pimienta<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• Sofría la cebolla en gotas de aceite<br />
        <br />
        • Condimente con sal, pimienta y ají de color<br />
        <br />
        • Incorpore las lentejas y revuelva<br />
        <br />
        • Añada el pimentón rojo, las papas y el zapallo, todo picado en cubitos pequeños. Agregue también las longanizas picadas en láminas<br />
        <br />
        • Incorpore el caldo<br />
        <br />
        • Cocine en una olla tapada por 25 minutos o hasta que estén cocidas.<br />
        <br />
        • Aparte prepare el pebre picando los tomates sin semillas ni piel en cubitos, la cebolla fina y el cilantro. Agregue el jugo de limón, aceite de oliva sal y pimienta.<br />
        <br />
        • Sirva las lentejas en un plato hondo y al centro agregue dos cucharadas de pebre.<br />
      </p>
