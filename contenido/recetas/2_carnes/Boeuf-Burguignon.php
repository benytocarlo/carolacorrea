	<h2>Boeauf Burguignon</h2>
                <h4>Ingredientes</h4>
                <p>• 1 kilo de posta rosada<br />
                • 15 cebollas perla<br />
                • 1 trozo de tocino<br />
                • 2 tazas de vino tinto<br />
                • 1 bandeja de champiñones<br />
                • 1 cucharadita de maicena<br />
                • Sal y pimienta      
                </p>
                <h4>Preparación</h4>
                <p>•	Corte la carne en cubos y deje marinar en vino tinto junto al tocino y a las cebollas perla partidas por la mitad por dos horas.<br />
                <br />
                •	Retire el tocino. Solo lo utilizamos para dar sabor<br />
                <br />
                •	Saltee la carne  y agregue los champiñones<br />
                <br />
                •	Cocine por 16 minutos y agregue la maicena disuelta en agua<br />
                <br />
                •	Condimente y sirva sobre unas espinacas a la crema <br />
                <br />
                •	Para las espinacas, córtelas pequeñas y cocínelas con salsa blanca saludable, sal y pimienta hasta que queden espesas y sabrosas<br />
                <br />
                </p>
