	<h2>Salmón Cai Cai</h2>
             <h4>Ingredientes</h4>
               <p>&#8226; 4 trozos de salmón de 160 grs<br />
  &#8226; 4 láminas de jamón de pavo <strong></strong><br />
  &#8226; 1 taza de choclo <br />
  &#8226; 8 champiñones<br />
  &#8226; 12 camarones &ldquo;opcional&rdquo;<br />
  &#8226; 4 tazas de salsa blanca saludable<br />
  &#8226; Panaché de verduras para acompañar</p>
               </p>
             <h4>Preparación</h4>
               <p><br />
&#8226; Prepare  una salsa blanca saludable, con ¼ taza de aceite vegetal, dos cucharadas de  harina y un litro de leche descremada. Condimentar con sal y pimienta<br />
               <br />
               &#8226;  En un  sartén caliente, saltear el jamón picado en tiras, los champiñones picados en  láminas, los camarones y el choclo<br />
               <br />
               &#8226;  Agregue  la salsa blanca y forme una salsa<br />
               <br />
               &#8226; Cocine  el salmón en un sartén muy caliente con apenas <br />
               unas gotas de aceite. <br />
               <br />
               &#8226; Para  el panaché de verduras, saltee un mix de verduras, con condimente con sal y  pimienta<br />
               <br />
               &#8226; Sirva  el salmón, cubra con la salsa Cai cai y acompañe con el panaché de verduras</p>
