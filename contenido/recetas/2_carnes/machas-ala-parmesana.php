	<h2>Machas a la parmesana</h2>
      <h4>Ingredientes (4 personas)</h4>
      <p>
        • 24 machas frescas<br />
        • &frac14; taza de vino blanco<br />
        • 3 cucharadas de aceite de oliva<br />
        • Jugo de 1 limón<br />
     	• 1 &frac12; taza de queso parmesano rallado<br />
        • Sal y pimienta<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• Retire las machas de las conchas, limpie las machas y lave las conchas. Reserve<br />
        <br />
        • Con una cuchara, golpee suavemente las lenguas de macha<br />
        <br />
        • A cada concha agregue ½ cucharadita de vino blanco, media cucharadita de aceite de oliva, unas gotas de jugo de limón, apoye una macha y cubra con queso parmesano<br />
        <br />
        • Deje todas las machas en una lata de horno y hornee por 10 minutos<br />
        <br />
        • De la familia de los quesos, los quesos duros, como el parmesano es de los que contienen menos cantidad de grasas. Siempre hay que preferir los quesos "duros"<br />
      </p>
