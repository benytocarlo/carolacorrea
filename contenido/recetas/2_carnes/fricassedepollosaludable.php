	<h2>Fricasé de pollo saludable</h2>
      <h4>Ingredientes (6 personas)</h4>
      <p>
        • 4 tazas de papas fritas "al horno"<br />
        • &frac34;  taza de arvejitas<br />
        • 1  zapallo italiano<br />
        • 1 pechuga de pollo en cubos<br />
     	• 3 huevos<br />
        • 1 cucharada de perejil picado<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• En un sartén caliente, con apenas unas gotas de aceite, dore el pollo y el zapallito italiano cortado en dados<br />
        <br />
        • Agregue las arvejitas<br />
        <br />
        • Aparte corte papas en cubos y déjelas en un horno precalentado, sobre la lata  a fuego máximo<br />
        <br />
        • Incorpore al sartén las papas fritas, los huevos, revuelva  y retire del fuego<br />
        <br />
        • Sirva en cada plato y agregue un poco de perejil<br />
		<br />
        • Puede decorar con triángulos de pan tostado con merquén, al horno<br />
      </p>
