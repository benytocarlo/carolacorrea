	<h2>Osobuco de pavo con puré al pimentón</h2>
      <h4>Ingredientes (4 personas)</h4>
      <p>
        • 8 osobucos de pavo<br />
        • 1 cebolla<br />
        • 1 diente de ajo<br />
        • &frac12; manzana verde<br />
     	• 1 vara de apio<br />
        • 2 hojas de laurel<br />
        • 1 ½  taza de agua<br />
        • 2 tazas de salsa de tomates<br />
        • Pimienta y sal<br />
        • <strong>Para el Puré</strong><br />
     	• 8 papas cocidas<br />
        • &frac12; pimentón verde<br />
        • &frac12; pimentón rojo<br />
        • 1 taza de leche descremada<br />
        • Sal, pimienta y ciboulette<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• Dore los osobucos en un poco de aceite<br />
        <br />
        • Agregue la cebolla pluma, el ajo machacado, la manzana en cubitos, el apio picado y el laurel. Condimente con sal y pimienta<br />
        <br />
        • Incorpore el agua, la salsa de tomates y condimente<br />
        <br />
        • Cocine por 20 minutos a fuego lento, con tostador<br />
        <br />
        • Para el puré, muela las papas cocidas junto a la leche  descremada<br />
        <br />
        • Mézclelo con los pimentones picados en pequeños cubitos, el ciboulette picado y condimente<br />
      </p>
