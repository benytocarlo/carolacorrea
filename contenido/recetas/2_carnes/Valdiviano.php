	<h2>Valdiviano</h2>
             <h4>Ingredientes</h4>
             <p>&#8226; 1 litro de caldo de carne<br />
               &#8226; 1 ají cacho de cabra<br />
  &#8226; 120 grs de charqui asado y desmenuzado<br />
  &#8226; 1 cebolla &ldquo;pluma&rdquo;<br />
  &#8226; 1 cucharada de ají de color<br />
  &#8226; 1 cucharada de perejil<br />
  &#8226; Orégano, comino<br />
  &#8226; 1 taza de vino blanco<br />
  &#8226; 3 papas cocidas<br />
  &#8226; 4 huevos pochados       </p>
            <h4>Preparación<br />
  </h4>
               <p>&#8226; Sofría  la cebolla  con sal y ají de color  hasta que esté transparente; agregue el  charqui, el perejil picado, el orégano, el comino, el cacho de cabra entero  (para luego retirarlo) y cocine a fuego medio.<br />
                 <br />
                 &#8226; Agregue  el vino y deje hervir hasta que el alcohol se evapore.<br />
                 <br />
                 &#8226; Vierta  la mezcla en una fuente honda o de greda idealmente, añada el caldo caliente y  cocine por cinco minutos<br />
                 <br />
                 &#8226; Por  último agregue las papas partidas en cuatro<br />
                 <br />
                 &#8226; Sirva  en gredas individuales y al centro deje en cada una <br />
            un huevo pochado y un poco  de cilantro picado</p></div>
