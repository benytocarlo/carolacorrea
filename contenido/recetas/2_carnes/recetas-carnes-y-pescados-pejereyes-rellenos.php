	<h2>Pejerreyes Rellenos</h2>
      <h4>Ingredientes</h4>
      <p>• 12 pejerreyes<br />
        • 1 taza de harina<br />
        • 1 taza taza de pan rallado<br />
        • 2 huevos<br />
        • 2 zanahorias<br />
        • ½ paquete de espinacas<br />
        <br />
      <h4>Salsa Tártara: </h4>
      <p>• 1  taza de pichanga <br />
        • 1  cucharadas de mayonesa light<br />
        • 1  cucharada de yogurt natural<br />
        • 1  cucharadita de mostaza</p>
      </p>
      <h4>Preparación:</h4>
      <p>&#8226; Aliñe  los filetes de pescado con orégano, sal y pimienta. Rellénelos con un bastón de  zanahoria y espinacas blanqueadas. Páselos por harina, huevo y  pan rallado.<br />
        <br />
        &#8226;  Hornee  en horno fuerte, precalentado por 10 minutos .Luego encendienda el grill y  cocine 5 minutos.<br />
        <br />
        &#8226;  Sirva  en un plato de a dos con la  salsa  tártara.<br />
        <br />
        &#8226;  Sirva  acompañados de puré con salsa de tomates</p>
