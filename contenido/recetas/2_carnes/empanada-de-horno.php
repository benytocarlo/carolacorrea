	<h2>Empanadas de horno</h2>
      <h4>Ingredientes</h4>
      <p><strong>Para la masa</strong><br />
        • 1 kilo de harina cernida<br />
        • 200 grs aceite<br />
        • &frac12; litro de salmuera (agua con sal)<br />
        <strong>Para el relleno</strong><br />
        • 1 &frac12;  kilo de asiento picana<br />
     	• 4 cebollas grandes<br />
        • 4 cucharadas de aceite<br />
        • 1 cucharadita de comino<br />
        • 2 cucharadas ají de color<br />
        • 20 aceitunas descarozadas <br />
        • 40 pasas <br />
        • 4 huevos duros<br />
        • Sal y pimienta <br />
        • Cilantro "opcional"<br /></p>
        
      <h4>Preparación</h4>
      <p>• Cierna la harina, deje en un bol y forme un hueco en el centro<br />
        <br />
        • Incorpore el aceite y la salmuera de a poco<br />
        <br />
        • Mezcle el ají de color con aceite e incorpórelo a la masa<br />
        <br />
        • Forme una masa blanda, amase y deje reposar 10 minutos<br />
        <br />
        • Uleree la masa hasta que quede con el grosor deseado y córtela con la 
ayuda de un plato<br />
		<br />
        • Para el relleno, pique la cebolla en pequeños cuadros y cocínela con 
Un poco de aceite por bastante tiempo, a fuego muy bajo<br />
        <br />
        • Incorpore la carne picada en cubitos y cocine hasta que se ablande<br />
        <br />
        • Condimente con sal, pimienta, comino, ají de color y ají en pasta<br />
        <br />
        • Deje enfriar. El cilantro se añade una vez frío el relleno<br />
        <br />
        <strong>Armado de las empanadas</strong><br />
        <br />
        • Deje una cucharada de pino en cada círculo de masa, añada una lámina de 
huevo duro, dos pasas y una aceituna<br />
        <br />
        • Moje los bordes de la masa con agua y cierre<br />
        <br />
        • Pinte las empanadas con clara de huevo para que queden brillantes y 
hornee por 30 minutos.<br />
      </p>
