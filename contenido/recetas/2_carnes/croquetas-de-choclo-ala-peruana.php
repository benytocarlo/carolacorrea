	<h2>Croquetas de choclo a la peruana con salsa de cilantro</h2>
      <h4>Ingredientes</h4>
      <p>• &frac12; kilo de choclos (de bolsa) o 6 choclos enteros rallados<br />
        • &frac14; cucharadita de jengibre<br />
        • 1 tarro de almejas<br />
        • &frac14;  taza de leche descremada<br />
        • 1 &frac12; taza de harina<br />
        • Sal y pimienta <br /></p>
         <h4>Para la salsa</h4>
      <p>• 1 atado de cilantro<br />
        • 4 cucharadas de aceite de oliva<br />
        • 2 cucharadas de yogurt natural<br />
        • Sal y pimienta<br /></p>
        
      <h4>Preparación</h4>
      <p>• En una procesadora, muela los choclos junto con la leche<br />
        <br />
        • Déjelos en un bol, mezcle con el jengibre, la harina, las almejas cocidas partidas en tiras finas, sal y pimienta<br />
        <br />
        • Caliente un sartén y forme croquetas de choclo ayudándose con las manos. Cocine con unas gotas de aceite por ambos lados<br />
        <br />
        • la salsa de cilantro, procese todo junto hasta que se forma la salsa<br />
        <br />
        • Sirva cómo picoteo acompañadas de  la salsa de cilantro.<br />
      </p>
