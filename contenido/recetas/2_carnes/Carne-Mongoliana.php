	<h2>Carne Mongoliana</h2>
      <h4>Ingredientes</h4>
      <p>• 1/2 kilo carne de vacuno (posta negra)<br />
        • ½  pimentón rojo<br />
        • ½  ají verde<br />
        • 1 zanahoria<br />
        • ½ brócoli<br />
        • 3 cucharadas de chancaca Deliciosa<br />
        • ½  bandeja de champiñones<br />
        • 1/2 taza caldo de vacuno<br />
        • 1 cucharadita de maicena<br />
        • salsa de soya<br />
        • pimienta y sal      </p>
      <h4>Preparación<br />
      </h4>
      <p>•	Corte la carne en tiras, el cebollín y el pimentón al sesgo, <br />
        <br />
        •	Saltee la carne en un sartén caliente con unas gotas de aceite.<br />
        <br />
        •	Agregue  el cebollín, pimentón, champiñón, zanahoria, brócoli (precocido) y cocine por cinco minutos<br />
        <br />
        •	Agregue la chancaca, previamente disuelta en una cucharada de agua<br />
        <br />
        •	Disuelva la maicena en agua fría e incorpórela a la preparación<br />
        <br />
        •	Incorpore  la  soya, pimienta, sal  sirva con arroz blanco<br />
        <br />
        TIP: Un secreto para lograr el brillo típico de las  recetas chinas, es incorporar una cucharadita de maicena disuelta en agua frío. Se añade al final de la preparación y le da  brillo  la consistencia. <br />
      </p>
