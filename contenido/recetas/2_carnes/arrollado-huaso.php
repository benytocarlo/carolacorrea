	<h2>Arrollado de Huaso</h2>
            <h4>Ingredientes</h4>
               <p>• Ingredientes  &quot;6 personas&quot;<br />
               • 1 ½ pulpa de cerdo<br />
               • 1/2 kg. de cuero de cerdo<br />
               • ¼  taza de vinagre de vino tinto<br />
               • 2 dientes de ajo<br />
               • Sal, pimienta y orégano<br />
               • 2 cucharadas de ají en pasta<br />
               • Cordel para amarra          </p>
            <h4>Preparación            </h4>
            <p>•	Retire toda la grasa de la carne y córtela    en tiras largas de 1 centímetro de ancho<br />
              <br />
              •	Condimente la carne con sal, pimienta, vinagre, ajo machacado, ají de color, orégano y déjelo marinar por un par de horas<br />
              <br />
              •	Corte el cuero en un cuadrado (el tamaño depende de la cantidad de cerdo que se va a utilizar, pero para 1 kilo de carne, debe ser un cuadrado de 15 cm. Aprox<br />
              <br />
              •	Deje el cuero sobre una tabla y ordene los trozos alargados de cerdo uno al lado de otro. <br />
              <br />
              •	Enrolle el cuero, formando el arrollado y amarre con hilo de cocina bien apretado<br />
              <br />
              •	Cueza el arrollado en un caldo (agua hirviendo, 3 hojas de laurel, 1 cebolla, 1 rama de apio) por 1 hora y media. Deje enfriar en la misma olla, escurra el exceso de agua y reserve.<br />
              <br />
              •	Sirva en láminas acompañado de ají en pasta y/o pebre</p></div>
