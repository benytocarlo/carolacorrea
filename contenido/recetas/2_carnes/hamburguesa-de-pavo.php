	<h2>Hamburguesas de Pavo Alemanas</h2>
      <h4>Ingredientes (5 personas)</h4>
      <p>
        • 1 bandeja de carne de pavo molida<br />
        • 5 pepinillos dulces<br />
        • 6 aceitunas negras<br />
        • 1 huevo<br />
     	• &frac12; taza de pan rallado<br />
        • 1 cucharada generosa de mostaza<br />
        • 1 cebolla morada picada y salteada<br />
        • 1 cuchadita de tomillo fresco<br />
        • 5 panes de hamburguesa<br />
        • 1 bandeja de berros<br />
        • 2 tomates en láminas<br />
        <strong>Salsa</strong><br />
        • &frac12; queso crema 0% grasa<br />
        • 1 yogurt natural<br />
        • 1/ atado de perejil<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• Para las hamburguesas, mezcla todos los ingredientes en un bol y condimenta<br />
        <br />
        • Arma las hamburguesas  y cocínalas en un sartén con gotas de aceite<br />
        <br />
        • Para la salsa, deja todos los ingredientes en  la Mini Blender hasta formar una salsa<br />
        <br />
        • Calienta el pan y unta cada uno con la salsa, encima deja la hamburguesa, luego el tomate y encima los berros<br />
      </p>
