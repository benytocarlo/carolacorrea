	<h2> Pechuga Rellena</h2>
                <h4>Ingredientes</h4>
                <p>• 1 pechuga de pollo<br />
                • 1 cebolla<br />
                • ¼ taza de agua<br />
                • 1 zanahoria<br />
                • ½ atado de acelgas <br />
                • 1 pimentón rojo<br />
                • 1 cucharada de mostaza<br />
                • Sal y pimienta</p>
                <h4>Para la Salsa</h4>
    			<p>• 1 taza de chancaca Deliciosa<br />
                • 1 ½  taza de vino tinto<br />
                • 1 cucharadita de maicena<br />
                • Sal y pimienta</p>
			    <h4>Para el Puré de Arvejitas</h4>
                <p>• ½ kilo de arvejitas<br />
                • 2 tazas de caldo de verduras<br />
                • Sal y pimienta       </p>
                <h4>Preparación</h4>
                <p>&#8226;	Abra la pechuga de pollo cortando a  lo largo<br />
                <br />
                &#8226; Una vez estirados los dos trozos de la pechuga, condimente con sal, pimienta y unte con mostaza<br />
                <br />
                &#8226; Cocine la zanahoria, la acelga y el pimentón hasta que estén blandos<br />
                <br />
                &#8226; Rellene la pechuga con bastones de zanahoria y el pimentón junto con las acelgas picadas<br />
                <br />
                &#8226; Enrolle la pechuga y amarre con hilo de cocina o junte con mondadientes<br />
                <br />
                &#8226; Selle la pechuga en un sartén caliente<br />
                <br />
                &#8226; Deje en una budinera la cebolla picada pluma, encima acueste las pechugas rellenas, agregue el agua, tape la budinera con papel aluminio y cocine por 20 minutos al horno<br />
                <br />
                &#8226; Para la salsa, derrita la chancaca junto con el vino. Una vez disuelto todo, agregue la maicena disuelta en agua fría<br />
                <br />
                &#8226; Para el puré, cocine las arvejitas con el caldo. Procese formando un puré y condimente <br />
                <br />
                &#8226; Corte la pechuga en trozos y sirva acompañado puré de arvejitas junto con la salsa de chancaca</p>
