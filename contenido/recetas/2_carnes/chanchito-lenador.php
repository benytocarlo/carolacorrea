	<h2>Chanchito Leñador</h2>
      <h4>Ingredientes</h4>
      <p>• 1 costillar de cerdo <br />
        • 1 cebolla<br />
        • 1 pimentón<br />
        • 1 taza de vino blanco<br />
        • Sal y pimienta<br />
        • 1 trozo de papel aluminio<br />
         <h4>Para la salsa</h4>
      <p>• 3 cebollas<br />
        • 1 pimentón rojo<br />
        • 1 bandeja de champiñones<br />
        • &frac12; taza de vino blanco<br />
        • Sal y pimienta<br />
      <h4>Preparación</h4>
      <p>• En una budinera grande, deje una capa de cebollas pluma, el vino blanco y acueste el costillar<br />
        <br />
        • Condimente el costillar con sal, pimienta y si desea, ají en pasta.<br />
        <br />
        • Tape la budinera con papel aluminio y cocine en un horno precalentado y a temperatura media por 1 &frac12; hora.<br />
        <br />
        • Retire del horno, corte en trozos y reserve.<br />
        <br />
        • Para la salsa, cocine la cebolla pluma con el pimentón y los champiñones en un sartén con gotas de aceite y sal.<br />
        <br />
         • Agregue el vino blanco y cocine a fuego bajo hasta que esté todo muy cocido, condimente.<br />
        <br />
         • Sirva el chanchito con la salsa encima y acompañe con lo que desee.<br />
      </p>
