	<h2>Asado Alemán a Mi Estilo</h2>
      <h4>Ingredientes</h4>
      <p>• ¾ kilo de carne molida de pavo<br />
        • 1 ½ cebolla <br />
        • 1 taza de avena<br />
        • 1 taza de salsa de tomates<br />
        • 3 huevos<br />
        • 1 sobre de queso rallado<br />
        • Hiervas frescas: albahaca – tomillo – perejil<br />
        • 2 tomates<br />
        • Sal y pimienta </p>
      <h4>Preparación</h4>
        <p><br />
          •	Sofría la cebolla.<br />
          <br />
          •	En un bol deje la carne, incorpore los huevos semi batidos, la avena, la salsa de tomates, la mitad del queso rallado, las hierbas picadas, sal y pimienta<br />
          <br />
          •	Haga un brazo de reina y al centro deje una capa de tomates partidos en láminas. Cubra con más carne y luego espolvoree el resto de queso rallado<br />
          <br />
          •	Hornee a fuego alto por 20 minutos.<br />
          <br />
          •	Sirva sobre tallarines salteados</p>
        </p>
