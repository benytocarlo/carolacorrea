	<h2>Pulmay</h2>
             <h4>Ingredientes</h4>
               <p>&#8226; 2&nbsp;cebollas pluma<br />
  &#8226; aceite de oliva o vegetal<br />
  &#8226; 1 litro de vino Blanco<br />
  &#8226; ¾ litro de agua<br />
  &#8226; 5 trutros de polllo cortos deshuesado sin cuero<br />
  &#8226; 5 conchas de picorocos<br />
  &#8226; 5 trozos de cerdo ahumado<br />
  &#8226; 1 kilo de choros maltones <br />
  &#8226; 1 kilo de almejas<br />
  &#8226; 5 papas<br />
  &#8226; Sal y pimienta            </p>
            <h4>Milcaos</h4>
               <p>&#8226; 3 papas crudas  ralladas<br />
                 &#8226; 3  papas cocidas molidas<br />
                 &#8226; 1  taza de harina<br />
                 &#8226; sal  y pimienta</p>
               </p>
             <h4>Preparación</h4>
               <p><br />
                 &#8226;  En un &ldquo;perol&rdquo; (olla grande) sofría la  cebolla, con el aceite, sal, pimienta hasta se ablande<br />
                 <br />
                 &#8226; Incorpore el pollo,  dore por ambos lados y retire<br />
                 <br />
                 &#8226; Agregue el vino blanco y cocine por 5  minutos<br />
                 <br />
                 &#8226; Deje en la olla las almejas, los choros,  los picorocos, el cerdo ahumado, el pollo y finalmente las papas. Integre el  agua<br />
                 <br />
                 &#8226; Tape la olla y cocine por 35 minutos a  fuego lento<br />
                 <br />
                 &#8226; Para los milcaos, mezcle la papa rallada  con la papa cocida, la harina, sal y pimienta, forme bolitas tipo hamburguesas  y cocine al horno por 12 minutos. Si es necesario incorporar más harina, hágalo<br />
                 <br />
                 &#8226;   Sirva en un plato un trozo de pollo,  otro de cerdo, un picoroco, almejas y choritos y acompañe con una tacita de  café con el caldo<br />
                 <br />
                 &#8226;  Acompañe también con los milcaos y pebre</p></div>
