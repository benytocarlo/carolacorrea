	<h2>Riñones al Jerez</h2>
             <h4>Ingredientes</h4>
               <p>&#8226; 1 riñón de vacuno, en mitades (1, 5 kilo)<br />
  &#8226; 2 cucharadas de harina<br />
  &#8226; 1 taza  de Jerez seco<br />
  &#8226; ½ taza  de caldo de vacuno<br />
  &#8226; 1 cucharadita de azúcar<br />
  &#8226; Arroz graneado o puré de papas para acompañar<br />
  &#8226; Vinagre<br />
  &#8226; Sal y pimienta</p>
               </p>
             <h4>Preparación</h4>
               <p><br />
                 &#8226;  Limpie  los riñones y dejarlos en un bol cubiertos de agua y media taza de vinagre por  dos horas. Luego, lávelos,  retire toda  la grasa y corte en trozos<br />
                 <br />
                 &#8226;  En  una olla, dore y condimente  el riñones,  con gotas de aceite, por tres minutos, retirar de la olla y reservar<br />
                 <br />
                 &#8226;  En  esa misma olla, agregue unas gotas de aceite   y la harina, revolver y agregar el jerez y el caldo y cocine por cinco  minutos<br />
                 <br />
                 &#8226;  Agregue  los riñones a la olla y cocine hasta que dejen de votar jugo. No debe cocinarse  por mucho rato ya que se ponen duros<br />
                 <br />
                 &#8226;  Agregar  el azúcar y reservar<br />
                 <br />
               &#8226; Servir  acompañado de arroz blanco</p></div>        
