	<h2>Pollo al Limón</h2>
             <h4>Ingredientes</h4>
               <p><br />
                 &#8226; 6 trutros  de pollo sin piel<br />
                 &#8226; ½ taza de  vino blanco<br />
                 &#8226; ½ taza de  cebolla molida<br />
                 &#8226; 1 diente  de ajo<br />
                 &#8226; 2  cucharaditas de jengibre rallado<br />
                 &#8226; 1/3 taza  de hojas de cilantro<br />
                 &#8226; 1/3 taza  de hojas de menta<br />
                 &#8226; 1  cucharadita de cúrcuma<br />
                 &#8226; 1 cucharadita  de merquén <br />
                 &#8226; 1 taza de  yogurt natural<br />
                 &#8226; 1 taza de  tomates en cubitos<br />
                 &#8226; Acompañar  con arroz perfumado en canela</p>
               </p>
            <h4>Preparación
              <br />
            </h4>
               <p><br />
                 1.- En una juguera muela la cebolla, ajo,  jengibre, cilantro y menta.<br />
                 <br />
                 2.- En una olla cocine la mezcla anterior,  incorpore el vino y deje evaporar <br />
                 <br />
                 3.- Integre la cúrcuma, sal y merquén.<br />
                 <br />
                 4.- Agregue el yogurt,  los tomates y revolver.<br />
                 <br />
                 5.- Incorpore el pollo y mezclar con la  salsa. Tape la olla y cocinar por 15 minutos.<br />
                 <br />
            6.- Servir con arroz a la canela            </p>
