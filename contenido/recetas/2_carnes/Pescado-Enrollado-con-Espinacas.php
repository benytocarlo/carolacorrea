	<h2>Pescado Enrollado con Espinacas</h2>
             <h4>Ingredientes</h4>
               <p>&#8226;  4  filetes de merluza<br />
                 &#8226;  1 atado  de espinacas<br />
                 &#8226;  2  zanahorias<br />
                 &#8226;  2 tazas  de berros<br />
                 &#8226;  1 tarro  chico de pimentón<br />
                &#8226;  1 taza  de salsa blanca saludable
                 </p>
               </p>
             <h4>Preparación</h4>
               <p><br />
                 &#8226;  Limpie el pescado, retirando la piel y  las espinas<br />
               <br />
               &#8226;   Aliñe con sal y pimienta cada filete y  cubra con espinacas blanqueadas<br />
               <br />
               &#8226;   Rellene con bastones de espinacas y  berros<br />
               <br />
               &#8226;   Enrolle el pescado, cierre con dos  mondadientes y cocine al horno<br />
               <br />
               &#8226;   Prepare una salsa de pimentón, moliendo  éstos y mezclándolos con la salsa blanca<br />
               <br />
               &#8226;   Sirva un espejo de salsa de pimentón y  encima deje el rollo de pescado</p>
               <p><strong>Dato</strong><br />
                 <p>Esta forma de cocinar el pescado, permite que  mantenga todos los jugos  dentro del  papel aluminio  y además que mantenga la  temperatura.</p>
