	<h2>Calamares Rellenos</h2>
      <h4>Ingredientes</h4>
      <p>• 12  calamares pequeños<br />
        • 6  láminas de jamón ahumado<br />
        • 1  ramita de romero<br />
        • 1  tomate<br />
        • 1 taza  de Salsa Blanca Saludable<br />
        • Sal y  pimienta</p>
      </p>
      <h4>Para la Salsa</h4>
        <p><br />
          • 1 taza  de vino tinto<br />
          • ½ taza  de soya<br />
          • 1  cucharada de maicena<br />
          • Sal y  pimienta</p>
          </p>
        </p>
        <h4>Preparación</h4>
        <p><br />
          • Limpiar  los calamares<br />
          <br />
          • Picar  el jamón y el tomate en pequeños cubos, juntar con la salsa blanca, el romero&nbsp; y condimentar <br />
          <br />
          • Rellenar  los calamares con la pasta con ayuda de una manga.<br />
          <br />
          • Para  la salsa: juntar el vino y la soya y cocinar por 5 a 10 minutos<br />
          <br />
          • Una vez que  hierva agregar la maicena previamente disuelta en vino.<br />
          <br />
          • Servir  sobre puré de habas</p>
          </p>
        </p>
