	<h2>Empanadas de mariscos</h2>
      <h4>Ingredientes (6 personas)</h4>
      <p><strong>Para la masa</strong><br />
        • &frac12; kilo de harina<br />
        • 1 taza de aceite de maravilla<br />
        • 1 pizca de ají de color<br />
        • 1 &frac12; taza de salmuera (sal con agua tibia)<br />
        <strong>Para el relleno</strong><br />
        • 2 cebollas<br />
     	• 1 diente de ajo<br />
        • Choritos<br />
        • Machas<br />
        • Almejas<br />
        • Ají de color<br />
        • Comino<br />
        • Cilantro<br />
        • Merquén<br /></p>
        
      <h4>Preparación</h4>
      <p>• Deje la harina en un bol, haga un hoyo al centro y deje ahí el aceite, el ají de color y la salmuera tibia<br />
        <br />
        • Junte todo y amase por cinco minutos hasta que la masa esté blanda<br />
        <br />
        • Haga un pino con la cebolla, el ajo, el comino, la sal y el merquén, cocine a fuego lento y añada los mariscos picados<br />
        <br />
        • Una vez listo y frío el relleno, agregue cilantro picado<br />
        <br />
        • Usleree la masa formando círculos del tamaño de un plato de pan<br />
		<br />
        • Pinte las orillas con agua, rellene con el pino frío y cierre las empanadas<br />
        <br />
        • Cocínelas en un horno precalentado, por 20 minutos<br />
        <br />
        • Sirva de inmediato<br />
      </p>
