	<h2>Pollo al Cogñac</h2>
             <h4>Ingredientes</h4>
             <p>&#8226; 1  pollo <br />
  &#8226; 1 ½  cebolla<br />
  &#8226; 4 tazas de vino blanco<br />
  &#8226; 1/2 taza de cognac<br />
  &#8226; Aceite<br />
  &#8226; Pimienta entera<br />
  &#8226; Sal</p>
            <h4>Preparación</h4>
               <p>&#8226;  Corte el pollo y  saltear cada presa en una olla caliente, <br />
                 con unas gotas de aceite.<br />
                 <br />
                 &#8226;  Agregue la cebolla  cortada pluma y cocinar por tres minutos<br />
                 <br />
                 &#8226; Integre el cognac,  el vino blanco y condimentar<br />
                 <br />
                 &#8226; Cocine con la olla  tapada a fuego lento por 40 minutos<br />
                 <br />
            &#8226; Acompañe con papas  fritas &ldquo;cocinadas al horno</p>
