	<h2>Carne mechada con fettuccini y salsa de tomates casera</h2>
      <h4>Ingredientes (6 personas)</h4>
      <p>• 1 trozo de punta de ganso<br />
        • 1 zanahoria<br />
        • 1 pimentón<br />
        • 1 vara de apio<br />
        • 1 taza de agua<br />
        • 2 cebollas<br />
        • &frac14; taza de harina<br />
        • 1 paquete de fettuccini<br />
        con un rico dressing</p>
         <h4>Para la salsa de tomates</h4>
      <p>• 5 tomates<br />
        • 1 taza de salsa de tomates<br />
        • 3 hojas de laurel<br />
        • 1 cebolla<br />
        • 1 zanahoria<br />
        • 1 diente de ajo<br />
        • Sal y pimienta</p>
      <h4>Preparación</h4>
      <p>• Tome la carne y "meche" con un cuchillo haciendo orificios por todos lados<br />
        <br />
        • En esos orificios introduzca bastones de zanahoria, apio y pimentón<br />
        <br />
        • Condimente la carne con sal y pimienta. Pásela por harina y dore en un sartén por todos sus lados<br />
        <br />
        • una budinera cubra con una cama de cebollas. Disponga la carne, agregue el agua, tape con papel aluminio y cocine por 40 minutos a fuego muy bajo.<br />
        <br />
        • Para la salsa de tomates, cocine la cebolla picada en cubitos a fuego lento, agregue los tomates partidos en 8, la salsa de tomates, la zanahoria picada, el ajo, el laurel, sal y pimienta.<br />
        <br />
         • Cocine por 30 minutos a fuego bajo, ideal con tostador.<br />
        <br />
         • Cocine los fettuccini al dente.<br />
        <br />
        • Sirva los fettuccini, agregue la salsa de tomates y monte sobre estos un trozo de carne mechada.
      </p>
