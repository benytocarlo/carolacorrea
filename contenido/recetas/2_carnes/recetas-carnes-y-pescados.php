	<h2>Albóndigas de Pavo y Manzanas</h2>
            <h4>Ingredientes</h4>
               <p>• 1/2 kilo de carne molida de pavo.<br />
                 • 1/2 taza de puré de manzanas.<br />
                 • 1  cebolla en cubitos.<br />
                 • 1 taza de pan rallado.<br />
                 • ½ taza de harina<br />
                 • sal y pimienta<br />
  <br />
  <h4>Para la  salsa:</h4>                 
  <p>• 4 hojas de salvia.<br />
                 • 2 tazas de salsa de tomates<br />
                 • 1 taza de agua<br />
                 • Sal y pimienta.</p>
  <br />
  <br />
  <h4>Preparación</h4>
               <p>•  Para las albóndigas, en un bowl juntar todos  los ingredientes, menos la harina<br />
                 <br />
                 • Formar albóndigas de tamaño mediano,  pasarlas por harina y reservar<br />
                 <br />
                 • Para la salsa, dejar todos los ingredientes  en una olla y una vez que hierva, cocinar ahí las albóndigas.<br />
                 <br />
                 • Cocinar hasta que estén cocidas<br />
                 <br />
               • Servir sobre tallarines</p>
