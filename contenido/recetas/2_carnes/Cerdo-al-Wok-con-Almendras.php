	<h2>Cerdo al Wok con Almendras</h2>
      <h4>Ingredientes</h4>
      <p>• ½ kilo de carne de cerdo, sin grasa<br />
        • ¼ taza de salsa de soya<br />
        • ¼ taza de agua<br />
        • 2 cucharadas de jugo de limón<br />
        • 1 cucharada de miel<br />
        • 1 cucharada de maicena<br />
        • 1 diente de ajo<br />
        • ½ ají verde<br />
        • 1 pimentón rojo<br />
        • 1 cebolla<br />
        • ¼ taza de almendras tostadas<br />
        • Arroz blanco con cebollín para acompañar</p>
      </p>
      <h4>Preparación<br />
        <br />
      </h4>
      <p>•	En un bol coloque el cerdo cortado en cubos, rociar con 2 cucharadas de soya y reservar<br />
        <br />
        •	En otro bol, mezcle el resto de la soya, el agua, el jugo de limón, la  miel, la maicena, el ají y reservar<br />
        <br />
        •	Caliente un wok cocinar el cerdo. Incorporar el ajo, pimentón en cubitos, cebolla en gajos.<br />
        <br />
        •	Incorpore la mezcla se soya<br />
        <br />
        •	Agregue las almendras y sirva con arroz<br />
        <br />
      </p>
      </p>
