	<h2>Terrina de Palta y Centolla</h2>
            <h4>Preparación</h4>
             <p>&#8226; 3 paltas maduras<br />
  &#8226; 1 tomate maduro<br />
  &#8226; 1 cucharada de ciboulette picado<br />
  &#8226; 5 hojas de albahaca picada<br />
  &#8226; ½  kilo de  centolla  / 1 pechuga de pollo<br />
  &#8226; ½ limón (su jugo)<br />
  &#8226; ½ cucharada de mayonesa light<br />
  &#8226; Pimienta fresca<br />
  &#8226; Sal<br />
             </p>
          <h4>Para la salsa :</h4>
               <p>&#8226;  ½ taza de soya<br />
                 &#8226; ½  cucharadita de aceite de sésamo tostado<br />
                 &#8226; 2  cucharadas de aceite de oliva             </p>
<h4>Preparación</h4>
               <p>&#8226; Moler las paltas, mezclarlas con el tomate en brunoise  (pelado, sin semillas y picado en cubitos muy pequeños). <br />
               Incorporar el ciboulette picado, la albahaca picada,  sal y pimienta fresca.<br />
               <br />
               &#8226; Aparte condimentar la centolla con jugo de limón,  pimienta fresca, sal y mayonesa.<br />
               <br />
            &#8226; En una procesadora emulsionar el aceite de oliva, el  aceite de sésamo y la soya. </p>
   <h4>Montaje del plato</h4>
               <p>&#8226; Rellenar un timbal con la palta.<br />
                 <br />
                 &#8226; Coronar con centolla.<br />
                 <br />
                 &#8226; Desmoldar el timbal al centro de un plato. <br />
                 <br />
                 &#8226; Dejar  encima una pata de centolla.<br />
                 <br />
                 &#8226; Pintar el plato con la salsa.<br />
                 <br />
                 &#8226; Decorar con una hoja fresca de albahaca.</p>
