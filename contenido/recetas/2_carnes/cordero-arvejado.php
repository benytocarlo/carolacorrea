	<h2>Cordero Arvejado</h2>
      <h4>Ingredientes</h4>
      <p>• 4 presas de cordero<br />
        • 1 diente de ajo<br />
        • 1 cebolla "pluma"<br />
        • 1 taza de vino blanco <br />
        • 1 zanahoria<br />
        • 1 pimentón <br />
     	• 1 taza y media de arvejitas<br />
        • 1 1 tomate<br />
        • 12 papas fritas "al horno"<br />
        • 1 pizca de comino <br />
        • 1 cucharadita de ají de Color<br />
        • Pimienta  y sal<br /></p>
        
      <h4>Preparación</h4>
      <p>• En un sartén dore las presas de cordero "sin grasa"<br />
        <br />
        • Añada la cebolla, la zanahoria en medias lunas, el pimentón en cubitos, el tomate en gajos, el comino, el ají de color, el diente de ajo machacado, sal y pimienta.<br />
        <br />
        • Cocine por cinco minutos.<br />
        <br />
        • Incorpore el vino blanco y cocine un par de minutos para que el alcohol se evapore.<br />
        <br />
        • Agregue las arvejitas y cocine por 20 minutos con la olla tapada.<br />
        <br />
         • Sirva con papas salteadas al perejil, cocinadas al horno.<br />
      </p>
