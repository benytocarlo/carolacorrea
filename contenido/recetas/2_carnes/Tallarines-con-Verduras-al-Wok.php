	<h2>Tallarines con Verduras al Wok</h2>
            <h4>Ingredientes</h4>
               
            <p>&#8226; ½  paquete de tallarines<br />
              &#8226; ½  pimentón rojo<br />
              &#8226; ½  pimentón verde<br />
              &#8226; ½  brócoli<br />
              &#8226; 2 ramas  de apio<br />
              &#8226; 1 taza  de brotes de alfalfa<br />
              &#8226; 1  zanahoria<br />
              &#8226; 1  cebolla &ldquo;pluma&rdquo;<br />
              &#8226; ¼ taza  de salsa de soya<br />
              &#8226; 1  cucharadita de maicena<br />
              &#8226; ¼ taza  de almendras fileteadas<br />
              &#8226; 1 trozo  (1/4 kilo) tofu<br />
              &#8226; Sal y  pimienta</p>
            </p>
    <h4>Preparación</h4>
               <p><br />
                 &#8226;  Saltee la cebolla en gotas de aceite,  introduzca el tofu en cubos pequeños (bocado)<br />
                 <br />
                &#8226;  Integre los pimentones cortados en  juliana, la zanahoria cortada en medias lunas, el apio picado y el brócoli en  trozos. Cocine<br />
                 <br />
                 &#8226;  Agregue la maicena disuelta en la soya,  las almendras, los brotes y los tallarines previamente cocidos &ldquo;al dente&rdquo;</p>
