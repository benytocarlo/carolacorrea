	<h2>Picante de Gallina</h2>
             <h4>Ingredientes</h4>
               <p>&#8226; ½ marraqueta<br />
  &#8226; 1 pechuga de pollo cocida, desmenuzada y  mantenida en el caldo de cocción<br />
  &#8226; Caldo de cocción del pollo <br />
  &#8226; 1 paquete de galletas de soda<br />
  &#8226; ½ litro de leche descremada<br />
  &#8226; 1 cucharada de cúrcuma o palillo<br />
  &#8226; 3 ajíes amarillo<br />
  &#8226; Para el caldo: 1 rama de apio – 1 cebolla – 1  diente de ajo<br />
  &#8226; Sal y pimienta</p>
               </p>
          <h4>Preparación</h4>
               <p><br />
                 &#8226;  Prepara  un caldo con agua, cebolla, apio y ajo, deja que hierba<br />
                 <br />
                 &#8226;  Cocina  la pechuga en el caldo, retira y desmenuza con los dedos<br />
                 <br />
                 &#8226;  Retira  el centro y las pepas de los ajíes y procesa en la mini blender con media taza  de agua<br />
                 <br />
                 &#8226;  Remoja  las galletas  previamente molidas  y la marraqueta en leche <br />
                 <br />
                 &#8226;  En  una olla deja el ají, incorporar el palillo, y cocina<br />
                 <br />
                 &#8226;  Agrega  el pan y las galletas remojadas. Agrega el pollo <br />
                 <br />
                 &#8226;  Incorpora  caldo de a poco hasta que tome una consistencia cremosa<br />
                 <br />
               &#8226;  Sirve  con arroz blanco</p></div>
