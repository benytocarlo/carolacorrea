	<h2>Sándwich del Huerto</h2>
    <br />	
            <p> • 4 panes italianos</p>
            <h4>Mayonesa saludable: </h4>
            <p>• 3 zanahorias cocidas<br />
                 • 1 pote de yogurt natural<br />
                 • ½ atado de ciboulette<br />
                 • Una pizca de sal<br />
                 • 1 berenjena asada<br />
                 • ½ taza de tomates deshidratados<br />
                 • 2 tazas de rúcula<br />
                 • 4 huevos</p>

            <h4>Preparación:</h4>
            <p>• Para la mayonesa, cuece las zanahorias y procesa en una juguera con el yogurt. Incorpora el ciboulette picado y condimenta</p>
            <p>• Lava y corta  la berenjena en rodajas gruesas, grilla en un sartén con un poco de sal y gotas de aceite de oliva</p>
            <p>• Hidrata los tomates en agua caliente y cocer los huevos en un sartén antiadherente Condimenta los tomates con aceite de oliva y orégano</p>
            <p>• Condimenta la rúcula con sal  y aceite de oliva</p>
            <p>• Calienta el pan en el horno y unta con la mayonesa de zanahoria.</p>
            <p>• Encima deja las berenjenas, los tomates, el huevo y la rúcula<br /> </p>
