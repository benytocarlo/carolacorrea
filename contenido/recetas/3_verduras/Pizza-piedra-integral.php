	<h2>Pizza Piedra Integral </h2>
                <h4>Ingredientes: </h4>
                <p>&#8226;  Agua tibia
               
                <br />
                &#8226;  Sal de mar fina
                30 grs de levadura fresca <br />
                &#8226;  200 grs de harina de avena
                400 grs<br />
                de harina de trigo, sin polvos de hornear <br />
                &#8226;  1 cucharada de aceite de oliva <br />
                &#8226;  30 grs de azúcar negra<br />
                &#8226;  200 grs de harina de centeno<br />
                &#8226;  200 grs de harina integral súper fina <br />
                &#8226;  El relleno: lo qué quieras!! </p>
                <h4>Preparación</h4>
                <p>&#8226;  Mezcla los 4 tipos de harinas en un recipiente, luego sazona con sal y mezcla nuevamente, forma una corona y en el centro coloque la levadura junto con el azúcar negra, agua tibia y el aceite de oliva, deje reposar de esa forma hasta que comiencen a formarse globitos. <br />
                <br />
                &#8226;                   Una vez formados los globitos une los ingredientes con las manos comenzando desde el centro hacia fuera, agregue agua a medida que necesite<br />
                <br />
                &#8226;  Una vez unidos vierte sobre la mesa comience a amasar hasta conseguir una masa suave y lisa. Cubre la masa y deje reposar hasta que duplique su volumen<br />
                <br />
                &#8226;  En ese momento, si quieres que te quede una masa piedra, toma la masa y golpéala, logrando que se comprima el aire entregado por la levadura. <br />
                <br />
                &#8226;  Deja la masa en la lata del horno, unta con salsa de tomates, deja encima los ingredientes que tú quieras&nbsp; y hornea por 25 minutos. 
                </p>
