	<h2>Verduras con Atún</h2>
            <h4>Ingredientes</h4>
            <p>• 1 paquete de caracoles<br />
              • 2 cebollines<br />
              • 1 zapallo  italiano<br />
              • 1 bandeja de champiñones<br />
              • 1 pimentón rojo<br />
              • 1 tarro de atún al agua<br />
              • ¼ taza de soya<br />
              • ¼ taza de maní<br />
            • ½  atado de perejil</p>
      <h4>Preparación</h4>
               <p>• Cocinar los caracoles hasta que estén al dente<br />
                 • En un wok saltear los cebollines, el pimentón, los champiñones y el zapallo italiano  picados no demasiado finos.<br />
                 • Agregar el maní, soya,  el perejil, el atún  y el aceite de oliva<br />
               • Servir con arroz</p>
