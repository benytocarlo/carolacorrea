	<h2>Sándwich para Picotear</h2>
               <p>&#8226; 6 ajíes  verdes<br />
                 • ½  filete de reineta<br />
                 • 1 tallo  de apio picado<br />
                 • ½ taza  de jugo de limón<br />
                 • ¼ taza  de aceite de oliva<br />
                 • 3  cucharadas de cilantro<br />
                 • 2  cebolla morada<br />
                 • Sal y  pimienta<br />
               • 1 lata  de jugo de tomates</p>
            <h4>Preparación:<br />
            <br />
          </h4>
               <p>• Lavar y cortar el zapallito en làminas delgadas <br />
                 <br />
                 • Ordenar en un plato grande, el zapallito bordeando el plato<br />
                 <br />
                 • Cortar la palta en finas làminas y ordenarlas al centro del plato<br />
                 <br />
                 • Hacer el aliño, mezclando los ingredientes e incorporar los tomates partidos en cuartos<br />
                 <br />
                 • Condimentar el Carpaccio con el aliño, esparcir las alcaparras, el ciboulette picado sino y el queso cortado en finas láminas por todo el carpaccio<br />
                 <br />
                 • Al centro decorar con una hoja de lechuga crespa</p></div>
