	<h2>Champiñones Rellenos</h2>
      <h4>Ingredientes</h4>
      <p>•  18 champiñones<br />
        • ½ pimentón rojo<br />
        • ½ pimentón verde<br />
        • 3 cebollines<br />
        • 1 taza de salsa blanca <br />
        • Sal, pimienta y salvia<br />
        Para  las espinacas:            <br />
        • ½ kilo de espinacas  congeladas<br />
        • ½ taza de  salsa blanca</p>
<h4>Preparación:<br />
        <br />
    </h4>
      <p>• Ahueque los champiñones y  saltéelos  con un poco de aceite de oliva</p>
      <p>• Retire del  sartén y reserve</p>
      <p>• Aparte saltee las verduras  picadas en brunoise (pequeños cubitos). El pimentón  y los champiñones que retiramos</p>
      <p>• Incorpore  la salsa blanca.</p>
      <p>• Para las espinacas,  descongélelas  y quíteles toda el agua.</p>
      <p>• Píquelas bien finitas y  mézclelas  en un sartén con la salsa  blanca. </p>
      <p>• Condimente con sal y  pimienta</p>
      <p>• Rellene los champiñones,  llévelos a un horno precalentado por 12 minutos y sirva sobre las espinacas &ldquo;a  la crema&rdquo;</p>
