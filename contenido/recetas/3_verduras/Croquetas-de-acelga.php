	<h2>Croquetas de Acelga</h2>
      <h4>Ingredientes</h4>
      <p>• 2 paquetes de acelga<br />
        • 2 huevos<br />
        • ½ taza de pan rallado<br />
        • 1 taza de harina<br />
        • Sal y pimienta<br />
        • Servir sobre espirales </p>
      <h4>Preparación</h4>
      <p>• Cocer y picar la acelga en juliana<br />
        <br />
        • En un bol, mezclar la acelga con el pan, sal,  pimienta y huevos.<br />
        <br />
        • Dar forma ovalada a cada croqueta<br />
        <br />
        • Dorar en un sartén  y terminar la cocción en el horno<br />
        <br />
        • Servir sobre pasta cocida, mezclada con aceite  de oliva, ciboulette y queso rallado</p>
