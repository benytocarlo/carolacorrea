	<h2>Panqueques de Espinacas</h2>
<h4>Ingredientes</h4>
               <p>• 2 tazas de harina<br />
                 • 2 tazas de leche semi descremada<br />
                 • 1 taza de espinacas cocidas, picadas y escurridas<br />
                 • 2 huevos<br />
                 • Relleno:	 ¼ kilo de jamón de pavo ahumado<br />
                 • 2 zapallitos italianos<br />
                 • 1 cebolla picada<br />
                 • 1 taza de salsa blanca saludable<br />
                 • ¼ taza de almendras<br />
                 • Salsa blanca para cubrir<br />
               • ½ sobre de queso rallado</p>
            <h4>Preparación:<br />
          </h4>
               <p>• Procese la leche con las espinacas formando una pasta verde</p>
               <p>• Para los panqueques, bata los huevos, incorpore la leche, incorpore la harina, una pizca de sal  y las espinacas cocidas y escurridas</p>
               <p>• Haga los panqueques en un sartén de teflón pintado apenas con aceite y reserve</p>
               <p>• Sofría la cebolla, saltee el jamó picado en juliana y los zapallitos italianos en finos cubos.  Incorpore las almendras picadas y la salsa blanca</p>
               <p>• Prepare una salsa blanca saludable</p>
               <p>• Hornee los panqueques sobre un bol dado vuelta, por tres minutos, para que tome forma de canastillo</p>
               <p>• Rellene los panqueques con la mezcla y báñelos en salsa blanca</p>
               <p>• Espolvoree con queso rallado<br />
               </p>
