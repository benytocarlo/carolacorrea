	<h2>Lasaña Mixta Vegetariana</h2>
      <h4>Ingredientes</h4>
      <p>• 3 zapallitos italianos<br />
        • 3 berenjenas<br />
        • 2 cebollas <br />
        • 4 tomates<br />
        • 4 tazas de salsa blanca saludable <br />
        • 2 tazas de salsa de tomates<br />
        • Orégano fresco<br />
        • 1 sobre de queso rallado<br />
        • 1 taza de pan rallado<br />
      • Sal y pimienta</p>
      <h4>Preparación:<br />
        <br />
      </h4>
      <p>• Lave y corte los zapallitos y las berenjenas a lo largo</p>
      <p>• Cote  las cebollas en gajos y cocine en una olla hasta que se ablanden</p>
      <p>• Corte los tomates en láminas delgadas</p>
      <p>• Prepare una salsa blanca saludable y mézclela con la salsa de tomates, orégano fresco, sal y pimienta</p>
      <p>• En una budinera familiar, deje una capa de salsa, una de zapallitos, una de  berenjenas, una de queso rallado,  una de tomates, una de salsa y así sucesivamente</p>
      <p>• Termine con salsa, orégano fresco y tomate</p>
      <p>• Espolvoree con pan rallado y cocine en un horno precalentado por media hora</p>
