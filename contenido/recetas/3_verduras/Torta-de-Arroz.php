	<h2>Torta de Arroz</h2>
               <h4>Ingredientes</h4>
            <p>&#8226; 2 tazas de arroz<br />
              • 1 caldo Maggi de verduras<br />
              • 4 tazas de agua<br />
              • 1 tarro de atún al agua<br />
              • 1 atado de espinacas<br />
              • 3 tomates<br />
              • 4 ramas de orégano fresco <br />
            • Sal y pimienta</p>
            <h4>Salsa:</h4> 
            <p>
              • 1 taza de salsa de tomates<br />
              • 1 sobre de salsa blanca Maggi<br />
              • 1 sobre de queso rallado</p>
            <h4>Preparación</h4>
            <p>• Sofría el arroz, agregue un caldo Maggi de verduras, cubra con agua hirviendo y cocine por 20 minutos. Una vez cocido, agregue el orégano</p>
            <p>• En un molde de quede, deje una capa de arroz</p>
            <p>• Deje una capa de atún, otra de arroz, otra de espinacas previamente salteadas, otra de arroz  y termine con tomate</p>
            <p>• Prepare una salsa con la salsa blanca Maggi, la salsa de tomates, el queso rallado, sal y pimienta</p>
            <p>• Caliente la torta en un horno precalentado, por 20 minutos, retire del horno, bañe con la salsa y sirva</p>
