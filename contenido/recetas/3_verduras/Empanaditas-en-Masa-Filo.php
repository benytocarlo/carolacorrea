	<h2>Empanaditas en Masa Filo</h2>
      <h4>Ingredientes</h4>
      <p>•  6 láminas  de masa filo<br />
        • 1 bandeja de champiñones<br />
        • 1 atado  de espinacas<br />
        • ½  taza de salsa blanca saludable<br />
        • 1/2 cebolla<br />
      • 1/2 taza de aceite</p>
      <h4>Preparación:</h4>
      <p><br />
          • Sofría la cebolla picada en  pequeños cubos, agregue la espinaca previamente lavada y picada en fina  juliana, cocine por 5 minutos y cuando este blanca, agregue los champiñones  laminados. <br />
        <br />
      • Cocine por 5 minutos más y  enfríe, quite el exceso de líquido y revuelva con la salsa blanca.      </p>
        <p>• Extienda la masa y corte en 4  láminas, guarde el resto siempre bien envuelto, para que no se seque. </p>
      <p>• Disponga el relleno en un  extremo y vaya ir doblando desde una punta hacia dentro, formado siempre  triángulos, envolviendo hasta terminar el largo de la masa, al fin de la masa  pegue con un poco de aceite. </p>
      <p>• Hornee en horno precalentado  hasta que la masa este dorada.</p>
