	<h2>Carpaccio de Palta y Zapallitos</h2>
      <h4>Ingredientes</h4>
      <p>• 2 palta  Hass<br />
        • 1  cucharada de alcaparras<br />
        • 2  cucharadas de queso parmesano<br />
        • 1  zapallo italiano<br />
        • 1 atado  de ciboultette<br />
        • 6  tomates cherry<br />
        • Aliño:  3 cucharadas de aceite de oliva<br />
        • 6 cucharadas de jugo de limón<br />
        • 1 cucharadita de aceto balsámico<br />
        • Lechuga  para decorar</p>
      <h4>Preparación:</h4>
        <p><br />
          •	Lavar y cortar el zapallito en láminas delgadas<br />
          <br />
          •	Ordenar en un plato grande, el zapallito bordeando el plato<br />
          <br />
          •	Cortar la palta en finas láminas y ordenarlas al centro del plato<br />
          <br />
          •	Hacer el aliño, mezclando los ingredientes e incorporar los tomates partidos en cuartos<br />
          <br />
          •	Condimentar el Carpaccio con el aliño, esparcir las alcaparras, el ciboulette picado sino y el queso cortado en finas láminas por todo el carpaccio<br />
          <br />
          •	Al centro decorar con una hoja de lechuga crespa</p>
        </p>
