	<h2>Alcachofas Rellenas Asadas</h2>
      <h4>Ingredientes</h4>
      <p> •  4 alcachofas blanqueadas<br />
        •  2 cebollines<br />
        •  ½ pimentón rojo<br />
        •  1 cucharadita de ají de color<br />
        •  8 láminas de jamón de  pavo Sopraval<br />
        •  1 taza de choclo<br />
        •  2 cucharadas de cilantro<u></u><br />
        •  1 ½   tazas de  salsa blanca saludable<br />
        •  1 cucharadas de queso rallado</p>
<h4>Preparación:</h4>
      <p>•  Cocine  las alcachofas por un rato hasta que queden &ldquo;casi&rdquo; cocidas</p>
      <p>• Ahueque cada una,  dejando las hojas de los bordes para que puedan sostener el relleno</p>
      <p>• Retire la carne de  las hojas y reserve</p>
      <p>• Saltee los  cebollines, el pimentón, el ají de color la carne de las hojas de las  alcachofas y el atún</p>
      <p>• Incorpore la salsa  blanca, la sal, la pimienta y el cilantro. Termine con queso y gratine al horno.</p>
      <p>• Para la salsa blanca  saludable, 2 cucharadas de aceite de oliva, una cucharada  y media de harina y un litro de leche  descremada. Nuez moscada y sal baja en sodio</p>
