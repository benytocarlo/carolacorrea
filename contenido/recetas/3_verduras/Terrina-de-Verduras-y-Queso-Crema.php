	<h2>Terrina de Verduras y Queso Crema</h2>
               <h4>Ingredientes</h4>
                 <p>• 1 pan  de queso crema &quot;light&quot;<br />
                   • ¼  paquete  de queso rallado<br />
                   • ½ atado  de espinacas<br />
                   • 1 atado de ciboulette<br />
                   • 2 huevos<br />
                   • 1 tarro chico de pimentón<br />
                   • 2 zanahorias<br />
                   • 6 espárragos<br />
                   • Sal y pimienta<br />
                 </p>
            <h4>Preparación:
            <br /></h4>
            <p>• En un bol bata el queso crema con el cebollín y el parmesano</p>
            <p>• Incorpore los huevos de a uno y bata hasta formar una pasta consistente y cremosa</p>
            <p>• Forre un molde con papel film y cubra con hojas de espinacas blanqueadas, precaviendo que no queden espacios vacíos y que caigan las hojas del molde para luego cubrir.</p>
            <p>• Deje una capa de crema, luego una capa de espárragos cocidos, luego crema, luego morrones, crema, zanahorias  y crema para terminar</p>
            <p>• Una vez listo, envuelva con las hojas de espinaca y cierre bien con papel film</p>
            <p>• Hornee a baño maría por 40 minutos en un horno suave y  precalentado</p>
            <p>• Una vez cocido, espere que se enfríe completamente para desmoldar</p>
            <p>• Sirva con salsa de pimentón<br />
            </p>
