	<h2>Ajíes Verdes Rellenos con Ceviche de Reineta</h2>
	<h4>Ingredientes</h4>
      <p>• 6 ajíes  verdes<br />
        • ½  filete de reineta<br />
        • 1 tallo  de apio picado<br />
        • ½ taza  de jugo de limón<br />
        • ¼ taza  de aceite de oliva<br />
        • 3  cucharadas de cilantro<br />
        • 2  cebolla morada<br />
        • Sal y  pimienta<br />
        • 1 lata  de jugo de tomates </p>
      <h4>Preparación:</h4>
      <p>•   Lave y corte los ajíes verdes a lo largo, retire sus  semillas y deje hervir por 1 minuto</p>
      <p>•  Para el cebiche, corte el pescado en cubos pequeños  y alíñelo con el aceite, el jugo de Limón, el  apio, el cilantro, sal, pimienta y una cebolla picada en cubitos</p>
      <p>•  Corte la otra cebolla a la pluma y deje marinar en  aceite de oliva, limón y sal</p>
      <p>•  Rellene los  ajíes con el cebiche y sirva sobre un plato acompañado del jugo de tomates</p>
      <p>•  Decore con la cebolla marinada</p>
