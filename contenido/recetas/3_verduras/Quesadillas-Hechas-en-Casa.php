	<h2>Quesadillas Hechas en Casa</h2>
<h4>Ingredientes</h4>
            <p>&#8226; Para las tortillas:<br />
              • 1 taza de agua tibia<br />
              • 2 tazas de harina <br />
              • ½ cucharadita de polvos de hornear<br />
              • 1 pizca de sal<br />
			</p>
            <h4>Para el relleno:<br /></h4>
              <p>• 2 zapallos italianos<br />
              • 1 cebolla pluma<br />
              • 1 pimentón rojo<br />
              • 1 pimentón verde<br />
              • 1 pechuga de pollo<br />
              • Guacamole &quot;palta – tomate – cebolla – cilantro&quot;<br />
            • Crema ácida saludable &quot;1 yogurt natural – 2 cucharadas de jugo de limón&quot;</p>
              </p>
            <h4>Preparación:<br /> </h4>
               <p>• Para las tortillas</p>
               <p>• En un bol deje el agua tibia, incorpore el polvo de hornear y la sal</p>
               <p>• Incorpore la harina  hasta formar una masa que se despegue de las manos</p>
               <p>• Forme bolas de 40 gramos. Estire con un uslero dando forma redonda</p>
               <p>• Deje las tortillas en un sartén caliente por 45 segundos. Luego délas vuelta y cocínelas por un minuto. Una vez que la masa se infle, délas vuelta nuevamente y retire</p>
               <p>Para el relleno:</p>
               <p>• Saltee los zapallos italianos picados en cubitos, la cebolla pluma, los pimentones en juliana fina y el pollo con una pizca de aceite</p>
               <p>• Condimente con sal y pimienta</p>
               <p>• Rellene las quesadillas con la carne,  guacamole y crema ácida saludable</p>
