	<h2>Crujiente de Queso Camembert</h2>
      <h4>Ingredientes</h4>
      <p>• 2 Queso  camembert<br />
        •  6 nueces molidas<br />
        •  3 cucharadas de coco rallado<br />
        •  2 huevos<br />
        •  ½  taza de  harina<br />
        •  3 pimentones rojos<br />
        •  1 taza de azúcar<br />
        •  4 cucharadas de agua<br />
        •  1 pizca de pimienta de Cayena</p>
<h4>Preparación:</h4>
        <br />
        <p>• Pase los quesos por harina, luego por huevo batido y luego uno lo pasa por las nueces molidas y el otro por el coco rallado</p>
      <p>• Precaliente el horno a fuego alto y los deja cocinar por 10 minutos, No deben derretirse mucho por dentro, solo un poco</p>
      <p>• Pique los pimentones en tiritas delgadas y cocínelos en una olla junto con el agua y el azúcar hasta que se forme una mermelada. </p>
      <p>• Condimente con pimienta de cayena o un poco de merquén. Deje enfriar</p>
      <p>• Sirva el que queso tibio bañado con los pimentones y acompañe con tostadas horneadas<br />
      </p>
