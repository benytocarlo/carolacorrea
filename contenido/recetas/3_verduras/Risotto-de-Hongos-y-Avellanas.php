	<h2>Risotto de Hongos y Avellanas</h2>
<h4>Ingredientes</h4>
               <p>•  2 tazas de quínoa<br />
                 • 4 tazas de caldo de verduras<br />
                 • 1/2 zapallo italiano<br />
                 • 1/2 bandeja de champiñones<br />
                 • ½ coliflor<br />
                 • ¼  brócoli<br />
                 • ¼ taza de almendras tostadas<br />
                 • 1  taza de salsa blanca saludable<br />
                 • 3 cucharadas de aceite de oliva<br />
               </p>
            <h4>Preparación:<br />
          </h4>
               <p>               • Cocine la quínoa en el caldo, por 20 minutos (1 de quínoa por dos de caldo)</p>
               <p>• Saltee las verduras picadas, agregue la quínoa, la salsa blanca, el aceite de oliva y sirva</p>
               <p>• Dore unas rodajas de zapallo italiano, y déjelos de base en el plato</p>
               <p>• Sobre éstos, sirva en un timbal, la  quínoa.</p>
               <p>• Decore  tomates cherry y hierbas<br />
               </p>
