	<h2>Chupe de Alcachofas</h2>
      <h4>Ingredientes</h4>
      <p>• 1 cebolla<br />
        • ½ pimentón rojo<br />
        • 1 diente de ajo<br />
        • 1 cdta. ají de color<br />
        • ¼  taza vino blanco<br />
        • ¼ taza caldo de verduras<br />
        • 8 rodajas pan de molde<br />
        • 2 tazas de leche descremada<br />
        • 6 alcachofas<br />
        • 3 tazas de Salsa Blanca Saludable<br />
        • 3 cucharadas de queso rallado </p>
      <h4>Preparación</h4>
      <p><br />
        • Saltear la cebolla picada fina en aceite  junto con el ajo y el pimentón rojo. <br />
        <br />
        • Agregar el vino blanco. Incorporar el pan previamente remojado en  leche. Cocinar.<br />
        <br />
        • incorporar la carne de alcachofa y condimentar con sal, pimienta y  ají de color.<br />
        <br />
        • Agregar el caldo para que no se seque y luego la salsa blanca.  Cocinar.<br />
        <br />
        • Dejar en pailas de greda y encima dejar queso rallado. Gratinar. </p>
