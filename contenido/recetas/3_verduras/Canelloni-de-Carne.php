	<h2>Canelloni de Carne</h2>
      <h4>Ingredientes</h4>
      <p> •  1 paquete  de lasaña <br />
        • ½  Pechuga de  pollo desmenuzada<br />
        •  1 trozo de  carne cocida y desmenuzada<br />
        • 1 atado de ciboulette<br />
        •  2 tazas de  salsa de tomates<br />
        •  1 taza de  salsa blanca  saludable al pimentón</p>
<h4>Preparación:<br />
        <br />
    </h4>
      <p> •  Cueza las láminas  de lasaña en agua hirviendo y deje escurrir</p>
      <p>•  Aparte  cocine la pechuga de pollo y carne y desmenúcela con los dedos</p>
      <p>•  Junte en  un bol el pollo, la carne,  la salsa  blanca y el ciboulette. </p>
      <p>•  Condimente  con sal y pimienta</p>
      <p>•  Rellene  las láminas de lasaña y enróllelas, formando un canelón</p>
      <p>•  Déjelos  en una budinera y bañe con salsa blanca al  pimentón</p>
