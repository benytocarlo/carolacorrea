	<h2>Pasta Deliciosa</h2>
<h4>Ingredientes</h4>
               <p>• 1 paquete de tallarines<br />
                 • 6 tomates cherry<br />
                 • ¼ de jamón de pavo<br />
                 • Cabecitas de espárragos<br />
                 • Orégano fresco<br />
               • 2 tazas de salsa blanca saludable</p>
            <p><h4>Preparación:<br />
         
              </h4>
            <p>• Cocer los tallarines en agua hirviendo</p>
               <p>• Saltear el jamón cortado en tiras, junto con los tomates partidos en cuartos.</p>
               <p>• Agregar las cabezas de espárragos y el orégano fresco.</p>
               <p>• Integrar la salsa blanca saludable</p>
               <p>• Mezclar los tallarines con la salsa y servir</p>
