	<h2>Sopaipillas con Pebre</h2>
               <h4>Ingredientes &ldquo;10 personas&rdquo;</h4>
               <p>•  1 taza de  puré de zapallo<br />
                 •  3 tazas de  harina<br />
                 •  1  cucharadita de levadura &ldquo;con una pizca de azúcar y agua tibia para que leude&rdquo;<br />
                 •  3  cucharadas de aceite vegetal<br />
                 •  1 pizca de  sal<br />
                 •  1 taza de  caldo de cocción del zapallo <br />
                 •  1 atado de  ciboulette</p>
               <h4>Pebre </h4>
                 <p>•  1 zapallo  italiano <br />
                 •  2 tomates,  en cubitos<br />
                 •  6  aceitunas <br />
                 •  1 cebolla <br />
                 •  1 atado de  cilantro <br />
                 •  Aceite,  sal y pimienta</p>
<h4>Preparación:<br />
          </h4>
               <p>•  Deje la  harina en un bol y haga un hueco en el centro</p>
               <p>•  Disponga  en el centro el puré de zapallo, el aceite, la sal, la levadura &ldquo;leudada&rdquo; y el  ciboulette picado fino. De poco agregue el caldo tibio (la cantidad necesaria  para que la masa no se pegue en las manos) </p>
               <p>•  Amase  hasta que quede la masa blanda.</p>
               <p>•  Forme las  sopaipillas de tamaño pequeño, pinche con un tenedor</p>
               <p>•  Hornee en un  horno precalentado a 200 grados por 6 minutos.</p>
               <p>•  Para  el  pebre, corte los zapallitos y el tomate en cubitos de igual tamaño, incorpore  las aceitunas picadas, la cebolla picada, el cilantro picado y condimente con  sal, pimienta y jugo de limón </p>
