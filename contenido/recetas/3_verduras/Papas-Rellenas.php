	<h2>Papas Rellenas</h2>
            <h4>Ingredientes</h4>
               <p>• 2 barras de Queso  Crema light<br />
                 • 2 kilos de papas,  limpias y peladas<br />
                 • 3 yemas de huevos<br />
                 • ¼ k de jamón de  pavo picado fino<br />
                 • 1 sobre de queso  rallado<br />
                 • ¾ taza de harina  sin polvos de hornear<br />
               •  Sal y Pimienta a gusto</p>
<h4>Preparación:<br />
          </h4>
               <p>• Cocer<strong> </strong>las papas en agua con sal hasta que  estén blandas.</p>
               <p>• Moler<strong> </strong>las papas calientes por el prensa papa  hasta formar un puré sin grumos.</p>
               <p>• Condimentar<strong> </strong>el puré con sal y pimienta. Agregar  las yemas de huevo y la harina, revolver bien. Reservar.</p>
               <p>• Juntar el queso  crema, el jamón. Llevar al freezer por unos 20 minutos y retirar.</p>
               <p>• Formar<strong> </strong>óvalos con el puré de papas, rellenar  con una cucharada de la mezcla de quesos y tapar bien con el puré de papas. Dar  forma con la ayuda de un poco de harina en las manos. </p>
               <p>• Seguir el  procedimiento hasta terminar con el puré de papas y la mezcla de quesos.  Pasarlas por el queso rallado</p>
               <p>• Cocinar las papas  en un horno precalentado a fuego mediano alto</p>
               <p>•  Servir con ensalada</p>
