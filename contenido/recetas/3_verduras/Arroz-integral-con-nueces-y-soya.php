	<h2>Arroz Integral con Nueces y Soya</h2>
      <h4>Ingredientes (6 personas)</h4>
      <p>• 2 tazas de arroz integral<br />
        • 4 tazas de agua<br />
        • ½ taza de soya<br />
        • 1 taza de nueces<br />
        • 1 cebolla<br />
        • 1 zanahoria<br />
        • 1 pimentón<br />
      	• Sal y pimienta </p>
      <h4>Preparación:<br />
        <br />
      </h4>
      <p>• Dejar remojar el arroz en agua por 4 horas.</p>
      <p>• Hacer un sofrito con la cebolla, la zanahoria y el pimentón, todo cortado en pequeños cubitos.</p>
      <p>• Agregar el arroz, ya hidratado y seco y cocinar por cinco minutos.</p>
      <p>• Condimentar, agregar las nueces.</p>
      <p>• Agregar la soya y el agua hirviendo.</p>
      <p>• Cocinar por 25 a 30 minutos.</p>
