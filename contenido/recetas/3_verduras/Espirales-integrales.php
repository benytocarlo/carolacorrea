	<h2>Espirales Integrales Deliciosos</h2>
      <h4>Ingredientes (4 personas)</h4>
      <p>• 1 paquete de espirales integrales<br />
        • 2 paltas<br />
        • 1 tomate<br />
        • 1 atado de albahaca<br />
        • ½ taza de nueces<br />
        • 5 cucharadas de salsa de soya<br />
        • 5 cucharadas de aceite de oliva<br />
      	• Sal </p>
      <h4>Preparación:<br />
        <br />
      </h4>
      <p>• Cocer al dente los espirales y dejarlos enfriar.</p>
      <p>• Picar la palta y el tomate en cubitos.</p>
      <p>• Dejar los espirales en un bol, agregar las verduras picadas, la albahaca picada fina, las nueces picadas.</p>
      <p>• Condimentar con soya, aceite de oliva y sal.</p>
      <p>• Comer sola o acompañada con un trozo de pollo o pescado.</p>
