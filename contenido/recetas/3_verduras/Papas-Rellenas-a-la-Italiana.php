	<h2>Papas Rellenas a la Italiana</h2>
<h4>Ingredientes</h4>
               <p>• 1 kilo de papas<br />
                 • 3 yemas de huevo<br />
                 • 2 cucharadas de harina<br />
                 • 1 atado de ciboulette<br />
                 • ¼ kilo de jamón de pavo<br />
                 • 1 atado de acelga<br />
                 • Queso rallado<br />
                 • 2 tomates<br />
                 • Albahaca fresca<br />
               • Salsa de tomates con callampas secas</p>
            <h4>Preparación:<br />
    </h4>
               <p>• Muela las papas, incorpore las yemas, el ciboulette, el harina.</p>
               <p>• Forme una pasta que se despegue de los dedos y de la forma de un gran huevo</p>
               <p>• Pique los  tomates en brunoise, la albahaca en juliana, el jamón en juliana y la acelga. </p>
               <p>• Junte todo, formando una pasta</p>
               <p>• Rellene las papas y páselas por queso rallado</p>
               <p>• Páselas por queso rallado y cocine al horno por 15 minutos</p>
               <p>• Haga una rica salsa de tomates con carne y callampas secas</p>
               <p>• Sirva sobre la salsa de tomates<br />
               </p>
