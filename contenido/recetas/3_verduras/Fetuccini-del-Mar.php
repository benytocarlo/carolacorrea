	<h2>Fetuccini del Mar</h2>
      <h4>Ingredientes</h4>
      <p>• 1 paquete de fetuccini<br />
        • 1 bolsa de surtido de mariscos congelados<br />
        • 4 tazas de salsa blanca saludable<br />
        • 1 tarro de pimiento morrón<br />
        • ½ atado de ciboulette<br />
        • 1 cebolla morada en cubitos<br />
        • ½ taza de vino blanco<br />
      • Sal y pimienta</p>
<h4>Preparación:<br />
        <br />
      </h4>
      <p>• Cuece la pasta hasta que quede al dente, retira, pasa por agua fría y vuélvela a la olla con un poco de aceite de oliva</p>
      <p>• Para la salsa, en un sartén, sofríe la cebolla y el pimentón. Añade vino blanco y cocina dos minutos más. Condimenta con sal y pimienta</p>
      <p>• Agrega los mariscos previamente descongelados, la salsa blanca, el ciboulette y cocina hasta que se forma la salsa</p>
      <p>• Sirve los fetuccini y báñalos con la salsa</p>
      <p>• Decora con ciboulette picado<br />
      </p>
