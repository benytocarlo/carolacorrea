	<h2>Bolitas de Queso Crema y Almendras</h2>
	<h4>Ingredientes (8 personas)</h4>
      <p>• 1 pan de queso crema Light<br />
        • 1 tarro de pimentón rojo<br />
        • ½ taza de almendras fileteadas y tostadas<br />
        • 1 cucharada de salsa de soya<br />
      <h4>Preparación:</h4>
      <p>•  Moler el queso crema y formar bolitas de 1 cm.</p>
      <p>•  Añadir la soya y mezclar.</p>
      <p>•  Cortar el pimentón en tiras gruesas y rellenar cada bolita, dejando el pimentón escondido al centro de cada una.</p>
      <p>•  Pasar las bolitas por las almendras.</p>
      <p>•  Dejar las bolitas en una fuente y acompañar con diferentes tipos de galletas y tostadas.</p>
