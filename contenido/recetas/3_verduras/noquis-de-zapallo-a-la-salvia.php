	<h2>Ñoquis de Zapallo a la Salvia</h2>
      <h4>Ingredientes</h4>
      <p>• 200 grs de papas<br />
        • 200 grs de zapallo<br />
        • 100 grs de maicena<br />
        • 100 grs de harina<br />
        • 2 cucharaditas de nuez moscada<br />
        • 1 yema de huevo<br />
        • Cáscara de limón<br />
        • 2 cucharadas de queso rallado<br />
        • 4 tomates<br />
        • 10 hojas de salvia<br />
        • 1 cebolla<br />
        • Sal y pimienta<br />
      </p>
      <h4>Preparación:<br />
        <br />
      </h4>
      <p>• Cueza las papas en agua con sal, ajo, laurel, pimienta entera. Pele y pase por cedazo.</p>
      <p>• Lave y cueza el zapallo. Luego séquelo bien y páselo por el cedazo</p>
      <p>• Ponga el puré de papas y el de zapallo en un bol, agregue la harina, la maicena, la mitad del queso, la mitad de  salvia picada, la cáscara de limón rallada, sal pimienta y nuez moscada.</p>
      <p>• Divida la masa en muchos cilindros y corte  en 1 cm dándole la forma del ñoqui</p>
      <p>• Para la salsa, sofría cebolla en cubitos junto con setas picadas, el resto de la salvia picada y salsa de tomates</p>
      <p>• Cueza los ñoquis en abundante agua con sal. Una vez cocidos pase a un sartén con gotas de aceite de oliva y caldo vegetal. Sirva sobre la salsa de tomates y cubra con queso rallado</p>
