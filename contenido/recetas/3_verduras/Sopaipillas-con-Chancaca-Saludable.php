	<h2>Sopaipillas con Chancaca Saludable</h2>
               <h4>Ingredientes &ldquo;6 personas&rdquo;</h4>
               <p>•  1 taza de  puré de zapallo<br />
                 •  3 tazas de  harina<br />
                 •  1  cucharadita de levadura <br />
                 •  3  cucharadas de aceite vegetal<br />
                 •  1 pizca de  sal<u></u><br />
                 •  1 taza de  caldo de cocción del zapallo <br />
                 •  1 pan de  chancaca Deliciosa<br />
                 •  4 clavos  de olor<br />
                 •  Cascarita  de naranja<br />
                 •  1 palo de  canela<br />
                 </p>

            <h4>Preparación:<br />
            </h4>
               <p>•  Deje la  harina en un bol y haga un hueco en el centro, dejando el puré de zapallo, el  aceite, la sal, la levadura  y de a poco  agregue el caldo tibio (la cantidad necesaria para que la masa no se pegue en  las manos)</p>
               <p>•  Amase  hasta que quede la masa blanda.</p>
               <p>•  Forme las  sopaipillas del tamaño que prefiera, pinche con un tenedor</p>
               <p>•  Hornee en  un horno precalentado a 200 grados por 6 minutos.</p>
               <p>•  Derrita la  chancaca (250 grs) con una taza de agua,   canela,  clavo de olor y naranja</p>
               <p>•  Una vez  derretida la chancaca y cocidas las sopaipillas, deje las segundas dentro de la  chancaca y sirva</p>
