	<h2>Empanaditas de Zapallo, Espinacas y Almendras</h2>
  <h4>Ingredientes (20 Unidades)<br />Para la masa  </h4>
	<p>• ½ kilo de harina sin polvos<br />
       • 1 cucharadita de sal baja en sodio<br />
       • ½ taza de aceite vegetal<br />
       • 1 ½ taza de agua fría<br />
       • 1 cucharadita de vinagre blanco<br />
      </p>
      <h4>Para el relleno:</h4>
      <p>• ¼ kilo de zapallo <br />
        • 1 sobre de queso rallado<br />
        • ½ atado de espinacas</p>
<h4>Preparación:<br />
        <br />
      </h4>
      <p>• Para la masa, mezclar la harina con la sal, dejar un hueco al centro y dejar el agua, el aceite y el vinagre.</p>
      <p>• Amasar bien hasta que la masa quede blanda y dejar reposar media hora tapada en el refrigerador.</p>
      <p>• Estirar la masa y dividirla en cuatro partes. Cortar cada parte en 5.</p>
      <p>• Cortar círculos de 12 cm de diámetro.</p>
      <p>• Para el relleno, cocer el zapallo envuelto en papel aluminio (al horno) hasta que esté blando. Cocer en agua las espinacas, retirarles el agua.</p>
      <p>• Procesar ambos hasta formar un puré espeso y con consistencia. Condimentar y agregar el queso y las almendras.</p>
      <p>• Rellenar las empanaditas, doblarlas y hornearlas a fuego alto por 25 minutos.</p>
