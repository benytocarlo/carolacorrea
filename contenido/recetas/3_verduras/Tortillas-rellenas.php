	<h2>Tortillas Rellenas</h2>
            <h4>Ingredientes</h4>
            <p>• 3  huevos<br />
              • 1  bandeja de champiñones<br />
              • 1 atado  de ciboulette<br />
              • Sal y  curry<br />
              • 1 atado  de espinacas<br />
              • 5  láminas de jamón de pavo<br />
              </p>

    <h4>Dressing:</h4>
              <p> • Mostaza – aceite de oliva – aceto balsámico</p>
      <h4>Preparación</h4>
               <p>• Para  la tortilla, batir los huevos, agregar los champiñones picadoe en láminas, el  ciboulette picado fino y condimentar.<br />
                 <br />
                 • Formar  dos tortillas &ldquo;planas&rdquo;<br />
                 <br />
                 • Para  el relleno, picar finamente la espinaca, hacer el aliño en la mini Blender,  condimentar<br />
                 <br />
                 • Formar  un sándwich con las tortillas y rellenarlo con las espinacas<br />
                 <br />
                 • Servir  en trozos con un tomate cherry pinchado.
                 </p>
