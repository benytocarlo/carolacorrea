	<h2>Pancitos de Choclo</h2>
<h4>Ingredientes</h4>
               <p>Masa para el pan<br />
                 • 3 ½  tazas de harina<br />
                 • 2 cucharadas de levadura<br />
                 • 2 huevos<br />
                 • 1 yema<br />
                 • ¼ taza de aceite vegetal<br />
                 • ¼  taza de leche descremada <br />
                 • 1 ½  taza de choclo rallado<br />
                 • 2 ramas de albahaca fresca<br />
                 • 6 aceitunas<br />
               • Sal </p>
            <h4>Preparación:<br />
            <br />
          </h4>
               <p>•  Para los pancitos, dejar en un bol la harina, hacer un hueco al centro, dejar la levadura, los huevos, el aceite, la leche, la sal, la albahaca, el choclo, las aceitunas picadas y mezclar.</p>
               <p>• Amasar bastante hasta que quede una masa suave. Dejar leudar 15 minutos en un lugar húmedo</p>
               <p>• Formar círculos pequeños y hornear a temperatura media alta por 15 minutos.</p>
               <p>• Servir como aperitivo.</p>
