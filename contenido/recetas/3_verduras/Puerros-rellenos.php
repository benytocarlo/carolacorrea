	<h2>Puerros Rellenos</h2>
            <h4>Ingredientes: </h4>
            <p>&#8226; 6 Puerros <br />
            &#8226; 1 taza de zetas<br />
&#8226; 1 atado de espinacas<br />
&#8226; ½ taza de nueces <br />
&#8226; 1 taza de Salsa Blanca Saludable "espesa"<br />
&#8226; 1 sobre de queso rallado<br />
&#8226; Salsa de zetas: ½ taza de champiñones <br />
&#8226; 1 tarro de salsa de tomates <br />
&#8226; 2 tazas de Salsa Blanca Saludable <br />
<br /><h4>Preparación</h4>
<p>
  &#8226;  Cocer un poco los puerros, abrirlos y reservar. Picar en fina juliana las espinacas, previamente lavadas, las nueces en trozos e hidratar los champiñones. Juntar todo en un bol e incorporar la salsa blanca y el queso rallado formando una pasta espesa. Abrir los puerros por el centro y rellenar. <br />
  <br />
  &#8226;  Hornear por 15 minutos a fuego suave, retirar e incorporar la salsa de champiñones.<br />
  <br />
&#8226;  Para la salsa de champiñones, procesar las champiñones,, mezclar con la salsa de tomates y la salsa blanca en una olla. Condimentar. Decorar con ciboulette picado </p>
