	<h2>Ensalada con Queso Crema</h2>
      <h4>Ingredientes</h4>
      <p>• Lechuga,&nbsp;  rúcula, tomates, pepino, perejil<br />
      </p>
      <h4>Dresing</h4>
      <p>• 1 cucharada de mostaza:<br />
        • 3 cucharadas de aceto balsámico<br />
        • 3 cucharadas de soya<br />
        • ¼ taza de maní<br />
        • ½ pan de queso crema<br />
        • Sal</p>
      </p>
      <h4>Preparación:</h4>
      <p>• Lavar bien los vegetales<br />
        <br />
        • Para el dresing, dejar todos los ingredientes en la mini blender y procesar hasta formar una pasta ligera<br />
        <br />
        • Ordenar todo en una fuente y bañar con la salsa</p>
