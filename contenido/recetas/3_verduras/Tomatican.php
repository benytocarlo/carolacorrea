	<h2>Tomaticán</h2>
               <h4>Ingredientes</h4>
            <p>&#8226; 6 tomates maduros<br />
              • ½  kg de carne <br />
              • 3 cebollas<br />
              • 3 choclos en granos<br />
              • 1 diente de ajo, opcional<br />
              • 2 cucharadas de ají de color<br />
            • Sal, pimienta y orégano </p>
            </p>
            <h4>Preparación:<br />
          </h4>
               <p>• Corte la carne en tiras, golpéelas suavemente con una cuchara y se saltee ligeramente en aceite de oliva. </p>
               <p>• Incorpore el  tomate en gajos y la cebolla pluma</p>
               <p>• Agregue el choclo en granos, el orégano y condimente</p>
            <p>• Cocine tapado, a fuego lento hasta que las verduras estén cocidas.</p>
               <p>• Sirva con papas cocidas.<br />
               </p>
</div>

