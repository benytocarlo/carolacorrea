	<h2>Pantrucas</h2>
      <h4>Ingredientes</h4>
      <p>
        • <strong>Para la masa</strong><br />
        • 3 tazas de harina<br />
        • 1 huevo<br />
        • Salmuera tibia<br />
     	• Sal<br />
        • <strong>Para el caldo</strong><br />
     	• 1 esqueleto de pavo, pollo o vacuno<br />
        • 1 caldo Maggi de gallina<br />
        • 1 cebolla<br />
        • 1 pimentón<br />
        • 2 ramas de apio<br />
        • 1 litro de agua<br />
        • Sal, pimienta y hoja de laurel<br /><br />
        • Papas en cubos<br />
        • Huevos<br />
        • Perejil<br />
        </p>
        
      <h4>Preparación</h4>
      <p>• Para el caldo, sofría en una olla la cebolla cortada a la pluma, el pimentón cortado en tiras, la rama de apio en trozos y el esqueleto. Cocine por 5 minutos<br />
        <br />
        • Incorpore el agua, el caldo Maggi, la sal, la pimienta y el laurel y cocine por 40 minutos<br />
        <br />
        • Una vez hecho el caldo, cuele rescatando solo el líquido<br />
        <br />
        • En el caldo, cocine las papas en cubos y una vez listas, incorpore la masa de pantrucas costándola con las manos<br />
        <br />
        • Para la masa, deje  la harina en un bol, al centro deje el huevo e incorpore de a poco la salmuera. Amase hasta conseguir una masa blanda.<br />
        <br />
        • Usleree la masa formando tiras delgadas. Ir cortando con la mano la masa en cuadrados de 4 centímetros.<br />
        • Antes de servir, añada perejil.<br />
      </p>
