	<h2>Pan Integral</h2>
<h4>Ingredientes &quot;Para dos Moldes&quot;</h4>
               <p>• 3 tazas de harina integral<br />
                 • 1 taza de harina<br />
                 • ¼ taza de linaza<br />
                 • ¼ taza de nueces picadas (opcional)<br />
                 • ½ zanahoria rallada<br />
                 • 5 cucharadas soperas de aceite de oliva<br />
                 • 1 cucharada de levadura<br />
                 • 1 cucharada de miel<br />
                 • 1 cucharada de azúcar morena<br />
                 • 1 ½ taza de agua tibia<br />
                 • 1 pizca de sal<br />
               </p>
            <h4>Preparación:<br />
            <br />
          </h4>
               <p>• Juntar las harinas y añadir la linaza, nueces, zanahoria.</p>
               <p>• Integrar el aceite de oliva.</p>
               <p>• Agregar la levadura disuelta en la miel y media taza de agua tibia.</p>
               <p>• Integrar el azúcar morena</p>
               <p>• Amasar la masa por 15 minutos. Es importante amasar bien.</p>
               <p>• Dejar la masa reposar por 1 ½ hora tapada con un paño y una bolsa plástica.</p>
               <p>• Luego, dejar el pan en dos moldes rectangulares y hornear a fuego fuerte 10 minutos y luego a fuego medio entre 15 y 20 minutos más.</p>
