	<h2>Tarta de Puerros con Base de Arroz</h2>
            <h4>Ingredientes</h4>
            <p>
              • Para la masa <br />
              • 2 tazas de arroz cocido<br />
              • 1 atado de cebollín<br />
              • 1 cucharada de aceite<br />
              • ½  sobre queso rallado<br />
              • 1 cucharada de perejil<br />
              • 1 huevo<br />
         <h4>Para el  Relleno</h4>
               <p>• 5  puerros<br />
• 3  huevos<br />
• 1 pote  de yogurt natural<br />
• ½  sobre de queso rallado<br />
• Sal,  pimienta y estragón<br />
               <h4>Preparación</h4>
           <p>• Para la masa, mezcle bien todos los ingredientes. Una vez lista la mezcla, aceite un molde y estire la masa cubriendo los bordes<br />
             <br />
             • Para el relleno, pique los cebollines y cocínelos en un sartén hasta que se ablanden<br />
             <br />
             • En un bol bata los huevos con el yogurt y la mitad del queso rallado, añada los puerros, el estragón y salpimente<br />
             <br />
             • Vierta la mezcla sobre la masa reservada y cocine por 20 minutos a temperatura media<br />
           </p>
