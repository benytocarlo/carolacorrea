	<h2>Roll de Queso Crema de Picoteo</h2>
            <h4>Ingredientes</h4>
            <p>• 1 pan de queso crema Light<br />
              • 1 palta<br />
              • 1 atado de ciboulette<br />
              • 1 zanahoria<br />
              • 1 pepino<br />
            • Semillas de sésamo negro</p>
<h4>Salsa</h4>
               <p>• 1 taza de vino añejo<br />
                 • 1 cucharada de azúcar<br />
                 • 1 cucharadita de maicena<br />
                 • Sal y pimienta<br />
               </p>
               <h4>Preparación</h4>
               <p>• Estirar el queso con un uslero y protegido entre dos láminas de papel film<br />
                 <br />
                 • Hacer una placa de palta y dejar por un momento <br />
                 <br />
                 • Cubrir el roll con la palta, dar vuelta y rellenar con los demás ingredientes.<br />
                 <br />
                 • Para la salsa, reducir el vino añejo junto con el azúcar sal y pimienta. Una vez que rompa el hervor, incorporar la maicena previamente disuelta en agua fría.<br />
                 <br />
                 • Servir el roll de queso en galletitas y bañar con la salsa de oporto</p>
