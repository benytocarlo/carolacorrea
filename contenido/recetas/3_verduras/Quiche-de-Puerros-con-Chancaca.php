	<h2>Quiche de Puerros con Chancaca</h2>
<h4>Ingredientes</h4>
               <p>&#8226; Para la Masa<br />
                 • 1/3 taza de aceite <br />
                 • 1/3 taza de agua helada<br />
                 • Sal y pimienta<br />
               • 2 tazas   de harina</p>
            <h4>Para el relleno:<br />
          </h4>
               <p>•  6 cebollines<br />
                 • 2 zapallos italianos rallados<br />
                 • 1/4  taza de chancaca derretida<br />
                 • 1 pizca de semillas de anís<br />
                 • 1 taza de salsa blanca saludable<br />
                 • 2 huevos ligeramente batidos<br />
               • ½  taza de queso rallado</p>
               <h4>Preparación<br /></h4>
                 <p> • Para la masa precaliente el horno a temperatura media. </p>
               <p>• En la procesadora coloque el aceite y el agua, agregue sal, pimienta y la harina. Procese hasta que la masa este homogénea. </p>
               <p>• Coloque la masa sobre una superficie lisa y  usleree  hasta que quede delgada.  Cocine por 10 minutos en un horno precalentado</p>
               <p>• En un sartén  cocine los cebollines por 5 minutos con un poco de aceite, agregue  la chancaca, el zapallo italiano y  el anís  y cocine 5 minutos más, hasta que los puerros estén blandos. </p>
               <p>• Condimente con sal y pimienta. Retire el sartén del fuego y deje entibiar. Agregue la salsa blanca, los huevos  y revuelva hasta mezclar. </p>
               <p>• Vierta la mezcla sobre la masa reservada, espolvoree el queso en la superficie y hornee  10 a 15 minutos, o hasta que el relleno esté firme. Sirva de inmediato<br />
               </p>
