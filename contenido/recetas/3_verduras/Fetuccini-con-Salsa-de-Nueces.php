	<h2>Fetuccini con Salsa de Nueces</h2>
      <h4>Ingredientes</h4>
      <p>• 1 paquete de fettuccini de espinacas<br />
        •  1 hoja de laurel<br />
        •  12 nueces picadas<br />
        •  ½ paquete de queso rallado<br />
        •  10 tomates deshidratados<br />
        •  1 caldo  de  verduras Maggi 50% menos sal<br />
        •  1 atado de albahaca<br />
        •  10 láminas de jamón de pavo<br />
        •  1 sobre de  salsa  blanca Maggi<br />
      •  Pimienta</p>
<h4>Preparación:<br />
        <br />
      </h4>
      <p>• Cueza  los fetuccini en agua con sal y una hoja de laurel</p>
      <p>• En  un sartén, cocine  el jamón picado en  cubitos, las nueces picadas, los tomates previamente hidratados y picados, la  albahaca picada y el caldo maggi de verduras 50% menos sal</p>
      <p>• Incorpore  la salsa blanca, la cual una vez lista, le incorpora el queso rallado</p>
      <p>• Cocine  los fettuccini al dente, sirva y bañe con la salsa</p>
