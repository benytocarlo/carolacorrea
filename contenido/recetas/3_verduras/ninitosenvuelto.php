	<h2>Niñitos envueltos</h2>
      <h4>Ingredientes (6 personas)</h4>
      <p>• 1 repollo mediano<br />
        • &frac12;  kg de carne de molida de pavo<br />
        • 1 taza de arroz<br />
        • 1 zanahoria rallada<br />
        • &frac12; cebolla<br />
        • Sal y pimienta<br />
        • Salsa de tomates<br />
      </p>
      <h4>Preparación:<br />
        <br />
      </h4>
      <p>• Blanquee las hojas de repollo, en una olla con agua hirviendo, dejarlo por dos minutos</p>
      <p>• Mezcle la carne molida, el arroz, la zanahoria, sal y pimienta</p>
      <p>• Rellene cada hoja con la mezcla y cocine en una olla con salsa de tomates</p>
      <p>• Sirva con la salsa de tomates y arroz blanco</p>
