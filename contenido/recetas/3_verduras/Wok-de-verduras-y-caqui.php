	<h2>Wok de Verduras y Caqui</h2>
            <h4>Ingredientes</h4>
            <p>• 2  zapallos italianos<br />
              • 1  bandeja de champiñones<br />
              • 2 ramas  de apio<br />
              • ½ taza  de almendras fileteadas<br />
              • 1 caqui  maduro<br />
              • 1 trozo  pequeño de rocotto<br />
              • 10  tomates cherry<br />
              • Salsa  de vino añejo<br />
              • 1 taza  de vino añejo<br />
              • 2  cucharadas de azúcar<br />
              • 1  cucharadita de maicena</p>
            <h4>Dressing:</h4>
			<p> • Mostaza – aceite de oliva – aceto balsámico</p>
			<h4>Preparación</h4>
               <p>• Hacer  la salsa, dejando el vino y la azúcar en una olla. Una vez que hierva agregar  la maicena disuelta en agua.<br />
                 <br />
                 • En  un wok dejar las verduras picadas en trozos del tamaño de un bocado. Agregar el  caqui en trocitos pequeños.<br />
                 <br />
                 • Cocinar  a fuego alto, condimentar, agregar las almendras y una vez listo, incorporar la  salsa y servir.</p></div>
