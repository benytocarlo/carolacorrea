	<h2>Ensalada de Porotos y Habas</h2>
      <h4>Ingredientes</h4>
      <p>• 1  taza de porotos<br />
• 1  kilo de habas<br />
• 15  tomates cherry<br />
• 1  cebolla morada<br />
• ½  atado de cilantro<br />
• 2  cebollines<br />
• ¼  taza de aceite de oliva<br />
• 4  cucharadas de vinagre de manzana<br />
• 1  cucharadita de mostaza<br />
• 1  taza de choclos<br />
• 5  cucharadas de miel de palma<br />
• Albahaca  fresca<br />
• Sal  y pimienta</p>
      <p>&nbsp; </p>
      <h4>Preparación:<br />
        <br />
      </h4>
      <p>• Remojar  y cocinar los porotos hasta que queden muy blandos</p>
      <p>• En  un bol mezclar los porotos ya cocidos, las habas cocidas, los tomates partidos  en dos, la cebolla picada a la pluma, el cilantro, los cebollines picados y  mezclar</p>
      <p>• En  una procesadora dejar el choclo, el aceite de oliva, el vinagre, la mostaza, la  miel de palma y condimentar</p>
      <p>• Procesar  hasta formar un aliño poco espeso, Si falta líquido se puede agregar agua o  leche</p>
      <p>• Mezclar  la ensalada con el aliño, añadir la albahaca picada muy fina y servir</p>
