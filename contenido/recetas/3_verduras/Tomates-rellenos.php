	<h2>Tomates Rellenos</h2>
            <h4>Ingredientes</h4>
            <p>• 6 tomates<br />
              • 12 hojas de espinacas<br />
              • 1 taza de choclo<br />
              • 2 palta hass<br />
              • 1 atado de ciboulette<br />
              • 1 atado de albahaca<br />
              • 1 yogurt natural<br />
              • Sal y pimienta         </p>
         <h4>Preparación</h4>
               <p>• Ahuecar y pelar los tomates<br />
                 <br />
                 • Blanquear las espinacas y dejarlas dentro del  tomate, para el color<br />
                 <br />
                 • Mezclar la palta en cubitos, el choclo, el  yogurt, el ciboulette, la albahaca, el yogurt y condimentar<br />
                 <br />
                 • Rellenar los tomates<br />
                 <br />
                 • Decorar con hojas de ciboulette y verduras
                 </p>
