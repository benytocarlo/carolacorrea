	<h2>Ensalada Fresca</h2>
      <h4>Ingredientes</h4>
      <p>•  Hojas de hojas verdes<br />
        • Quesillo o queso mozarella<br />
        • Albahaca fresca<br />
        • 2 cucharadas de queso parmesano  rallado<br />
        • 15 tomates cherry<br />
      </p>
      <h4>Para el dressing:</h4>
      <p>• ¼ taza de aceite de oliva <br />
        • ¼ taza de jugo de limón<br />
        • 1 cucharadita  de mostaza<br />
        • Sal y pimienta</p>
<h4>Preparación:<br />
        <br />
      </h4>
      <p>•  Lave bien las hojas (lechuga –  endivias – rúcula – berros)</p>
      <p>• Aparte en un bol, mezcle batiendo el  aceite, el limón, la mostaza, sal y pimienta</p>
      <p>• Una las verduras con el quesillo  cortado en cubos,  la albahaca fresca  picada y los tomates partidos en dos</p>
      <p>• Condimente con el dressing antes se  servir    </p>
