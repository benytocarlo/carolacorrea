	<h2>Tomates Asados</h2>
            <h4>Ingredientes</h4>
            <p>• 6  tomates grandes<br />
              • ½  pechuga de pollo<br />
              • 2  cebollines<br />
              • 1  zanahoria rallada<br />
              • 6  huevos<br />
              • 3  cucharadas de queso rallado<br />
              • 1 taza  de Salsa Blanca Saludable<br />
              • Sal y  pimienta   </p>
         <h4>Preparación</h4>
               <p>• Lave y  ahueque los tomates<br />
                 <br />
                 • Cocine  la pechuga en caldo de verduras y desmenuce con los dedos<br />
                 <br />
                 • Haga un  sofrito con los cebollines, la zanahoria y condimente<br />
                 <br />
                 • Incorpore  el pollo desmenuzado y la salsa blanca<br />
                 <br />
                 • Rellene  los tomates con la mezcla hasta la mitad de los tomates, entre medio deje un  huevo crudo y termine de rellenar<br />
                 <br />
                 • Cubra  con queso rallado y cocine en el horno por 20 minutos<br />
                 <br />
                 • Sirva  como entrada o si prefiere cómo plato de fondo acompañado de arroz
                 </p>
