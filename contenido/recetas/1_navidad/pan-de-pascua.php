	<h2>Pan de Pascua</h2>
            <h4>Ingredientes </h4>
            <p>&#8226; ¾  kilo de harina<br />
              &#8226; ½ kilo de margarina Light<br />
              &#8226; 1 ¼  tazas de azúcar Light<br />
              &#8226; 200grs. de pasas<br />
              &#8226; 200grs. de naranjas confitadas<br />
              &#8226; 300grs. de almendras peladas<br />
              &#8226; 300grs. de nueces picadas<br />
              &#8226; 6 huevos<br />
              &#8226; 3 cucharadas de ron<br />
              &#8226; 2 cucharadas de vinagre blanco<br />
              &#8226; 1 cucharadita de polvos de hornear<br />
              &#8226; 1 cucharadita de canela en polvo<br />
              &#8226; 1 cucharadita de clavo de olor tostado y molido<br />
              &#8226; Raspadura de nuez moscada            <br /><br />
            </p>
            <h4>Preparación
            </h4>
            <p><br />
&#8226;	Separa las claras de las yemas y junta todos los ingredientes en un bol, excepto las claras, las cuales se baten a nieve y se incorporan a la mezcla.<br />
             <br />
             &#8226; 	Llena un molde enharinado y hornea a fuego bajo durante una hora y cuarto o hasta que la superficie se dore.<br />
             <br />
             &#8226; 	Deja enfriar y decora con mazapán teñido rojo formando guindas y naranjitas.<br />
           </p>

