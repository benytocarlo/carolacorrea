	<h2>Galletas de Navidad</h4>
      <h4>Ingredientes</h3>
      <p>&#8226; 1 ½ taza de harina<br />
        &#8226; 3 cucharadas de margarina Light<br />
        &#8226; 25 gotas de sucralosa Iansa<br />
        &#8226; 2 cucharadas de miel<br />
        &#8226; ½ cucharadita de jengibre<br />
        &#8226; ½ cucharadita de clavo de olor<br />
        &#8226; ½ cucharadita de canela</p>
      </p>
      <h4>Preparación<br />
      </h4>
      <p>•	Junta en un bol la harina, la margarina, la miel, la sucralosa, el jengibre, el clavo de olor y la canela.<br />
        <br />
        •	Uslerea y corta con moldes de galletas.<br />
        <br />
        •	Hornea sobre papel mantequilla por 15 minutos.<br />
        <br />
        •	Decora con mostacillas de colores, las cuales se pegan pintando cada galleta con un pincel con clara de huevo.</p>
      </p>
</div>

