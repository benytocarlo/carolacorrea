	<h2>Pavo de Pascua</h2>
    <h4>Ingredientes</h4>
            <p>&#8226; 1 pavo  (4 a 5 kg)<br />
  &#8226; 2 cebollas<br />
  &#8226; 2 manzanas<br />
  &#8226; 1 taza de ciruelas secas<br />
  &#8226; 1 taza de damascos secos<br />
  &#8226; 2 tazas de vino blanco<br />
  &#8226; 1 taza de jugo de piña<br />
  &#8226; 2 cucharadas de miel<br />
  &#8226; 1 taza de pasas<br />
  &#8226; 2 hojas de laurel<br />
  &#8226; Tomillo y orégano fresco <br /><br />
            </p>
            <h4>Para la Salsa</u> Ingredientes</h4>
            <p>&#8226; 2 tazas de arándanos<br />
  &#8226; 4 tazas de vino tinto<br />
  &#8226; ½ taza de azúcar<br />
  &#8226; 1 cucharada de maicena</p>
            </p>
            <h4>Preparación<br />
              <br />
            </h4>
            <p>&#8226; 	En una budinera dispone una cama de cebollas.<br />
              <br />
              &#8226; Remoja las ciruelas, los damascos y las pasas en ron por 1 hora.<br />
              <br />
              &#8226; Rellena el pavo con las frutas, las manzanas partidas en cuartos y el tomillo y orégano.<br />
              <br />
              &#8226; Deja el pavo en la budinera y condimentar con sal, pimienta y unta con miel.<br />
              <br />
              &#8226; Dispone el vino y el jugo de piña en la budinera.<br />
              <br />
              &#8226; Tapa la budinera con papel aluminio y hornea a fuego lento por 1 ½ hora. Retira el papel y hornea a fuego máximo por 30 minutos más.<br />
              <br />
              &#8226; Para la salsa, deja los arándanos en una olla junto con el vino y el azúcar hasta que se reduzca a tres cuartas partes.<br />
              <br />
              &#8226; Añade al final la maicena disuelta en agua. Reserva.<br />
              <br />
              &#8226; Corta el pavo y baña con salsa de arándanos.</p>
