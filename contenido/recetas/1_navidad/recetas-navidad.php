	<h2>Cola de Mono</h2>
    <h4>Ingredientes</h4>
    <p>&#8226; ¾  botella de agua ardiente<br />
               &#8226; 1 tarro de leche condensada 0% grasa<br />
               &#8226; ¾  litro de leche descremada<br />
               &#8226; 1 cucharada sopera de café<br />
               &#8226; 8 clavos de olor<br />
               &#8226; 1 pizca de canela<br />
            &#8226; ¼   taza de agua hirviendo<br />
            </p>
             <h4>Preparación</h4>
               <p>1.-	Un bol juntar la leche, la leche condensada y el café.<br />
                 2.-	Mezclar muy bien con una paleta.<br />
                 3.-	Hervir la leche con el clavo de olor y la canela<br />
                 4.-	Incorporar la leche y luego el aguardiente de a poco para que no se corte.<br />
                 5.-	Disponer en botellas, llenar hasta tres cuartos de su capacidad y congelar.<br />
                 6.-	Antes de servir, sacar del frío y dejar reposar por 15 minutos para que se descongele.<br />
             7.-	Derretir chocolate, untar un dedo y pintar las copas por dentro, esperar que se enfríen y servir ahí la cola de mono </p>
               </p>
