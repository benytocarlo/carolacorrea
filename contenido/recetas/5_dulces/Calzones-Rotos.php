	<h2>Calzones Rotos</h2>
      <h4>Ingredientes &quot;10 Personas&quot;</h4>
      <p>
        • 3 tazas de harina<br />
        • 1 cucharadita de polvos de  hornear<br />
        • 1 cucharadita de endulzante<br />
        • 1/2 taza de azúcar flor<br />
        • 2 cucharadas de aceite de  maravilla<br />
        • 1 huevo<br />
        • 2 yemas<br />
        • 1 cucharada de ralladura de  limón</p>
      <h4>Preparación:<br />
      </h4>
      <p>• Deje la harina en un bol, haga un  hueco al centro y deje ahí los polvos de hornear, el endulzante, el aceite, los  huevos y la ralladura de limón.<br />
        <br />
        • Amase bien hasta que la masa quede  blanda <br />
        <br />
        • Estire la masa y corte en tiras,  luego las tiras las corta en rectángulos, haga un corte al centro y forme los  calzones rotos<br />
        <br />
        • Hornéelos en un horno precalentado,  por 20 minutos<br />
        <br />
        • Retire del horno y espolvoree azúcar  flor</p>
