	<h2>Terrina de Frutas y Yogurt</h2>
         <h4>Ingredientes</h4>
         <p>• 4 yogurt de frutilla  0% grasa<br />
           • 1 tarro de leche  evaporada<br />
           • 2 sobres de gelatina  sin sabor<br />
           • Frutillas – mangos<br />
           • Salsa de frambuesas         </p>
         <h4>Preparación</h4>
           <p>• Bata  la leche evaporada (helada) hasta que suba, incorporar el yogurt.<br />
             <br />
             • Disuelva  la gelatina en agua caliente e incorporela a la mezcla.<br />
             <br />
             • En  moldes individuales, forrados con papel film  deje primero una capa de frutillas y luego cubra  con la mezcla, luego una capa de mangos y  volva a  cubrir con la mezcla.<br />
             <br />
             •  Lleve  al refrigerador por 3 horas y desmolde.<br />
             <br />
            •  Sirva  con salsa de frambuesas y decore con hojas de menta.</p>
