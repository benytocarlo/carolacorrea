	<h2>Tarta Tatín</h2>
         <h4>Ingredientes</h4>
         <p>• 6 manzanas verdes<br />
           • 1 tazas de azúcar<br />
           • 1 tazas de agua</p>
         <h4>Para la Mesa</h4>
         <p>•  2 tazas de  harina<br />
           • ¾ de margarina Light<br />
           • ¾ de azúcar flor<br />
           • Canela para  espolvorear         </p>
         <h4>Preparación</h4>
           <p>• Haga la  masa, juntando los ingredientes. Si queda muy húmeda es necesario dejarla en el  refrigerador por una media hora.<br />
             <br />
             • Estire  la masa sobre un molde de 24   cm y hornee a horno medio por 25 minutos. Retire y dejar  enfriar.<br />
             <br />
             • Prepare la crema pastelera y corte los  higos en láminas<br />
             • Ponga sobre la masa la crema pastelera y  encima y ordene los higos 5.<br />
             <br />
             •  Para la crema pastelera, caliente dos tazas  de leche descremada con ½ taza de azúcar. Aparte bata tres yemas de huevo con ¼  taza de leche, una cucharada de vainilla y tres cucharadas de maicena. Una vea  que la leche hierva, incorpore la mezcla y recuelva constantemente, Una vez que  se espesa, retire del fuego.</p>
