	<h2>Muffins de Naranja y Jengibre</h2>
      <h4>Ingredientes</h4>
      <p> • 3 huevos<br />
        • 1 yogurt natural<br />
        • 3 medidas de pote de yogurt    de  harina<br />
        • ¾  taza   de azúcar light<br />
        • 2 cucharaditas de polvos de hornear<br />
        • ½  taza de aceite vegetal<br />
        • 2 cucharadas de zeste de naranja<br />
        • 1 cucharada de ralladura de jengibre</p>
      <h4>Preparación: <br />
      </h4>
      <p>•  Mezcla la harina, con los polvos de hornear y la azúcar light<br />
        <br />
        • Aparte mezcla batiendo los huevos, el yogurt  y  el aceite<br />
        <br />
        • Incorpora a la mezcla el zeste de naranja y el jengibre<br />
        <br />
        • Junta los ingredientes secos y los húmedos formando una masa<br />
        <br />
        • Llena cápsulas individuales hasta ¾ de su capacidad y lleva al horno por 20 minutos o  hasta que suban y estén cocidos</p>
    </div>

