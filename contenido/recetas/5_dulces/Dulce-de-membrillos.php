	<h2>Dulce de Membrillo</h2>
      <h4>Ingredientes</h4>
      <p> • 2 kilos de membrillos no muy maduros.<br />
        • 1 ½  kilo de azúcar granulada.<br />
        • ½  limón<br />
        • Agua hasta cubrir los membrillos.<br />
      </p>
      <h4>Preparación</h4>
      <p> •  Lave bien los membrillos.<br />
        <br />
        •  Ponga los membrillos enteros en una olla junto con el limón, cubra con abundante agua.<br />
        <br />
        • Hierva  los membrillos a fuego medio  hasta que estén blandos <br />
        <br />
        •  Una vez cocidos, escúrralos y páselos por cedazo.<br />
        <br />
        •  Pese  la pulpa y utilice la misma cantidad de azúcar.<br />
        <br />
        •  Ponga en una olla el azúcar y la pulpa , cocine al menos 30 minutos hasta lograr el punto del dulce de membrillos</p>
        <br />
      <p>Dato<br /><br />
        El punto se ve sacando una cucharada de la olla, dejándolo en un plato,
        rápidamente debe tomar consistencia y forma</p>
