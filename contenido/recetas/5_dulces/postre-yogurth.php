	<h2>Postre de Yogurt con Berries</h2>
           <h4>Ingredientes</h4>
              <p>• 2 paquetes de galletas de cereal<br />
              • 3 yogurt naturall <br />
              • 1 tarro de leche condensada 0% grasa<br />
              • 1 taza de berries<br />
              • 5 cucharadas de leche descremada<br />
            </p>
            <h4>Preparación:<br /></h4>
              <p>• Procesa las galletas<br />
                <br />
                • Mezcla las galletas con la leche hasta que tome consistencia de masa<br />
                <br />
                •  Mezcla el yogurt con la leche condensada y las frambuesas.<br />
                <br />
                • En una budinera previamente enmantequillada deja las galetas en la base con ayuda de los dedos.<br />
                <br />
                • Cubre la masa con la mezcla de yogurt y frutas<br />

                <br />
                • Lleva al horno preclaentado a una temperatura mediana, por aproximadamente por 20 minutos <br />

                <br />
                • Retirar del horno, deje enfriar y corte en cuadrados</p></div>

