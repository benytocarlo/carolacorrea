	<h2>Queque de Zanahoria</h2>
           <h4>Ingredientes</h4>
              <p>•  3 tazas de harina<br />
              •  2 cucharadas de miel <br />
              •  2 cucharaditas de polvos de hornear<br />
              •  2 huevos<br />
              •  1 taza de aceite de maravilla<br />
              •  3 zanahorias ralladas<br />
              •  2 tazas de puré de manzanas<br />
            •  1 cucharadita de canela en polvo</p>
          <h4>Preparación:</h4>
              <p>•  En un bol cernir la harina junto con la canela y polvos de hornear<br />
                <br />
                • En otro bol agregue cuidadosamente los huevos, luego incorpore el
                aceite, la zanahoria rallada, las nueces picadas, las pasas, el puré de
                manzana y la miel. Mezclar bien hasta que la masa quede húmeda.<br />
                <br />
                •  Vierta la mezcla en un molde  enharinado. <br />
                <br />
                •  Cocinar en un horno precalentado a temperatura media/baja, (170°C) durante 1 hora. Retirar y dejar enfriar.<br />
                <br />
                • Retirar y dejar enfriar</p></div>

