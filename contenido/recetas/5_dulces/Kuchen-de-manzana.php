	<h2>Kuchen de Manzana</h2>
    <h4>Ingredientes</h4>
    <p>• Masa:&nbsp;<br />
        • ¼&nbsp; taza de azúcar Light<br />
        • ¾&nbsp; taza de aceite<br />
        • 2 taza de harina<br />
        <br />
        <h4>• Relleno:</h4>
        <p> •  3 manzanas verdes<br />
        • 5 ciruelas<br />
        • 1 cucharada de jugo de limón<br />
        • 4 cucharadas de azúcar dorada<br />
        • Canela</p>
      <h4>Preparación:</h4>
      <p>&#8226; Para&nbsp; hacer la masa reúna los ingredientes en un bol <br />
        <br />
        • Vacíe el contenido en un molde y hornee a fuego medio por 15 minutos<br />
        <br />
        • Para el relleno, picar las manzanas y las ciruelas en cuartos y luego laminarlas<br />
        <br />
        • Saltearlas con azúcar y canela.<br />
        <br />
        • Vierta &nbsp;la mezcla sobre la masa<br />
        <br />
        • Espere a que se enfríe y luego sirva.</p>
