	<h2>Naranjas Rellenas</h2>
      <h4>Ingredientes</h4>
      <p> • 4  naranjas<br />
        • 1 sobre de gelatina de frutilla<br />
        • ½  taza de frutillas</p>
      <h4>Preparación: <br />
      </h4>
      <p>• Partir las  naranjas por la mitad y sacar toda la carne, dejando solamente la cáscara<br />
        <br />
        • Preparar la gelatina<br />
        <br />
        • Incorporar las frutillas picadas a la  gelatina<br />
        <br />
        • Vaciar la gelatina en las mitades de  naranja, llevar al refrigerador y dejar enfriar.<br />
        <br />
        • Una vez hecha la jales, cortar cada  mitad de naranja en dos gajos con un buen cuchillo</p>
    </div>

