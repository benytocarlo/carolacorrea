	<h2>Galletas con Chips de Chocolate</h2>
      <h4>Ingredientes &quot;24 Unidades&quot;</h4>
      <p>
        • 2 ½ taza de harina<br />
        • ¾ taza de margarina Light<br />
        • 1 taza de azúcar flor<br />
        • ½ taza de chips de chocolate<br />
        • ¼ taza de coco rallado</p>
      <h4>Preparación: <br />
      </h4>
      <p>• Mezcle la harina con la azúcar y agregue  la margarina<br />
        <br />
        • Con  la punta de los dedos mezcle todos los ingredientes, sin amasar, forme una masa  suave<br />
        <br />
        • Una vez lista, agregue los chips de chocolate y el coco, forme 2 cilindros y  refrigere por 1 hora <br />
        <br />
        • Trascurrido  el tiempo, con un cuchillo corte los cilindros formando de esta forma las  galletas, extienda sobre una lata con papel mantequilla y hornee a fuego medio  por 12 minutos o hasta que las orillas de las galletas comiencen a dorar. <br />
        <br />
        • Retire  y enfríe sobre una rejilla.</p>
    </div>

