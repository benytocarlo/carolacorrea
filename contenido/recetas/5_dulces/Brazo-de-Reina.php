	<h2>Brazo de Reina</h2>
      <h4>Ingredientes &quot;10 Personas&quot;</h4>
      <h4> Masa:  </h4>
      <p>
        • ½ taza de azúcar<br />
        • 5 huevos separados<br />
        • 4 cucharadas de harina<br />
        • 4 cucharadas de maicena<br />
        • 1 cucharadita de polvos de hornear<br />
        • Manjar o mermelada para rellenar<br />
        • Azúcar flor para espolvorear</p>
      </p>
      <h4>Preparación:<br />
      </h4>
      <p>• Bata las  claras y agregar un almíbar de pelo con el azúcar.<br />
        <br />
        • Vierta  las yemas de a una al merengue e incorporar bien.<br />
        <br />
        • Cierna  juntos la harina, la maicena y los polvos e integre al merengue con movimientos  envolventes.<br />
        <br />
        • Vierta  la masa a un papel rectangular, enmantequillado y enharinado. <br />
        <br />
        • Cocine  por 20 minutos.<br />
        <br />
        •  Retire  del horno y desmolde con un paño húmedo dándole <br />
        la forma redondeada.<br />
        <br />
        • Una  vez frío desenrolle y rellene con manjar, enrolle y sirva espolvoreado con  azúcar flor.</p>
    </div>

