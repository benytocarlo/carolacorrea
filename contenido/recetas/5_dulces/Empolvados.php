	<h2>Empolvados</h2>
      <h4>Ingredientes &quot;24 Unidades&quot;</h4>
      <p>  <br />
        • 6 huevos<br />
        • 6 cucharadas de harina cernida<br />
        • 3 cucharadas de azúcar light de Iansa<br />
        • 1 cucharadita de bicarbonato<br />
        • Manjar<br />
        • Azúcar flor para espolvorear</p>
      <h4>Preparación:<br />
      </h4>
      <p>• Mezcle la harina con el bicarbonato, bata  a nieve las claras y cuándo estén firmes,   agregue el azúcar de a poco, sin dejar de batir.<br />
        <br />
        • Agregue a las claras ya batidas con el azúcar, las yemas previamente batidas,  en forma envolvente con la ayuda de una cuchara de palo, finalmente agregue la  harina de a poco de la misma forma, envolviendo el batido, procurando que todo  quede bien homogéneo<br />
        <br />
        • Disponga  sobre una lata con papel mantequilla con la ayuda de una cuchara formando círculos.  Hornee a 180º (horno precalentado) por 7 a 10 minutos<br />
        <br />
        • Despegue  una vez fríos del papel,  rellene con  manjar y espolvoree con azúcar flor con la ayuda de un colador.</p>
