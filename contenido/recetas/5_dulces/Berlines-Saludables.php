	<h2>Berlines Saludables</h2>
      <h4>Ingredientes &quot;10 Personas&quot;</h4>
      <h4> Masa:</h4>
        <p>• 3 tazas de harina<br />
        • 2 cucharadas de levadura<br />
        • 2 huevos<br />
        • 1 yema<br />
        • ¼ taza de aceite vegetal<br />
        • 1 cucharada de endulzante<br />
        • 1 cucharada de vainilla<br />
        • ¾ taza de leche</p>
<h4>Para la  crema pastelera:</h4>
     <p>  • ½ litro de leche descremada<br />
      • 2 cucharadas de vainilla<br />
      • 2 cucharadas soperas de maicena<br />
      • 2 yemas de huevo<br />
      • 1 cucharadita de endulzante      
      </p>
      <h4>Preparación:</h4>
      <p>• Para lo berlines, deje en un bol la harina, haga un hueco al  centro y deje el endulzante, la levadura previamente leudada, los huevos, el  aceite, la leche, la vainilla y mezcle<br />
        <br />
        • Amase bastante  hasta que quede una masa suave. Dejar leudar 15 minutos<br />
        <br />
        • Forme bolitas y  hornee a temperatura media alta por 15 minutos.<br />
        <br />
        •  Para la crema pastelera,  hierva la leche en una olla. Retire del fuego y agregue las yemas de a una. Vuelva  al fuego sin que hierva y agregue la vainilla, el endulzante y la maicena  previamente disuelta en agua fría.<br />
        <br />
        •  Rellene los  berlines y sirva</p>
