	<h2>Mousse de Chocolate</h2>
      <h4>Ingredientes</h4>
      <p> • ½ litro de crema para batir, Light<br />
        • 4 claras batidas<br />
        • 1 ½  taza de chocolate en polvo  amargo<br />
        • 1 ½ taza de chocolate en polvo dulce<br />
        • Hojas de menta fresca<br />
        • ¼  kilo de cobertura de chocolate  semi amarga para los casquitos</p>
      <h4>Preparación: <br />
      </h4>
      <p>• Bata  la crema, helada, a punto de letra<br />
        <br />
        • Incorpore las claras batidas a nieve<br />
        <br />
        • Incorpore con un batidor manual los chocolates con movimientos envolventes<br />
        <br />
        • Agregue la menta picada fina<br />
        <br />
        • Haga casquitos de chocolate con la cobertura.<br />
        <br />
        • Rellene los casquitos con el mousse y decore con hojas de  menta fresca.<br />
        <br />
        • Prepare una salsa de frutillas, endulzada con miel, para acompañar.</p>
