	<h2>Granola</h2>
      <h4>Ingredientes</h4>
      <p>• 2 tazas de germen de trigo<br />
        • ½ taza de almendra<br />
        • ½ taza de nueces picadas<br />
        • ¾ taza de agua<br />
        • 1 taza de miel<br />
        • ¼ taza de aceite de oliva<br />
        • Ralladura de limón</p>
      <h4>Preparación: <br />
      </h4>
      <p>• Mezclar los ingredientes secos en un bol<br />
        <br />
        • Incorporar los ingredientes húmedos y  mezclar.<br />
        <br />
        • Dejar en una lata de horno y cocinar por  1 hora a temperatura media baja revolviendo de vez en cuando<br />
        <br />
        • Dejar enfriar y guardar en frascos  herméticos<br />
        <br />
        • Servir con leche descremada, leche  cultivada o yogurt natural.</p>
    </div>

