	<h2>Algodón de manzana</h2>
      <h4>Ingredientes</h4>
        <p>• 1 manzana rallada <br />
        • 3 claras de huevo<br />
        • 6 cucharadas de azúcar  granulada<br />
        • &frac14; taza de jugo de limón<br />
        • 1 cucharada de nueces picadas<br />
        • 1 cucharadita de vainilla<br />
        • 1 pizca de canela en polvo<br />  
      </p>
      <h4>Preparación:</h4>
      <p>• Rallar la manzana<br />
        <br />
        • Batir las claras a nieve<br />
        <br />
        • Mezclar la manzana con la vainilla y el jugo de limón<br />
        <br />
        • Juntar el merengue y la manzana<br />
        <br />
        •  Hacer montoncitos y llevarlos al horno para que se dore el merengue<br />
        •  Servir con un poco de caramelo<br /></p>
    </div>

