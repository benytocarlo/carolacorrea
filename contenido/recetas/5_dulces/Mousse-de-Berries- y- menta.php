	<h2>Mousse de Berries y Menta</h2>
      <h4>Ingredientes</h4>
      <p> • 4 potes de yogurt natural<br />
        • Taza de pulpa de berries<br />
        • 12 hojas de menta fresca<br />
        • 3 hojas de colapez<br />
        • 4 cucharadas de azúcar Light<br />
      • Salsa de frambuesas</p>
      <h4>Preparación: <br />
      </h4>
      <p>• En un bol junte el yogurt con la pulpa de berries, las hojas de menta picadas y la azúcar. Reserve un poco de berries enteros<br />
        <br />
        • En un bol pequeño, disuelva la gelatina en agua caliente hasta que quede absolutamente derretida <br />
        <br />
        • Incorpore la gelatina a la mezcla<br />
        <br />
        • Deje  el mousse en potes individuales y lleve al refrigerador<br />
        <br />
        • Sierva con salsa de frambuesas<br />
      </p>
    </div>

