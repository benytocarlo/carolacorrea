	<h2>Bavarrois Helado de Chirimoya Alegre</h2>
            <h4>Ingredientes</h4>
               <p>• 2 chirimoyas<br />
               • 1 tarro de leche evaporada<br />
               • 1 tarro de leche condensada Light<br />
               • ¾ taza de jugo de naranjas<br />
               • 4 mandarinas<br />
            • 3 tiras de colapes</p>
               </p>
       <h4>Preparación:<br />
       </h4>
               <p>• Bate la leche evaporada previamente fría hasta que doble su tamaño<br />
                 <br />
                 • Incorpora la leche condensada, el jugo de naranjas y la chirimoya, sin pepas<br />
                 <br />
                 • Agrega el colapes diluido<br />
                 <br />
                 • En un molde, deja una base de gajos de mandarina y sobre éstos deja el postre<br />
                 <br />
                 • Lleva al congelador por tres horas<br />
                 <br />
                 • Sirve con salsa de mandarina (2 tazas de jugo de mandarina, azúcar y maicena)</p></div>

