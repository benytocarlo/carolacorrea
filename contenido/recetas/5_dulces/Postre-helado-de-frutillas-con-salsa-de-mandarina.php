	<h2>Postre de Helado de Frutillas con Salsa de Mandarina</h2>
         <h4>Ingredientes</h4>
         <p>           •  8  manzanas verdes<br />
           • 2  tarros de leche condensada 0% grasa<br />
           • ½  taza de nueces<br />
           • 1  cucharadita de canela<br />
           • 1  taza de chancaca Deliciosa<br />
           • 4  huevos<br />
           • Merengue</p>
         <h4>Preparación</h4>
            <p>• Ralle  las manzanas y mézclelas con la leche condensada,  las yemas de huevo,  la canela y las nueces<br />
              <br />
              • Deje  todo en una budinera y cocine a horno medio por 20 minutos<br />
              <br />
              • Aparte  haga un merengue con las claras <br />
              <br />
              • Retire  del horno, cubra con el merengue y hornee por tres minutos con grill para que  se dore la superficie<br />
              <br />
            • Para  servir, bañe el plato con chancaca previamente disuelta en un poco de agua y  sirva la leche asada encima, le dará un sabor muy rico, original y casero</p>
