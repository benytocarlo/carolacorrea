	<h2>Leche Asada</h2>
      <h4>Ingredientes</h4>
      <p>• 2 yemas de huevo<br />
        • 3  huevos enteros<br />
        • ½  taza de azúcar<br />
        • 3  tazas de leche descremada<br />
        • 1 ½  cucharadita de extracto de vainilla<br />
        • 1  flor de lavanda<br />
        • Caramelo  líquido</p>
      <h4>Preparación: <br />
      </h4>
      <p>• En un bol bata las yemas y los huevos<br />
        <br />
        • Caliente  la leche en una olla con la flor de lavanda para que se perfume<br />
        <br />
        • Añada  el azúcar y vierta de a poco la leche caliente, revolviendo constantemente<br />
        <br />
        • Agregue  la vainilla y vierta en una fuente de horno<br />
        <br />
        • Espolvoree  con nuez moscada y cocine a un horno medio por 20 minutos<br />
        <br />
        • Retire  del horno y cubra con una capa delgada de caramelo. Deje enfriar y sirva</p>
    </div>

