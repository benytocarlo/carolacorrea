	<h2>Panqueques de Miel</h2>
         <h4>Ingredientes</h4>
         <p> • 2 claras<br />
           • 1 cucharada  sopera  de harina<br />
           • ¾ taza  leche descremada<br />
           • Edulcorante a  gusto<br />
           • Miel de abejas</p>
         <h4>Preparación para la Pannacotta:
           <br /></h4>
            <p>• Batir las claras a nieve.<br />
              • Incorporar el harina, la leche y el  endulzante.<br />
              • Hacer los panqueques y reservar.<br />
            • Servir con miel</p>
