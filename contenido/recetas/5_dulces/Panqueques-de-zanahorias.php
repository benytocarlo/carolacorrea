	<h2>Panqueques de Zanahorias</h2>
    <h4>Ingredientes: </h4> 
    <p>• 1 huevo<br />
    • 1 taza de maicena<br />
    • 2&nbsp; zanahorias cocidas<br />
    • 1 ½ taza de leche<br />
    • 1 cucharada de azúcar Ligh 
    </p>
    
    <p><strong>Para el relleno:</strong><br />
      • 1 bolsa de frutos del bosque congelados<br />
      • 1 tapita de triple sec<br />
      • 1 cucharadita de azúcar Light<br />
      • Hojitas de menta fresca<br />
    • Yogurt natural</p>
	<h4>Preparación</h4>
    
    <p>&#8226; Pele y cueza&nbsp; las zanahorias, luego hágalas puré<br />
              Para los panqueques, deje todos los ingredientes en la licuadora Caliente un sartén antiadherente y haga los panqueques que deben quedar bien delgados<br />
              <br />
              &#8226; En un sartén, saltee los berries junto al azúcar y la menta. Incorpore el triple sec<br />
              <br />
              &#8226; Prepare una salsa con el yogurt y la menta picada<br />
            Rellene los panqueques con los berries y bañe con la salsa de yogurt</p>
