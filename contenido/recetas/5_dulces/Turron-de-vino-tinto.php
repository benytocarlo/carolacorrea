	<h2>Turrón de Vino Tinto</h2>
         <h4>Ingredientes</h4>
         <p>•  5 claras de huevo<br />
           • 15 cucharadas de azúcar<br />
           • 1 ½ taza de vino tinto<br />
           • 1 ramita de canela<br />
           • Nueces picadas<br />
           • Salsa inglesa         </p>
         <h4>Preparación</h4>
            <p>• Bata  las claras a nieve<br />
            <br />
            • Haga  un almíbar de pelo, con el vino, el azúcar y el palo de canela<br />
            <br />
            • Incorpore  el almíbar en forma de hilo de a poco a las caras batidas<br />
            <br />
            • Bata  hasta que se enfríe<br />
            <br />
            •  Haga un  polvo de nueces <br />
            <br />
            •  Preparare  una salsa inglesa<br />
            <br />
          •  Sirva  el turrón, con nueces espolvoreadas y decore con salsa de vino tinto reducido,  para lo cual prepare un caramelo de vino y azúcar. </p>
