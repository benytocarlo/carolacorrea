	<h2>Waffles Saludables</h2>
         <h4>Ingredientes</h4>
         <p> • 2  tazas de harina<br />
 • 3 cucharaditas de polvo de hornear<br />
 • 2 cucharadas de azúcar rubia<br />
 • 2 huevos <br />
 • 1 ½ taza de leche semi descremada<br />
 • 6 cucharadas de aceite </p>
<h4>Preparación</h4>
            <p>• Coloque en  una licuadora la harina, el polvo de hornear, el azúcar, los huevos, la leche y  el aceite<br />
            <br />
            • Licúe todo<br />
            <br />
            • Utilice una wafflera de teflón y  vierta la masa en el centro de la plancha, cuando ésta esté bien caliente  déjela correr hacia los costados. No ponga demasiada masa. Deje cocinar<br />
            <br />
          • Sirva con miel o mermelada </p>
