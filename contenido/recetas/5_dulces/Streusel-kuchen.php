	<h2>Streusel Kuchen</h2>
         <h4>Ingredientes</h4>
         <p>           • 2 tazas de harina<br />
           • 100 grs de margarina Light<br />
           • ¾ taza de azúcar<br />
           • ¼ taza de leche semi descremada<br />
           • 2 huevos<br />
           • 2 cucharaditas de polvos de hornear<br />
           • 3 tazas de frambuesas<br />
           • 2 tazas de crema pastelera</p>
           <h4>Streusel:</h4>
           <p>• ¾ taza de  margarina light<br />
             • 3 cucharadas de harina<br />
             • 2 cucharadas de azúcar<br />
             • 1 cucharadita de canela
           </p>
           <h4>Preparación</h4>
           <p>• Para  la masa, bata la margarina con el azúcar. Integre los huevos, la leche y forme  una pasta<br />
             <br />
             • Incorpore  la harina junto con el polvo de hornear y forme la masa<br />
             <br />
             • Cubra  un molde y reserve<br />
             <br />
             • Deje  encima la crema pastelera, encima las frambuesas bien escurridas para que no  voten mucho líquido<br />
             <br />
             • Para  el streusel, junte en un bol todos los ingredientes y juntelos con los dedos,  formando así pelotitas<br />
             <br />
            • Cubra  el kuchen con el streusel y cocine a un horno precalentado por 25 minutos o  hasta que el streusel tome color</p>
