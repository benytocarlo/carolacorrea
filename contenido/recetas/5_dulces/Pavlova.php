	<h2>Pavlova de Yogurt y Frutillas</h2>
         <h4>Ingredientes</h4>
         <p>• 4 claras de huevo<br />
           • ½ taza  de azúcar granulada<br />
           • ½ taza de azúcar flor<br />
           • 1 cucharadita de extracto de  vainilla<br />
           • 2 yogurt natural 0% grasa<br />
           • 1 taza de frutillas<br />
           • 1 cucharadita de sucralosa  Iansa<br />
           • 6 hojas de menta<br />
           • 2 láminas de colapez</p>
<h4>Preparación:<br /></h4>
   <p>• Precalienta el horno a temperatura  media• baja. Forra una lata de horno con papel mantequilla y dibuja un círculo  de 8cm. de diámetro.<br />
    <br />
    • En un bol bate las claras hasta que  estén a punto de nieve. Agrega azúcar granulada en tres partes sin dejar de  batir junto con el azúcar flor en 3 partes y bate hasta que la mezcla esté  firme y brillante.<br />
              <br />
              • Esparce el batido en el círculo dibujado  en el papel mantequilla y ahueca el centro dejando los lados más altos.<br />
              <br />
              • Cocina en el horno por 1 hora a  fuego bajo, saca del horno y deja enfriar.<br />
              <br />
              • Junta el yogurt, la vainilla, las  hojas de menta, las frutillas y la sucralosa   y procesa con la   Mini Blender.<br />
              <br />
            • Hidrata con agua fría las hojas de  colapez, escúrrelas, derretirlas y agregarlas a la mezcla de frutillas.</p>
            <p>• Rellenar el merengue con la mezcla de frutillas y decora con hojas de menta y  frutillas</p>
