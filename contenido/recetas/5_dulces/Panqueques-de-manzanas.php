	<h2>Panqueques de Manzanas</h2>
            <h4>Ingredientes</h4>
              <p>• 3 huevos<br />
              • 1 cucharadita de polvos de hornear<br />
              • 2 tazas de harina<br />
              • 3 manzanas<br />
              • Ralladura de medio limón<br />
            • ¼ taza de aceite</p>
              </p>
          <h4>Preparación:</h4>
              <p>• Lave, pele y ralle las manzanas<br />
                <br />
                • En un bol,&nbsp; junte la harina y el polvo de hornear<br />
                <br />
                • Añada los huevos, las manzanas ralladas, la ralladura de limón y el aceite<br />
                <br />
                • Forme una masa y haga los panqueques en un sartén caliente<br />
                <br />
              • Sirva con miel o mermelada</p></div>

