	<h2>Alfajores con manjar</h2>
      <h4>Ingredientes &quot;15 Unidades&quot;</h4>
      <h4> Masa:</h4>
        <p>• 1 taza de aceite <br />
        • 1 taza de azúcar <br />
        • 4 yemas<br />
        • 1 cucharada de esencia de vainilla<br />
        • 1 cucharada de cognac <br />
        • 2 cucharadas de jugo de limón<br />
        • 2 tazas de maicena<br />
      • 2 tazas de harina<br />
      • &frac12; kilo de manjar<br />
      • Almendras picadas o coco rallado      
      </p>
      <h4>Preparación:</h4>
      <p>• Bata en un bol el aceite junto con el azúcar. Incorpore las yemas de a una, la esencia de vainilla, el cognac y el jugo de limón.<br />
        <br />
        • Tamice los ingredientes secos e incorpórelos, formando una masa lisa.<br />
        <br />
        • Envuelva en papel antiadherente y llevar a la heladera durante ½ hora.<br />
        <br />
        •  Estire la masa sobre una mesa espolvoreada con harina y corte las tapas con un cortante redondo de 2cm.<br />
        <br />
        •  Coloque las tapas sobre una placa pintada con aceite  y enharinada<br />
        •   Cocine durante 8 minutos. Deje enfriar.<br />
        •  Rellene las tapas con el manjar y pase los bordes por almendras picadas o coco rallado<br />
        •  Nota: pincharlos en brochetas y presentar pinchados en naranjas.<br /></p>
    </div>

