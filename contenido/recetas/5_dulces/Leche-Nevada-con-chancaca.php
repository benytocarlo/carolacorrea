	<h2>Leche Nevada con Chancaca</h2>
      <h4>Ingredientes</h4>
      <p>• 1  tarro de leche condensada 0% grasa<br />
        • 3  tarros de agua<br />
        • 3  huevos<br />
        • 6  cucharadas de azúcar <br />
        • 1  cucharada de maicena<br />
        • 125 gramos de chancaca</p>
      <h4>Preparación: <br />
      </h4>
      <p>• Mezcle  la leche condensada con el agua &ldquo;del mismo tarro&rdquo;, y hierva<br />
        <br />
        • Bat las claras a nieve, añada el azúcar  de a poco.<br />
        <br />
        • Ponga por cucharadas el merengue sobre  la leche caliente (sin hervir), dándolos vuelta. Escurra y déjelos en un  colador.<br />
        <br />
        • Diluya la maicena en agua fría, añada  las yemas y mezcle bien, incorpore a la leche batiendo. Deje a fuego bajo para  que se cocine la maicena y espese la mezcla<br />
        <br />
        • Derrita la chancaca con ½ taza de agua<br />
        <br />
        • Vierta le leche en copas o en una  fuente, distribuya los copos en la superficie y bañe con  caramelo de chancaca</p>
