	<h2>Muffins  de Arándanos</h2>
      <h4>Ingredientes </h4>
      <p>
        • 2 tazas de harina<br />
        • 1 ½ cucharada de azúcar light<br />
        • Ralladura de una naranja<br />
        • ½ taza de aceite vegetal<br />
        • 3 huevos <br />
        • 1 cucharadita de polvos de hornear<br />
        • 2 tazas de arándanos
      </p>
      <h4>Preparación:</h4>
      <p> • Batir el aceite con el azúcar<br />
        <br />
        • Agregar los huevos de a uno sin dejar de batir.<br />
        <br />
        • Incorporar la harina cernida junto con los polvos de hornear.<br />
        <br />
        • Incorporar  la ralladura  y los arándanos<br />
        <br />
        • Aceitar un molde y hornear a 160º por 35 minutos<br />
      </p>
    </div>

