	<h2>Queque de Zapallo</h2>
         <h4>Ingredientes</h4>
         <p>           • 2 tazas de harina<br />
           • 1 taza de harina integral<br />
           • 3 cucharadas de miel <br />
           • ½ taza de azúcar rubia<br />
           • ¼ taza de nueces<br />
           • 2 cucharaditas de polvos de hornear<br />
           • 2 huevos <br />
           • 1/2    taza de aceite de oliva<br />
           • 2 tazas de zapallo cocido</p>
<h4>Preparación</h4>
           <p>• En un bol cierna las harinas junto con  el polvo de hornear y el azúcar.<br />
             <br />
             • En otro bol una ligeramente los  huevos, integre el aceite, las nueces  y  agregue el zapallo cocido y la miel. Mezcle bien hasta que quede húmedo.<br />
             <br />
             • Vierta la mezcla en un molde  enharinado. <br />
             <br />
            • Cocine en un horno precalentado a temperatura  media durante 1 hora. 5.<br />
            <br />
            • Retire y dejar enfriar</p>
