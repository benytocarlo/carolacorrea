	<h2>Galletones de Avena</h2>
      <h4>Ingredientes</h4>
      <p>• Tiempo: 50 minutos<br />
        • N° Personas: 12 unidades<br />
        • 12 cucharadas soperas rasas de harina común <br />
        • 8 cucharadas soperas de avena <br />
        • 4 cucharadas soperas de azúcar rubia<br />
        • 1 huevo <br />
        • ½ taza de aceite vegetal<br />
        • ½ taza de chips de chocolate</p>
      </p>
      <h4>Preparación<br />
      </h4>
      <p>• Mezcla los ingredientes secos en un bol.<br />
        <br />
        • Agrega el huevo, el aceite, los chips de chocolate y hasta que se forme una masa húmeda.<br />
        <br />
        • Toma una cucharada de masa y déjala en la lata del&nbsp; horno aplastada con los dedos.<br />
        <br />
        • 	Hornea a fuego mediano por 15 minutos o hasta que se doren un poco.</p>
    </div>

