	<h2>Queque de Limón y Miel</h2>
         <h4>Ingredientes</h4>
         <p>           •  2 tazas de harina<br />
           • 3 cucharadas de azúcar morena<br />
           • Ralladura de dos limones<br />
           • ½ taza de aceite vegetal<br />
           • 3 huevos <br />
           • 1 cucharadita de polvos de hornear<br />
           • 1 taza de miel<br />
         </p>
         <h4>Preparación</h4>
           <p>• Bata el aceite con el azúcar<br />
             <br />
             • Agregue los huevos de a uno sin dejar de batir.<br />
             <br />
             • Incorpore la harina cernida junto con los polvos de hornear.<br />
             <br />
             • Incorpore la miel y la ralladura  de limón<br />
             <br />
             • Aceite un molde y hornee a fuego medio por 35 minutos<br />
           </p>
</div>

