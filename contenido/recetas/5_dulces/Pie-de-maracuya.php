	<h2>Pie de Maracuyá</h2>
         <h4>Ingredientes</h4>
         <p>           •  4 cucharadas de margarina &ldquo;light&rdquo;  (125 grs)<br />
           • 2 yemas<br />
           • 2 tazas de harina<br />
           • 1/4 taza de leche descremada</p>
<h4>Rellenos</h4>
            <p>• 2 tarros de leche condensada 0%  grasa<br />
              • 1 taza de maracuyá<br />
            • Merengue</p>
<h4>Preparación</h4>
            <p>• Para la masa, mezcla todos los ingredientes  hasta formar una masa. <br />
              <br />
              • Aceita un molde y cubre con los  dedos. La masa debe quedar delgada. Pínchala con un tenedor<br />
              <br />
              • Hornea por 15 minutos  a fuego medio<br />
              <br />
              • Aparte, mezcla la leche condensada con  la pulpa de maracuyá (con semillas)<br />
              <br />
              • Rellena la masa ya horneada con la  mezcla, cubre con el merengue y hornea por 4 minutos más<br />
              <br />
            • Decora con gajos de limón de pica y  hojas de menta</p>
