	<h2>Empanaditas de Pera</h2>
      <h4>Para el Relleno </h4>
      <p>Ingredientes  &quot;12 unidades&quot;<br />
        • Para la masa: <br />
        • 3 tazas de harina<br />
        • 5 yemas de huevo <br />
        • 6 cucharadas de aceite vegetal<br />
        • Leche descremada<br />
        • Para el relleno: <br />
        • 8 peras maduras<br />
        • 6 cucharadas de azúcar Light<br />
        • 3 clavos de olor<br />
        • Palitos de canela</p>
      <h4>Preparación<br />
      </h4>
      <p> • Colocar la harina en una superficie lisa y hacer un hueco al centro.<br />
        <br />
        • Agregar las yemas, aceite<br />
        <br />
        • Añadir suficiente leche para formar una masa blanda, sobar hasta que
        esté elástica, uslerear delgada y cortar en círculos.<br />
        <br />
        • Rellenar con el dulce de pera, sellar los bordes y  cocinar en un horno a temperatura media 15 a 20 minutos.<br />
        <br />
        • Para el dulce de peras, pelar y dejar en una olla las peras el agua,
        la azúcar, los clavos y la canela. Cocinar a fuego medio hasta que se
        forme un puré.<br />
      </p>
