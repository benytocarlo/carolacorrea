	<h2>Galletas de Frambuesa</h2>
      <h4>Ingredientes &quot;24 Unidades&quot;</h4>
      <p>  <br />
        • 1 pan de  chancaca  <br />
        • ½ kilo de miel<br />
        • 1 taza de nueces<br />
        • 1 cucharada de  clavo de olor<br />
        • 1 cucharada de  cáscara de naranjas rallada<br />
        • 1 cucharada de  chocolate en polvo dulce<br />
        • 1 cucharada de  chocolate en polvo amargo<br />
        • 1 pan de  margarina Light  <br />
        • 2 cucharaditas  de bicarbonato<br />
        • 1 kilo de  harina sin polvos de hornear</p>
      <p><u>Cobertura de chocolate </u></p>
      <p>•2 tazas de  azúcar  Iansa<br />
        •½ taza de  chocolate en polvo dulce<br />
        •½ taza de  chocolate en polvo amargo</p>
      <h4>Preparación: <br />
      </h4>
      <p>• Derrite  la chancaca en 1 ½ taza de agua a fuego lento. Agrega la miel, las nueces  picadas, el clavo de olor molido (previamente tostado en una paila) y la  ralladura de naranjas. Revuelve, agrega el chocolate en polvo dulce y amargo,  vuelve a revolver y agrega la margarina. <br />
        <br />
        • Deja  hervir en la olla durante 15 minutos revolviendo de vez en cuando. <br />
        <br />
        • Deja entibiar por 4 horas a  temperatura ambiente.<br />
        <br />
        • Una vez pasado el tiempo, disuelve el bicarbonato en ½ taza chica de agua y agrégalo  a la mezcla junto a la harina cernida. <br />
        <br />
        • Deja reposar hasta el otro día sin refrigerar, solamente tapada con un paño.<br />
        <br />
        •  Al día siguiente, usleree  la masa y  disponla en una lata enharinada.<br />
        <br />
        • Cocina en un horno precalentado por 10 minutos a fuego medio•bajo y luego por 15  minutos a fuego medio. Retira.<br />
        <br />
        • Para  la cobertura, dispone la azúcar en una olla, cúbrela con agua y una vez que el  almíbar tome punto (10 minutos aproximadamente) agrega el chocolate dulce y  amargo y retira del fuego. <br />
        <br />
        • Todavía  caliente la lata de galletas, báñala con la cobertura, deja enfriar y corta en  rombos pequeños.<br />
        <br />
        • Mantén  las galletas en frascos herméticos hasta la noche de navidad. </p>
      <h4>Datos:</h4>
      <p> • La cobertura se seca rápido. Si haces una lata de galletas, hace la  mitad de la receta de la cobertura, porque cuando se seca no se puede volver a  usar.<br />
        <br />
        • La masa cuando está lista pareciera que está cruda por lo blanda, pero cuando  la sacas del horno se empieza a endurecer. </p>
      </p>
