	<h2>Timbal de Manzana</h2>
    
    <h4>Ingredientes "8 personas"</h4>
	<p>
    • 6 manzanas verdes de tamaño medio<br />
    • 1/4 taza de pasas<br />
    • 1/4 taza de nueces picadas<br />
    • 1/4 taza de almendras picadas<br />
    • 2 tapitas de licor o esencia de amaretto<br />
    • 1/4 taza de azúcar rubia<br />
    • 1 pan de chancaca<br />
    • 1 paño de canela<br />
    </p>
    
    <h4>Preparación: </h4>
    <p>• Ralle las manzanas y déjelas dentro de una olla a fuego medio. Agregue las pasas, las nueces, las almendras, el amaretto, el azúcar y cocine por 20 minutos a fuego bajo, hasta que las manzanas tomen un color cristalino.<br />
• Retire del fuego y deje enfriar a temperatura ambiente.<br /> 
• Para la salsa, disuelva un pan de chancaca pequeño en una taza de agua y cocine con un palo de canela. Deje reducir a fuego medio, hasta lograr consistencia.<br />
• Para servir, monte el timbal sobre la salsa de chancaca y decore con un palo de canela, menta fresca. Si quiere, lo puede servir con una bola de helado de vainilla.<br />
    </p>
