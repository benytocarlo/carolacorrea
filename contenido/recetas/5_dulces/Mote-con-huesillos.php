	<h2>Mote con Huesillos</h2>
      <h4>Ingredientes</h4>
      <p> • 1 taza de mote en granos<br />
        • 8 huesillos <br />
        • 3/4 taza de azúcar granulada <br />
        • 2 ramitas de canela<br />
        • Cáscara de naranja ( media naranja)<br />
        • 2lt de agua <br />
      </p>
      <h4>Preparación</h4>
      <p> • Remoje los huesillos la noche anterior anterior en un litro de agua, agregue azúcar, canela y cáscaras de naranja<br />
        <br />
        • Hierva los huesillos en la misma agua, durante 30 min<br />
        <br />
        • Retire la cáscaras de naranja<br />
        <br />
        • Deje enfriar <br />
        <br />
        • Hierva el mote en 1lt. de agua durante 30 min, cuela y deje enfriar y srva el mote con huesillos en potes</p>
    </div>

