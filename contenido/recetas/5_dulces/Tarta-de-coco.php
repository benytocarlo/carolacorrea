	<h2>Tarta de Coco</h2>
         <h4>Ingredientes</h4>
         <h4>Para la Mesa</h4>
         <p>•  2  tazas de harina<br />
           • 1  cucharada de azúcar light<br />
           • ½  taza de aceite vegetal<br />
           • 1  huevo</p>
         <h4>Relleno</h4>
         <p>• 1  tarro de leche condensada 0% grasa<br />
           • ½  tarro de leche descremada<br />
           • 3  huevos<br />
           • 1  taza de coco rallado<br />
           • 1  ½ taza de crema pastelera           </p>
         <h4>Preparación</h4>
           <p>• Mezcle la harina con el azúcar  light y el  aceite <br />
             <br />
             • Incorpore el huevo y una formando una masa. <br />
             <br />
             • Estire la masa y forre un molde para tarta (la masa es quebradiza)<br />
             <br />
             • Pre cocine al horno precalentado a temperatura mediana por 10 minutos<br />
             <br />
             • Junte la leche condensada con la leche, los huevos y el coco y reserve.<br />
             <br />
             • Cubra la tarta con la crema pastelera y encima deje caer la mezcla reservada<br />
             <br />
            •  Cocine al horno por 40 minutos a fuego mediano</p>
