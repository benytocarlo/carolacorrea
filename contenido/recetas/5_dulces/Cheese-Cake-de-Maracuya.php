	<h2>Cheese Cake de Maracuyá</h2>
      <h4>Ingredientes &quot;10 Personas&quot;</h4>
      <p>
        • 2 paquetes de galletas de  chocolate<br />
        • Taza de leche descremada<br />
        • 3 tazas de pulpa de maracuyá<br />
        • 2 sobres de gelatina sin  sabor<br />
        • 4 potes de yogurt natural<br />
        • 1 leche condensada 0% grasa<br />
        • 1 taza de frambuesas<br />
        • ½ taza de jugo de limón</p>
      <h4>Preparación:<br />
      </h4>
      <p>• Muela las galletas  en una procesadora y mézclelas con la leche<br />
        <br />
        • Cubra con las galletas la base de un  molde con ayuda de los dedos<br />
        <br />
        • Para el relleno, en un bol mezcle 2  tazas de pulpa de maracuyá con el yogurt, la gelatina sin sabor y la leche  condensada.<br />
        <br />
        • Vierta el relleno en el molde sobre  la base de galletas<br />
        <br />
        • Refrigere por un par de horas<br />
        <br />
        • Para la salsa, procese las frambuesas  junto al jugo de limón en una licuadora<br />
        <br />
        • Sirva el cheesecake acompañado de la salsa  de berries, báñelo con la salsa de maracuyá y decore con hojas de menta fresca</p>
