	<h2>Postre helado de frutillas con salsa de mandarinas</h2>
      <h4>Ingredientes (8 personas)</h4>
        <p>• &frac12; kilo de frutillas <br />
        • 1 tarro de leche evaporada<br />
        • &frac12; taza de jugo de limón<br />
        • 3 hojas de colapés<br />
        • 2 tazas de jugo de mandarinas<br />
        • 2 cucharadas de azúcar<br />
        • 1 cucharadita de maicena<br />  
      </p>
      <h4>Preparación:</h4>
      <p>• Batir la leche evaporada (debe estar fría)<br />
        <br />
        • Una vez que doble su tamaño, incorporar las frutillas molidas, el jugo de limón, la menta picada, la azúcar y el colapés previamente disuelto<br />
        <br />
        • Vaciar la mezcla en potes individuales o un molde familiar, previamente forrado en papel film y llevar al congelador por 4 horas<br />
        <br />
        • Para la salsa, dejar el jugo de las mandarinas en una olla con la azúcar. Agregar la maicena disuelta, dejar enfriar<br />
        <br />
        •  Servir el postre helado, bañado con la salsa de mandarinas y decorar con menta fresca<br /></p>
    </div>

