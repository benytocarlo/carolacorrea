    <h2>Flores con Agua de Color</h2>
    <p class="p_first">Para hacer un original arreglo y darle otro aspecto a la  mesa, busca dos floreros de tamaños distintos. Pon uno dentro del otro y  llénalos con agua teñida con anilina de distintos colores.</p>
    <p>&nbsp;</p> 
    <p>Elige una flor que  convine con los colores que elegiste para el agua. Te darás cuenta que es un  arreglo precioso y muy simple. </p>
