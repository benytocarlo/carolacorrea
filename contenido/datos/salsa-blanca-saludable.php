    <h2>Salsa Blanca Saludable</h2>
     <p class="p_first">En muchas de mis recetas, van a encontrar este ingrediente. Si queremos hacer comidas menos calóricas y bajas en grasas animales, les recomiendo hacer la salsa blanca con leche descremada y aceite vegetal en vez de mantequilla.<br />
               <br />
De esta forma estamos bajando en forma importante las grases saturadas.</p>
            <h4>La receta</h4>
               <p>En una olla caliente deje ½ taza de aceite vegetal, dos cucharadas de harina y revuelva hasta que se forme una bola y en ese momento comience a agregar &quot; de a poco&quot; un litro de leche descremada, revolviendo constantemente.<br />
               Se condimenta con sal, pimienta y nuez moscada.<br />
             </p>
