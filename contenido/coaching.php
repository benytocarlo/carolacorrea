	<h2>Coaching de Cocina para  Empresas</h2>
    <p class="p_first">Incluye:<br />
                               &#8226; Cócktail de bienvenida (4 bocados por persona)<br />
                                 &#8226; Clase de cocina (Aperitivo - Entrada - Plato de fondo - Postre)<br />
                                 &#8226; Comida <br />
                                 &#8226; Café <br />&#8226; Arriendo taller de cocina<br />
      							 &#8226; Maridaje con sommelier del Mundo del Vino (Vinos Reserva)<br />
    &#8226; Diploma para cada persona	</p>
    <p class="p_first">El taller consiste en preparar el menú completo.<br />
1.- Se montan mesas redondas y se sirve a cada uno el plato que se está cocinando.<br />
                                2.- Se hace un maridaje con la entrada, con el plato de fondo y con el postre.<br />
                                3.- Se sirve todo a la redonda con garzones.<br />
                                4.- Se hace una rifa:  2 vinos reserva<br />
                                5.- 1 Mini Blender de Philips<br />
                                6.- 1 Sartén T-Fal                             
    Tiempo de duración: 2  horas </p>
